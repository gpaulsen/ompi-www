<p>
I'll try the MIPS (aka MIPS32) solution - hey, it
<br>
can't kill anything. There are some subtle
<br>
differences, but most MIPS64 boards'll run MIPS32 code
<br>
(I think in a similar way to the way AMD's 64-bit
<br>
chips also run 32-bit code).
<br>
<p>However, for anyone planning a &quot;native&quot; MIPS64
<br>
solution, I've found a few technical documents that
<br>
seem to cover the material wanted:
<br>
<p>The basic MIPS64 (and MIPS32) specification,
<br>
instruction set, et al, can be found at:
<br>
<p><a href="http://www.mips.com/content/Documentation/MIPSDocumentation/ProcessorCores/doclibrary">http://www.mips.com/content/Documentation/MIPSDocumentation/ProcessorCores/doclibrary</a>
<br>
<p>Free registration is required to download the manuals.
<br>
<p>The Broadcom manual specifically for the SB1 chip and
<br>
the boards Broadcom uses them in can be found at:
<br>
<p><a href="http://sibyte.broadcom.com/public/resources/">http://sibyte.broadcom.com/public/resources/</a>
<br>
<p>Although I can't see this being overly useful for
<br>
OpenMPI, the SB1 also implements HyperTransport.
<br>
<p><a href="http://www.hypertransport.org/tech/tech_specs.cfm">http://www.hypertransport.org/tech/tech_specs.cfm</a>
<br>
<p>It might be useful if someone was writing some really
<br>
exotic stuff, though, and I've found it's almost
<br>
impossible to have too many manuals.
<br>
