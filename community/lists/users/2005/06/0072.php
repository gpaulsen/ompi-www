On Jun 15, 2005, at 6:07 PM, Jeff Squyres wrote:
<br>
<p><span class="quotelev2">&gt;&gt; bin/ompi_info presents an opportunity to help all us shlubs that
</span><br>
<span class="quotelev2">&gt;&gt; have to do gnu build systems.
</span><br>
<p>BTW, I forgot to mention -- try running &quot;ompi_info -all&quot; and/or 
<br>
&quot;ompi_info -all -parsable&quot;.  It is explicitly aimed at those who need 
<br>
to query the capabilities of an Open MPI installation, and potentially 
<br>
need to do so in an automated fashion (e.g., in a script, so the 
<br>
-parsable output is quite grep/sed/awk/cut friendly).
<br>
<p>Here's a general purpose question for users out there:
<br>
<p>The ompi_info command was directly derived from the LAM/MPI laminfo 
<br>
command.  However, I've never liked the fact that there's a &quot;_&quot; in the 
<br>
name.  Should it be renamed?  Options I see are:
<br>
<p>1. ompi_info (the current name)
<br>
2. ompi-info
<br>
3. ompiinfo
<br>
4. something else entirely
<br>
<p>This is a completely trivial issue, but it does bug me a little bit, so 
<br>
I figured I'd ask before the name becomes fixed for all time.  :-)
