<p>
Hi all,
<br>
<p>You might remember the very intriging discussions we had on a MPI ABI. 
<br>
During this discussion the idea of a 'MorphMPI' was launched 
<br>
(<a href="http://www.open-mpi.org/community/lists/users/2005/03/0028.php">http://www.open-mpi.org/community/lists/users/2005/03/0028.php</a>).
<br>
<p>I for one like the idea because it allows us to launch applications 
<br>
using MPI implementations that are different from the MPI implementation 
<br>
used for compiling the application.
<br>
<p>We are seriously looking into going this route but first I wanted to ask 
<br>
if anybody already implemented such a solution (and eventually 
<br>
open-source'd it).
<br>
