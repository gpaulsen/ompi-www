<p>
<span class="quotelev1">&gt; The reason to include it is that it would make integrating with batch
</span><br>
<span class="quotelev1">&gt; systems less of a headache for system administrators. Right now most
</span><br>
<span class="quotelev1">&gt; only support 1 MPI version because this is annoying.
</span><br>
<p><p>Easy integration with a batch system would be a huge bonus.  Also, the 
<br>
ability to test a new revision of the MPI library without having to 
<br>
recompile would be nice.  Should also allow program such as Totalview 
<br>
to be available on more platforms.
<br>
<p>One extra feature that an ABI would allow you to implement is the 
<br>
ability to use different MPI's in the same job.  Theoretically you 
<br>
could run a single job on a cluster with different nodes connected via 
<br>
different interconnects.  Sure, your performance might be whacky, but 
<br>
it allows more freedom in how you connect your cluster.
<br>
<p><span class="quotelev1">&gt; And if testing became easier, people might actually fix their code.
</span><br>
<span class="quotelev1">&gt; OK, *now* I'm dreaming... ;-)
</span><br>
<p>Yes, that is definitely dreaming ;)  Given that a lot of vendors even 
<br>
wince at change a dot-version in their compilers.
<br>
