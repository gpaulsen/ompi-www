<br>
<p><span class="quotelev1">&gt; I am developing an application that needs the FT features.  I cannot
</span><br>
<span class="quotelev1">&gt; find any evidence of support for it in OpenMPI.  Is the implementation
</span><br>
<span class="quotelev1">&gt; much changed from the UTK version?  Scheduled for a later release?
</span><br>
<span class="quotelev1">&gt; Searching the site for, for example, &quot;RESTARTED&quot; turned up nothing.
</span><br>
<p>The Open MPI 1.0 series does not contain fault tolerance features  
<br>
like those found in FT-MPI.  We are actively working on both  
<br>
implementing features similar to those found in LAM/MPI (system-level  
<br>
checkpoint/restart) and those found in FT-MPI.  We do not currently  
<br>
have a time table for releasing this work, but will announce when we  
<br>
are ready for users to start testing our work.
