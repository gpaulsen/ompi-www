Tony -
<br>
<p>It looks like you ran into a bug in Libtool.  Unfortunately, in order  
<br>
to better support Fortran 90 when building shared libraries, we use a  
<br>
beta version of Libtool 2, which means we're living a bit more on the  
<br>
edge than we'd like.  I have a patch for this issue that I'll be  
<br>
submitting to the Libtool developers shortly, so hopefully the  
<br>
problem will go away in the near future, definitely before 1.2b2.
<br>
<p>In the mean time, there is a mostly evil hack you can use to compile  
<br>
Open MPI 1.2b1 on 64 bit Mac OS X platforms (note: this is *NOT*  
<br>
needed if you are building 32 bit, and will break things if you try  
<br>
to build 32 bit).  After running Open MPI's configure script, edit  
<br>
the 'libtool' script that was generated by configure (it will be in  
<br>
the same directory where you ran configure) and change the one  
<br>
instance where the command 'otool' is invoked to be 'otool64'.
<br>
<p>Brian
