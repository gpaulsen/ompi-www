<p><p>===============================================================
<br>
CALL FOR PAPERS
<br>
<p><p>Workshop on Virtualization/Xen in High-Performance Cluster
<br>
and Grid Computing (XHPC'07)
<br>
<p>&nbsp;&nbsp;&nbsp;as part of The 16th IEEE International Symposium on High
<br>
&nbsp;&nbsp;&nbsp;Performance Distributed Computing (HPDC-16), June 27-29, 2007,
<br>
&nbsp;&nbsp;&nbsp;Monterey, California.
<br>
<p>&nbsp;&nbsp;and
<br>
<p>Workshop on Virtualization in High-Performance
<br>
Cluster and Grid Computing (VHPC'07)
<br>
<p>&nbsp;&nbsp;&nbsp;as part of The 13th International European Conference on Parallel
<br>
&nbsp;&nbsp;&nbsp;and Distributed Computing (Euro-Par 2007), August 28-31, 2007,
<br>
&nbsp;&nbsp;&nbsp;IRISA, Rennes, France.
<br>
<p><p>===============================================================
<br>
<p><p><p>Date: June 27-29, 2007 and August 28-31, 2007
<br>
<p><p>HPDC-16: <a href="http://www.isi.edu/hpdc2007/">http://www.isi.edu/hpdc2007/</a>
<br>
Euro-Par 2007: <a href="http://europar2007.irisa.fr/">http://europar2007.irisa.fr/</a>
<br>
Workshop URL: <a href="http://xhpc.wu-wien.ac.at/">http://xhpc.wu-wien.ac.at/</a>
<br>
<p>(due date for both: January 26, Abstracts Jan 12)
<br>
<p><p>Scope:
<br>
Virtual machine monitors are reaching wide-spread adoption
<br>
in a variety of operating systems as well as scientific educational
<br>
and operational usage areas. With their low overhead, hypervisors
<br>
allows for concurrently running large numbers of virtual machines,
<br>
providing each encapsulation, isolation and in the case of Xen,
<br>
network-wide CPU migratability. VMMs offer a network-wide
<br>
abstraction layer of individual machine resources to OS environments,
<br>
thereby opening whole new cluster-and grid high-performance
<br>
computing (HPC) architectures and HPC services options. With VMMs
<br>
finding applications in HPC environments, these workshops aim to
<br>
bring together researchers and practitioners active on virtualization
<br>
in distributed and high-performance cluster and grid computing
<br>
environments.
<br>
<p><p>The workshops will be one day in length, composed of 20 min paper
<br>
presentations, each followed by 10 min discussion sections.
<br>
Presentations may be accompanied with interactive demonstrations.
<br>
The workshop will end with a 30 min panel discussion by presenters.
<br>
<p><p><p>TOPICS
<br>
<p>Topics include, but are not limited to, the following subject matters:
<br>
<p>&nbsp;&nbsp;&nbsp;- Virtualization/Xen in cluster and grid environments
<br>
&nbsp;&nbsp;&nbsp;- Workload characterizations for VM-based clusters
<br>
&nbsp;&nbsp;&nbsp;- VM cluster and grid architectures
<br>
&nbsp;&nbsp;&nbsp;- Linux Hypervisors
<br>
&nbsp;&nbsp;&nbsp;- Cluster reliability, fault-tolerance, and security
<br>
&nbsp;&nbsp;&nbsp;- Compute job entry and scheduling
<br>
&nbsp;&nbsp;&nbsp;- Compute workload load leveling
<br>
&nbsp;&nbsp;&nbsp;- Cluster and grid filesystems for VMs
<br>
&nbsp;&nbsp;&nbsp;- VMMs, VMs and QoS guarantees
<br>
&nbsp;&nbsp;&nbsp;- Research and education use cases
<br>
&nbsp;&nbsp;&nbsp;- VM cluster distribution algorithms
<br>
&nbsp;&nbsp;&nbsp;- MPI, PVM  on virtual machines
<br>
&nbsp;&nbsp;&nbsp;- System sizing
<br>
&nbsp;&nbsp;&nbsp;- Hardware support for virtualization
<br>
&nbsp;&nbsp;&nbsp;- High-speed interconnects in Xen/hypervisors
<br>
&nbsp;&nbsp;&nbsp;- Hypervisor extensions and utilities for cluster and grid computing
<br>
&nbsp;&nbsp;&nbsp;- Network architectures for VM-based clusters
<br>
&nbsp;&nbsp;&nbsp;- VMMs/Hypervisors on large SMP machines
<br>
&nbsp;&nbsp;&nbsp;- Performance models
<br>
&nbsp;&nbsp;&nbsp;- Performance management and tuning hosts and guest VMs
<br>
&nbsp;&nbsp;&nbsp;- VMM performance tuning on various load types
<br>
&nbsp;&nbsp;&nbsp;- Xen/other VMM cluster/grid tools
<br>
&nbsp;&nbsp;&nbsp;- Device access from VMs
<br>
&nbsp;&nbsp;&nbsp;- Management, deployment of clusters and grid environments with VMs
<br>
<p><p><p>PAPER SUBMISSION
<br>
<p>Papers submitted to each workshop will be reviewed by at least two
<br>
members of the program committee and external reviewers. Submissions
<br>
should include abstract, key words, the e-mail address of the
<br>
corresponding author, and must not exceed 10 pages, including tables
<br>
and figures. Submission of a paper should be regarded as a
<br>
commitment that, should the paper be accepted, at least one of the
<br>
authors will register and attend the conference to present the work.
<br>
An award for best student paper will be given. Papers can only be
<br>
submitted for one workshop, but not both at the same time.
<br>
<p>XHPC'07:
<br>
<p>Accepted papers will be included in proceedings published by the
<br>
IEEE Computer Society Press.
<br>
<p>The IEEE 8 1/2 x 11 CS format must be used. Submission can be in
<br>
either PDF or Postscript.
<br>
<p><a href="ftp://pubftp.computer.org/Press/Outgoing/proceedings/">ftp://pubftp.computer.org/Press/Outgoing/proceedings/</a>
<br>
<p><p>VHPC'07:
<br>
<p>Accepted papers will be included in proceedings published in the
<br>
Springer LNCS series.
<br>
<p>The format must be according to the Springer LNCS Style and
<br>
preferably be in LaTeX or FrameMaker, although submissions
<br>
using the LNCS Word template are also possible. Initial
<br>
submissions are in PDF, accepted papers will be requested
<br>
to provided source files.
<br>
<p><a href="http://www.springer.de/comp/lncs/authors.html">http://www.springer.de/comp/lncs/authors.html</a>
<br>
<p><p>Submission Link:
<br>
<a href="https://edas.info/newPaper.php?c=5189&amp;submit=0">https://edas.info/newPaper.php?c=5189&amp;submit=0</a>&amp;;
<br>
(add either XHPC: or VHPC: in the title of the paper to designate the
<br>
target workshop)
