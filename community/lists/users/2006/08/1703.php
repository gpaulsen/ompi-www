<p>
I do a lot of testing and development on my MacBook Pro in OSX (core duo).
<br>
I do a lot of development on a lot of dual-processor Linux machines as well.
<br>
Other developers work on multi-processor machines in other operating
<br>
systems.
<br>
<p>Open MPI doesn't depend on how many CPUs or cores you have in your machine.
<br>
You should be able to install it on most POSIX-based platforms (we generally
<br>
limit ourselves to platforms/compilers that the GNU Auto tools support --
<br>
Autoconf, Automake, and Libtool.  That's a pretty wide range, actually).
