Unfortunately, there is no MPI analog to PVM's pstat().  MPI lacks some of
<br>
the explicit fault tolerance mechanisms that PVM has.  Fault tolerance is an
<br>
issue left open to quality of implementation.  See
<br>
<a href="http://www.open-mpi.org/faq/?category=ft">http://www.open-mpi.org/faq/?category=ft</a> for the current state of fault
<br>
tolerance in Open MPI.
