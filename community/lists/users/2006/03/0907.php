<div style='background-color:'><DIV class=RTE>Hi Allan, i ran hpbench on a cluster of two machines having just convetional PCI bus and i didnt find any performance improvement. On the contrary the performace degraded. I figured out it may be because conventional PCI bus (33 MHz, 32 bits) provides a bandwidth with 33x32 = 1056 Mbps, hence just one 1000 Mpbs NIC Card is enough to saturate the bandwidth.</DIV>
<DIV class=RTE>&nbsp;</DIV>
<DIV class=RTE>If you need enhanced performance, you need to get PCI-X (100/133, 62 bits) based machines.</DIV>
