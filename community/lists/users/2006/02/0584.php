mlistus,
<br>
<p>Thank you for your question. The integration of fault tolerant  
<br>
techniques into Open MPI is under active development at the moment.  
<br>
As the site notes, we are scheduled to incorporate many forms of  
<br>
fault tolerance into Open MPI which were previously supported by our  
<br>
core projects, and likely some new methods as we go along.
<br>
So we will support:
<br>
&nbsp;&nbsp;- FT-MPI: user directed, and communicator driven fault tolerance
<br>
&nbsp;&nbsp;- LA-MPI: data reliability, and network fault tolerance
<br>
&nbsp;&nbsp;- LAM/MPI: process fault tolerance by checkpoint and restart
<br>
<p>I cannot state a timeline for the completion of any of these styles,  
<br>
but I can say they are in active development. Look for updates on  
<br>
this users list, and on the website.
<br>
<p>Cheers,
<br>
Josh
