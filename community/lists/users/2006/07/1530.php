<p>===============================================================
<br>
CALL FOR PAPERS (XHPC'06)
<br>
<p>Workshop on XEN in High-Performance Cluster and Grid Computing
<br>
Environments as part of:
<br>
<p>The Fourth International Symposium on Parallel and Distributed
<br>
Processing and Applications (ISPA'2006). Sorrento, Italy
<br>
===============================================================
<br>
<p>Date: 1-4 December 2006
<br>
<p>ISPA'2006:  <a href="http://www.ispa-conference.org/2006/">http://www.ispa-conference.org/2006/</a>
<br>
Workshop URL: <a href="http://xhpc.ai.wu-wien.ac.at/ws/">http://xhpc.ai.wu-wien.ac.at/ws/</a>
<br>
<p>(due date: August 4, Abstracts Jul 17)
<br>
<p><p>Scope:
<br>
The Xen virtual machine monitor is reaching wide-spread adoption
<br>
in a variety of operating systems as well as scientific educational
<br>
and operational usage areas. With its low overhead, Xen allows for
<br>
concurrently running large numbers of virtual machines, providing
<br>
each encapsulation, isolation and network-wide CPU migratability.
<br>
Xen offers a network-wide abstraction layer of individual machine
<br>
resources to OS environments, thereby opening whole new cluster-and
<br>
grid high-performance computing (HPC) architectures and HPC services
<br>
options. With Xen finding applications in HPC environments, this
<br>
workshop aims to bring together researchers and practitioners active
<br>
on Xen in high-performance cluster and grid computing environments.
<br>
<p>The workshop will be one day in length, composed of 20 min paper
<br>
presentations, each followed by 10 min discussion sections.
<br>
Presentations may be accompanied with interactive demonstrations.
<br>
The workshop will end with a 30 min panel discussion by presenters.
<br>
<p><p>TOPICS
<br>
<p>Topics include, but are not limited to, the following subject matters:
<br>
<p>&nbsp;&nbsp;&nbsp;- Xen in cluster and grid environments
<br>
&nbsp;&nbsp;&nbsp;- Workload characterizations for Xen-based clusters
<br>
&nbsp;&nbsp;&nbsp;- Xen cluster and grid architectures
<br>
&nbsp;&nbsp;&nbsp;- Cluster reliability, fault-tolerance, and security
<br>
&nbsp;&nbsp;&nbsp;- Compute job entry and scheduling
<br>
&nbsp;&nbsp;&nbsp;- Compute workload load levelling
<br>
&nbsp;&nbsp;&nbsp;- Cluster and grid filesystems for Xen
<br>
&nbsp;&nbsp;&nbsp;- Research and education use cases
<br>
&nbsp;&nbsp;&nbsp;- VM cluster distribution algorithms
<br>
&nbsp;&nbsp;&nbsp;- MPI, PVM  on virtual machines
<br>
&nbsp;&nbsp;&nbsp;- System sizing
<br>
&nbsp;&nbsp;&nbsp;- High-speed interconnects in Xen
<br>
&nbsp;&nbsp;&nbsp;- Xen extensions and utilities for cluster and grid computing
<br>
&nbsp;&nbsp;&nbsp;- Network architectures for Xen clusters
<br>
&nbsp;&nbsp;&nbsp;- Xen on large SMP machines
<br>
&nbsp;&nbsp;&nbsp;- Measuring performance
<br>
&nbsp;&nbsp;&nbsp;- Performance tuning of Xen domains
<br>
&nbsp;&nbsp;&nbsp;- Xen performance tuning on various load types
<br>
&nbsp;&nbsp;&nbsp;- Xen cluster/grid tools
<br>
&nbsp;&nbsp;&nbsp;- Management of Xen clusters
<br>
<p><p>PAPER SUBMISSION
<br>
<p>Papers submitted to each workshop will be reviewed by at least three
<br>
members of the program committee and external reviewers. Submissions
<br>
should include abstract, key words, the e-mail address of the
<br>
corresponding author, and must not exceed 15 pages, including tables
<br>
and figures, and preferably be in LaTeX or FrameMaker, although
<br>
submissions in the LNCS Word format will be accepted as well.
<br>
Electronic submission through the submission website is strongly
<br>
encouraged. Hardcopies will be accepted only if electronic submission
<br>
is not possible. Submission of a paper should be regarded as a
<br>
commitment that, should the paper be accepted, at least one of the
<br>
authors will register and attend the conference to present the work.
<br>
An award for best student paper will be given.
<br>
<p><a href="https://edas.info/newPaper.php?c=4945&amp;submit=0">https://edas.info/newPaper.php?c=4945&amp;submit=0</a>&amp;
<br>
<p>Format should be according to the Springer LNCS Style
<br>
<a href="http://www.springer.de/comp/lncs/authors.html">http://www.springer.de/comp/lncs/authors.html</a>
<br>
<p>It is expected that the proceedings of the workshop programs will
<br>
be published by Springer's LNCS series or IEEE CS.
<br>
