</span><br>
<p>Please check the wiki page with detailed instructions of using it : 
<br>
<a href="https://svn.open-mpi.org/trac/ompi/wiki/WindowsPort#HowtobuildOpenMPIonWindowsusingCMake">https://svn.open-mpi.org/trac/ompi/wiki/WindowsPort#HowtobuildOpenMPIonWindowsusingCMake</a> 
<br>
