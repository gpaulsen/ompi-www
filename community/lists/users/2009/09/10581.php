</span><br>
<span class="quotelev1">&gt;   
</span><br>
<p>It should work, but the build process is complex and time consuming. I 
<br>
wouldn't recommend it.
<br>
<p><span class="quotelev1">&gt; Is there a version of openmpi that works under windows vista in some
</span><br>
<span class="quotelev1">&gt; other environment?
</span><br>
<span class="quotelev1">&gt;   
</span><br>
<p>Yes, with CMake and Visual Studio.
<br>
<span class="quotelev1">&gt; If the answer to either of these is yes, is there a set of
</span><br>
<span class="quotelev1">&gt; instructions that applies to this isntallation?
