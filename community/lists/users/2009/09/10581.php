<p>It's very difficult to build Open MPI under cygwin, there was a thread 
<br>
talking about this before. See: 
<br>
<a href="http://www.open-mpi.org/community/lists/users/2008/11/7294.php">http://www.open-mpi.org/community/lists/users/2008/11/7294.php</a>
<br>
