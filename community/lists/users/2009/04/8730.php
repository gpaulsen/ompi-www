<p>2009/4/2 George Bosilca &lt;bosilca_at_[hidden]&gt;:
<br>
<span class="quotelev1">&gt; The Open MPI Team, representing a consortium of bailed-out banks, car
</span><br>
<span class="quotelev1">&gt; manufacturers, and insurance companies, is pleased to announce the
</span><br>
<span class="quotelev1">&gt; release of the &quot;unbreakable&quot; / bug-free version Open MPI 2009,
</span><br>
<span class="quotelev1">&gt; (expected to be available by mid-2011). &#160;This release is essentially a
</span><br>
<span class="quotelev1">&gt; complete rewrite of Open MPI based on new technologies such as C#,
</span><br>
<span class="quotelev1">&gt; Java, and object-oriented Cobol (so say we all!). &#160;Buffer overflows
</span><br>
<span class="quotelev1">&gt; and memory leaks are now things of the past. &#160;We strongly recommend
</span><br>
<span class="quotelev1">&gt; that all users upgrade to Windows 7 to fully take advantage of the new
</span><br>
<span class="quotelev1">&gt; powers embedded in Open MPI.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; This version can be downloaded from the The Onion web site or from
</span><br>
<span class="quotelev1">&gt; many BitTorrent networks (seeding now; the Open MPI ISO is
</span><br>
<span class="quotelev1">&gt; approximately 3.97GB -- please wait for the full upload).
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Here is an abbreviated list of changes in Open MPI 2009 as compared to
</span><br>
<span class="quotelev1">&gt; the previous version:
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; - Dropped support for MPI 2 in favor of the newly enhanced MPI 11.7
</span><br>
<span class="quotelev1">&gt; &#160;standard. &#160;MPI_COOK_DINNER support is only available with additional
</span><br>
<span class="quotelev1">&gt; &#160;equipment (some assembly may be required). &#160;An experimental PVM-like
</span><br>
<span class="quotelev1">&gt; &#160;API has been introduced to deal with the current limitations of the
</span><br>
<span class="quotelev1">&gt; &#160;MPI 11.7 API.
</span><br>
<span class="quotelev1">&gt; - Added a Twitter network transport capable of achieving peta-scale
</span><br>
<span class="quotelev1">&gt; &#160;per second bandwidth (but only on useless data).
</span><br>
<span class="quotelev1">&gt; - Dropped support for the barely-used x86 and x86_64 architectures in
</span><br>
<span class="quotelev1">&gt; &#160;favor of the most recent ARM6 architecture. &#160;As a direct result,
</span><br>
<span class="quotelev1">&gt; &#160;several Top500 sites are planning to convert from their now obsolete
</span><br>
<span class="quotelev1">&gt; &#160;peta-scale machines to high-reliability iPhone clusters using the
</span><br>
<span class="quotelev1">&gt; &#160;low-latency AT&amp;T 3G network.
</span><br>
<span class="quotelev1">&gt; - The iPhone iMPI app (powered by iOpen MPI) is now downloadable from
</span><br>
<span class="quotelev1">&gt; &#160;the iTunes Store. &#160;Blackberry support will be included in a future
</span><br>
<span class="quotelev1">&gt; &#160;release.
</span><br>
<span class="quotelev1">&gt; - Fix all compiler errors related to the PGI 8.0 compiler by
</span><br>
<span class="quotelev1">&gt; &#160;completely dropping support.
</span><br>
<span class="quotelev1">&gt; - Add some &quot;green&quot; features for energy savings. &#160;The new &quot;--bike&quot;
</span><br>
<span class="quotelev1">&gt; &#160;mpirun option will only run your parallel jobs only during the
</span><br>
<span class="quotelev1">&gt; &#160;operation hours of the official Open MPI biking team. &#160;The
</span><br>
<span class="quotelev1">&gt; &#160;&quot;--preload-result&quot; option will directly embed the final result in
</span><br>
<span class="quotelev1">&gt; &#160;the parallel execution, leading to more scalable and reliable runs
</span><br>
<span class="quotelev1">&gt; &#160;and decreasing the execution time of any parallel application under
</span><br>
<span class="quotelev1">&gt; &#160;the real-time limit of 1 second. &#160;Open MPI is therefore EnergyStar
</span><br>
<span class="quotelev1">&gt; &#160;compliant when used with these options.
</span><br>
<span class="quotelev1">&gt; - In addition to moving Open MPI's lowest point-to-point transports to
</span><br>
<span class="quotelev1">&gt; &#160;be an external project, limited support will be offered for
</span><br>
<span class="quotelev1">&gt; &#160;industry-standard platforms. &#160;Our focus will now be to develop
</span><br>
<span class="quotelev1">&gt; &#160;highly scalable transports based on widely distributed technologies
</span><br>
<span class="quotelev1">&gt; &#160;such as SMTP, High Performance Gopher (v3.8 and later), OLE COMM,
</span><br>
<span class="quotelev1">&gt; &#160;RSS/Atom, DNS, and Bonjour.
</span><br>
<span class="quotelev1">&gt; - Opportunistic integration with Conflicker in order to utilize free
</span><br>
<span class="quotelev1">&gt; &#160;resources distributed world-wide.
</span><br>
<span class="quotelev1">&gt; - Support for all Fortran versions prior to Fortran 2020 has been
</span><br>
<span class="quotelev1">&gt; &#160;dropped.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Make today an Open MPI day!
</span><br>
