Ankush Kaul wrote:
<br>
<p><span class="quotelev1">&gt; Finally, after mentioning the hostfiles the cluster is working fine. 
</span><br>
<span class="quotelev1">&gt; We downloaded few benchmarking softwares but i would like to know if 
</span><br>
