<p>
The Open MPI Team, representing a consortium of bailed-out banks, car
<br>
manufacturers, and insurance companies, is pleased to announce the
<br>
release of the &quot;unbreakable&quot; / bug-free version Open MPI 2009,
<br>
(expected to be available by mid-2011).  This release is essentially a
<br>
complete rewrite of Open MPI based on new technologies such as C#,
<br>
Java, and object-oriented Cobol (so say we all!).  Buffer overflows
<br>
and memory leaks are now things of the past.  We strongly recommend
<br>
that all users upgrade to Windows 7 to fully take advantage of the new
<br>
powers embedded in Open MPI.
<br>
<p>This version can be downloaded from the The Onion web site or from
<br>
many BitTorrent networks (seeding now; the Open MPI ISO is
<br>
approximately 3.97GB -- please wait for the full upload).
<br>
<p>Here is an abbreviated list of changes in Open MPI 2009 as compared to
<br>
the previous version:
<br>
<p>- Dropped support for MPI 2 in favor of the newly enhanced MPI 11.7
<br>
&nbsp;&nbsp;standard.  MPI_COOK_DINNER support is only available with additional
<br>
&nbsp;&nbsp;equipment (some assembly may be required).  An experimental PVM-like
<br>
&nbsp;&nbsp;API has been introduced to deal with the current limitations of the
<br>
&nbsp;&nbsp;MPI 11.7 API.
<br>
- Added a Twitter network transport capable of achieving peta-scale
<br>
&nbsp;&nbsp;per second bandwidth (but only on useless data).
<br>
- Dropped support for the barely-used x86 and x86_64 architectures in
<br>
&nbsp;&nbsp;favor of the most recent ARM6 architecture.  As a direct result,
<br>
&nbsp;&nbsp;several Top500 sites are planning to convert from their now obsolete
<br>
&nbsp;&nbsp;peta-scale machines to high-reliability iPhone clusters using the
<br>
&nbsp;&nbsp;low-latency AT&amp;T 3G network.
<br>
- The iPhone iMPI app (powered by iOpen MPI) is now downloadable from
<br>
&nbsp;&nbsp;the iTunes Store.  Blackberry support will be included in a future
<br>
&nbsp;&nbsp;release.
<br>
- Fix all compiler errors related to the PGI 8.0 compiler by
<br>
&nbsp;&nbsp;completely dropping support.
<br>
- Add some &quot;green&quot; features for energy savings.  The new &quot;--bike&quot;
<br>
&nbsp;&nbsp;mpirun option will only run your parallel jobs only during the
<br>
&nbsp;&nbsp;operation hours of the official Open MPI biking team.  The
<br>
&nbsp;&nbsp;&quot;--preload-result&quot; option will directly embed the final result in
<br>
&nbsp;&nbsp;the parallel execution, leading to more scalable and reliable runs
<br>
&nbsp;&nbsp;and decreasing the execution time of any parallel application under
<br>
&nbsp;&nbsp;the real-time limit of 1 second.  Open MPI is therefore EnergyStar
<br>
&nbsp;&nbsp;compliant when used with these options.
<br>
- In addition to moving Open MPI's lowest point-to-point transports to
<br>
&nbsp;&nbsp;be an external project, limited support will be offered for
<br>
&nbsp;&nbsp;industry-standard platforms.  Our focus will now be to develop
<br>
&nbsp;&nbsp;highly scalable transports based on widely distributed technologies
<br>
&nbsp;&nbsp;such as SMTP, High Performance Gopher (v3.8 and later), OLE COMM,
<br>
&nbsp;&nbsp;RSS/Atom, DNS, and Bonjour.
<br>
- Opportunistic integration with Conflicker in order to utilize free
<br>
&nbsp;&nbsp;resources distributed world-wide.
<br>
- Support for all Fortran versions prior to Fortran 2020 has been
<br>
&nbsp;&nbsp;dropped.
<br>
<p>Make today an Open MPI day!
