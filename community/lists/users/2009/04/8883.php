Hi Ankush
<br>
<p>To test if OpenMPI works, compile and run the examples (hello_c, etc)
<br>
in the  examples/ directory (on the directory where you decompressed
<br>
the OpenMPI tarball, not where you installed OpenMPI).
<br>
Compile them with mpicc, etc, and run them with mpiexec,
<br>
all from OpenMPI.
<br>
Using full path names help avoid confusion with other
<br>
MPI flavors.
<br>
<p>One MPI benchmark available free from Intel:
<br>
<p><a href="http://www.intel.com/cd/software/products/asmo-na/eng/219848.htm">http://www.intel.com/cd/software/products/asmo-na/eng/219848.htm</a>
<br>
<p>There may be others though.
