Yes, perhaps.  Note that there really are lots of options out there.  
<br>
Spend a few minutes googling.  There are tools, surveys of tools, etc.  
<br>
Some of the dimensions in this space include:
<br>
<p>* profiling (aggregate information) vs tracing (record specific events 
<br>
in execution history)
<br>
* quality of MPI support (e.g., your original question about gprof is 
<br>
basically, &quot;Can I just take a familiar tool and use it for a 
<br>
multi-process MPI job?&quot;)
<br>
* cost of licensing (&quot;free&quot; vs &quot;commercial&quot;)
<br>
* data collection vs expert analysis (some tools try to digest the data 
<br>
and turn it into more usable/understandable/actionable terms)
<br>
<p>Anyhow, there's more to it than just gprof, Studio, and vprof (and 
<br>
Allinea OPT and TAU and ...).  I guess the subject deserves a FAQ entry, 
<br>
and the trick will be to tame the answer to something manageable.
