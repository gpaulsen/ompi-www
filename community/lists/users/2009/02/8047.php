On Feb 12, 2009, at 1:30 PM, George Bosilca wrote:
<br>
<p><span class="quotelev1">&gt; Some of the configure options changed their name for the 1.3. Here  
</span><br>
<span class="quotelev1">&gt; is the updated list you should use in order to get valgrind support  
</span><br>
<span class="quotelev1">&gt; for the 1.3:
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; --enable-memchecker --with-valgrind --with-memory-manager=none
</span><br>
<p>Note that --enable-memchecker and --with-valgrind are new options for  
<br>
the v1.3 series; that support didn't exist in the v1.2 series.
<br>
<p>And just to be clear, --without-memory-manager is the same as --with- 
<br>
memory-manager=none.
