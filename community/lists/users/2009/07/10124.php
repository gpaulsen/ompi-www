<p>
This mailing list supports the Open MPI implementation of the MPI  
<br>
standard. If you have concerns about Intel MPI you should contact  
<br>
their support group.
<br>
<p>The ompi_checkpoint/ompi_restart routines are designed to work with  
<br>
Open MPI, and will certainly fail when used with other MPI  
<br>
implementations due to library and protocol disparities.
<br>
<p>If you are interested in how checkpoint/restart is supported in Open  
<br>
MPI, I suggest looking at the User's Guide posted on the wiki page  
<br>
below:
<br>
&nbsp;&nbsp;&nbsp;<a href="https://svn.open-mpi.org/trac/ompi/wiki/ProcessFT_CR">https://svn.open-mpi.org/trac/ompi/wiki/ProcessFT_CR</a>
<br>
