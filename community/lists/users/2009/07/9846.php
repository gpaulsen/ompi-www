</span><br>
<p><p>I've used Boost MPI before and it really isn't that bad and shouldn't be seen as 
<br>
&quot;just another library&quot;.  Many parts of Boost are on their way to being part of 
<br>
the standard and are discussed and debated on.  And so, it isn't the same as 
<br>
going to some random person's web page and downloading their library/template. 
<br>
Of course, it takes time to make it into the standard and I'm not entirely sure 
<br>
if everything will (probably not).
<br>
<p>(One &quot;annoying&quot; thing about Boost MPI is that you have to compile it...if you 
<br>
are distributing your code, end-users might find that bothersome...oh, and 
<br>
serialization as well.)
<br>
<p>One suggestion might be to make use of Boost and once you got your code working, 
<br>
&nbsp;&nbsp;start changing it back.  At least you will have a working program to compare 
<br>
against.  Kind of like writing a prototype first...
