<br>
<p><p>Luis Vitorio Cargnini wrote:
<br>
<span class="quotelev1">&gt; Thanks, but I really do not want to use Boost.
</span><br>
<span class="quotelev1">&gt; Is easier ? certainly is, but I want to make it using only MPI itself 
</span><br>
<span class="quotelev1">&gt; and not been dependent of a Library, or templates like the majority of 
</span><br>
<span class="quotelev1">&gt; boost a huge set of templates and wrappers for different libraries, 
</span><br>
<span class="quotelev1">&gt; implemented in C, supplying a wrapper for C++.
</span><br>
<span class="quotelev1">&gt; I admit Boost is a valuable tool, but in my case, as much independent I 
</span><br>
