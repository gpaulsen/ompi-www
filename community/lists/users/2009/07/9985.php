</span><br>
<span class="quotelev2">&gt; 	&gt; Version 1.3.3 can be downloaded from the main Open MPI web
</span><br>
<span class="quotelev2">&gt; 	&gt; site or any of its mirrors (mirrors will be updating shortly).
</span><br>
<span class="quotelev2">&gt; 	&gt;
</span><br>
<span class="quotelev2">&gt; 	&gt; Here is a list of changes in v1.3.3 as compared to v1.3.2:
</span><br>
<span class="quotelev2">&gt; 	&gt;
</span><br>
<span class="quotelev2">&gt; 	&gt; - Fix a number of issues with the openib BTL 
</span><br>
<span class="quotelev1">&gt; (OpenFabrics) RDMA CM,
</span><br>
<span class="quotelev2">&gt; 	&gt;    including a memory corruption bug, a shutdown 
</span><br>
<span class="quotelev1">&gt; deadlock, and a route
</span><br>
<span class="quotelev2">&gt; 	&gt;    timeout.  Thanks to David McMillen and Hal 
</span><br>
<span class="quotelev1">&gt; Rosenstock for help in
</span><br>
<span class="quotelev2">&gt; 	&gt;    tracking down the issues.
</span><br>
<span class="quotelev2">&gt; 	&gt; - Change the behavior of the EXTRA_STATE parameter 
</span><br>
<span class="quotelev1">&gt; that is passed to
</span><br>
<span class="quotelev2">&gt; 	&gt;    Fortran attribute callback functions: this value 
</span><br>
<span class="quotelev1">&gt; is now stored
</span><br>
<span class="quotelev2">&gt; 	&gt;    internally in MPI -- it no longer references the 
</span><br>
<span class="quotelev1">&gt; original value
</span><br>
<span class="quotelev2">&gt; 	&gt;    passed by MPI_*_CREATE_KEYVAL.
</span><br>
<span class="quotelev2">&gt; 	&gt; - Allow the overriding RFC1918 and RFC3330 for the 
</span><br>
<span class="quotelev1">&gt; specification of
</span><br>
<span class="quotelev2">&gt; 	&gt;    &quot;private&quot; networks, thereby influencing Open MPI's TCP
</span><br>
<span class="quotelev2">&gt; 	&gt;    &quot;reachability&quot; computations.
</span><br>
<span class="quotelev2">&gt; 	&gt; - Improve flow control issues in the sm btl, by both 
</span><br>
<span class="quotelev1">&gt; tweaking the
</span><br>
<span class="quotelev2">&gt; 	&gt;    shared memory progression rules and by enabling the &quot;sync&quot;
</span><br>
<span class="quotelev2">&gt; 	&gt; collective
</span><br>
<span class="quotelev2">&gt; 	&gt;    to barrier every 1,000th collective.
</span><br>
<span class="quotelev2">&gt; 	&gt; - Various fixes for the IBM XL C/C++ v10.1 compiler.
</span><br>
<span class="quotelev2">&gt; 	&gt; - Allow explicit disabling of ptmalloc2 hooks at 
</span><br>
<span class="quotelev1">&gt; runtime (e.g., enable
</span><br>
<span class="quotelev2">&gt; 	&gt;    support for Debain's builtroot system).  Thanks to 
</span><br>
<span class="quotelev1">&gt; Manuel Prinz and
</span><br>
<span class="quotelev2">&gt; 	&gt;    the rest of the Debian crew for helping identify and fix
</span><br>
<span class="quotelev2">&gt; 	&gt; this issue.
</span><br>
<span class="quotelev2">&gt; 	&gt; - Various minor fixes for the I/O forwarding subsystem.
</span><br>
<span class="quotelev2">&gt; 	&gt; - Big endian iWARP fixes in the Open Fabrics RDMA CM support.
</span><br>
<span class="quotelev2">&gt; 	&gt; - Update support for various OpenFabrics devices in 
</span><br>
<span class="quotelev1">&gt; the openib BTL's
</span><br>
<span class="quotelev2">&gt; 	&gt;    .ini file.
</span><br>
<span class="quotelev2">&gt; 	&gt; - Fixed undefined symbol issue with Open MPI's 
</span><br>
<span class="quotelev1">&gt; parallel debugger
</span><br>
<span class="quotelev2">&gt; 	&gt;    message queue support so it can be compiled by Sun Studio
</span><br>
<span class="quotelev2">&gt; 	&gt; compilers.
</span><br>
<span class="quotelev2">&gt; 	&gt; - Update MPI_SUBVERSION to 1 in the Fortran bindings.
</span><br>
<span class="quotelev2">&gt; 	&gt; - Fix MPI_GRAPH_CREATE Fortran 90 binding.
</span><br>
<span class="quotelev2">&gt; 	&gt; - Fix MPI_GROUP_COMPARE behavior with regards to 
</span><br>
<span class="quotelev1">&gt; MPI_IDENT.  Thanks to
</span><br>
<span class="quotelev2">&gt; 	&gt;    Geoffrey Irving for identifying the problem and 
</span><br>
<span class="quotelev1">&gt; supplying the fix.
</span><br>
<span class="quotelev2">&gt; 	&gt; - Silence gcc 4.1 compiler warnings about type 
</span><br>
<span class="quotelev1">&gt; punning.  Thanks to
</span><br>
<span class="quotelev2">&gt; 	&gt;    Number Cruncher for the fix.
</span><br>
<span class="quotelev2">&gt; 	&gt; - Added more Valgrind and other memory-cleanup fixes. 
</span><br>
<span class="quotelev1">&gt;  Thanks to
</span><br>
<span class="quotelev2">&gt; 	&gt;    various Open MPI users for help with these issues.
</span><br>
<span class="quotelev2">&gt; 	&gt; - Miscellaneous VampirTrace fixes.
</span><br>
<span class="quotelev2">&gt; 	&gt; - More fixes for openib credits in heavy-congestion scenarios.
</span><br>
<span class="quotelev2">&gt; 	&gt; - Slightly decrease the latency in the openib BTL in 
</span><br>
<span class="quotelev1">&gt; some conditions
</span><br>
<span class="quotelev2">&gt; 	&gt;    (add &quot;send immediate&quot; support to the openib BTL).
</span><br>
<span class="quotelev2">&gt; 	&gt; - Ensure to allow MPI_REQUEST_GET_STATUS to accept an
</span><br>
<span class="quotelev2">&gt; 	&gt;    MPI_STATUS_IGNORE parameter.  Thanks to Shaun 
</span><br>
<span class="quotelev1">&gt; Jackman for the bug
</span><br>
<span class="quotelev2">&gt; 	&gt;    report.
</span><br>
<span class="quotelev2">&gt; 	&gt; - Added Microsoft Windows support.  See 
</span><br>
<span class="quotelev1">&gt; README.WINDOWS file for
</span><br>
<span class="quotelev2">&gt; 	&gt;    details.
</span><br>
