<p>
Greetings Open MPI users and system administrators.
<br>
<p>In response to user feedback, Open MPI is changing how its releases will be numbered.
<br>
<p>In short, Open MPI will no longer be released using an &quot;odd/even&quot; cadence corresponding to &quot;feature development&quot; and &quot;super stable&quot; releases.  Instead, each of the &quot;A.B.C&quot; digits in the Open MPI version number will have a specific meaning, conveying semantic information in the overall release number.
<br>
<p>The details of this scheme, and our roadmap plans for releases over the next several months, are included in the form of slides, available here:
<br>
<p>* On my blog:
<br>
&nbsp;&nbsp;<a href="http://blogs.cisco.com/performance/open-mpi-new-versioning-scheme-and-roadmap">http://blogs.cisco.com/performance/open-mpi-new-versioning-scheme-and-roadmap</a>
<br>
<p>* On the Open MPI web site:
<br>
&nbsp;&nbsp;<a href="http://www.open-mpi.org/~jsquyres/www.open-mpi.org/papers/versioning-update-2015/">http://www.open-mpi.org/~jsquyres/www.open-mpi.org/papers/versioning-update-2015/</a>
