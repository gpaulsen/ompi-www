<p>
Unfortunately, we have a few apps which use LAM/MPI instead of OpenMPI (and this is something I have NO control over).   I have been making an effort to try and convince those who handle such apps to move over to LAM/MPI as it is (as I understand it) no longer supported and end-of-life.   In fact, the website www.lam-mpi-org&lt;<a href="http://www.lam-mpi-org">http://www.lam-mpi-org</a>&gt; is no longer accessible as the domain name expired on 2/20/2015.
<br>
<p>Aside from a change of library paths in the Makefiles, I am assuming that hardly any code changes would be needed for them to rebuild and run the code with OpenMPI.  Comments on this?
<br>
<p>Yeah, any other recommendations I can give to convince the powers-that-be that immediate sun-setting of LAM/MPI would be great.   Sometimes I feel like I am trying to fit a square peg in a round hole :(
