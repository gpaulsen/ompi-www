<p>
Also, with regard to your subject line, there are a wide variety of options for connecting to data.  
<br>
Everything from `redis'  (<a href="http://redis.io/">http://redis.io/</a>, <a href="http://cran.r-project.org/web/packages/rredis/index.html">http://cran.r-project.org/web/packages/rredis/index.html</a>) 
<br>
to HDF5 (<a href="http://cran.r-project.org/web/packages/hdf5/index.html">http://cran.r-project.org/web/packages/hdf5/index.html</a>),
<br>
to memory mapped files (<a href="http://cran.r-project.org/web/packages/mmap/index.html">http://cran.r-project.org/web/packages/mmap/index.html</a>), 
<br>
to RDBMSs (<a href="http://cran.r-project.org/web/packages/RODBC/index.html">http://cran.r-project.org/web/packages/RODBC/index.html</a>).
<br>
<p>With regard to the latter, just slam down some $$$ for a <a href="http://www.oracle.com/us/products/database/exadata/overview/index.html">http://www.oracle.com/us/products/database/exadata/overview/index.html</a>
<br>
and you're good to go.  (Wondering if one of these days DB2 or Oracle appliance will be the next big thing in parallel filesystems.)
