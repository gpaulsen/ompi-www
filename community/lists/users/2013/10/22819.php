<p>
We've been asked several times over the past few years if we would ever make more Open MPI logo shirts.  Making those shirts was always a giant logistical hassle in the past: get quotes from printers, gather orders, get the shirts made, collect payments, distribute the shirts, ...etc.
<br>
<p>But now it seems that the interwebs have solved this problem for us -- w00t!
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.cafepress.com/openmpi">http://www.cafepress.com/openmpi</a>
<br>
<p>We put up a bunch of shirts, sweatshirts, and some other random stuff (e.g., keychains) that CafePress pre-selected for us in a bundle.  The pictures mostly show black, grey, and white items, but if you click through, a bunch of them have multiple colors available.
<br>
<p>To be clear: we're doing this because a) it's fun, b) I personally need a new Open MPI shirt (my old ones are wearing out), and c) people have asked for it.  
<br>
<p>We're NOT making any money off this -- we set the markup value to 0% on all the items (i.e., they're basically being sold at print-on-demand cost).  If some kind of accounting weirdness comes up and we end up making some money off these items, we'll probably just donate the money to some worthy cause.
<br>
<p>Enjoy!
