<p>
Those are quite different questions. MPI on Hadoop (if you mean using MPI from within a Hadoop application) isn't really possible at the moment, but will be soon.
<br>
<p>MPI in a cloud environment is certainly doable - some of the folks on this list have reported using it with success on Amazon, for example. There are a few things you need to do to work thru the security firewalls, but it works okay once you do - not the best performance, but still workable.
<br>
