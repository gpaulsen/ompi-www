<p><p>Recent Advances in Message Passing Interface. 20th European MPI Users' Group Meeting (EuroMPI 2013) 
<br>
<p>EuroMPI 2013 is being held in cooperation with SIGHPC 
<br>
<p>Madrid, Spain, September 15-18, 2013 
<br>
<p>www.eurompi2013.org
<br>
<p>BACKGROUND AND TOPICS 
<br>
------------------------------- 
<br>
<p>EuroMPI is the preeminent meeting for users, developers and researchers to interact and discuss new developments and applications of message-passing parallel computing, in particular in and related to the Message Passing Interface (MPI). The annual meeting has a long, rich tradition, and the 20th European MPI Users' Group Meeting will again be a lively forum for discussion of everything related to usage and implementation of MPI and other parallel programming interfaces. Traditionally, the meeting has focused on the efficient implementation of aspects of MPI, typically on high-performance computing platforms, benchmarking and tools for MPI, short-comings and extensions of MPI, parallel I/O and fault tolerance, as well as parallel applications using MPI. The meeting is open towards other topics, in particular application experience and alternative interfaces for high-performance heterogeneous, hybrid, distributed memory systems. 
<br>
<p>Topics of interest include, but are not limited to: 
<br>
<p>- MPI implementation issues and improvements 
<br>
- Extensions to and shortcomings of MPI 
<br>
- Tools and environments for MPI 
<br>
- Hybrid and heterogeneous programming with MPI and other interfaces 
<br>
- Relation of MPI to alternative interfaces for hybrid/heterogeneous distributed memory systems 
<br>
- Interaction between message-passing software and hardware, in particular new high performance architectures 
<br>
- Fault tolerance in message-passing implementations and systems 
<br>
- Performance evaluation for MPI and MPI based applications- 
<br>
- Automatic performance tuning of MPI applications and implementations 
<br>
- Verification of message passing applications and protocols 
<br>
- Applications using message-passing, in particular in Computational Science and Scientific Computing 
<br>
- Non-standard message-passing applications 
<br>
- Parallel algorithms in the message-passing paradigm 
<br>
- Algorithms using the message-passing paradigm 
<br>
<p>The meeting will feature contributed talks on the selected, peer-reviewed papers, invited expert talks covering upcoming and future issues, a vendor session where selected vendors will present their new developments in hybrid and heterogeneous cluster and high-performance architectures, a poster session, and a tutorial day. 
<br>
<p>The scientific part of the conference is organized in cooperation with ACM SIGHPC. Conference proceedings will be published in the ACM Digital Library, which includes short and long papers, workshop papers, and posters. Selected high quality papers will be published in an international journals. There will also be a reward for the overall best paper from the academic conference. 
<br>
<p>WORKSHOPS
<br>
------------------------------- 
<br>
<p>IMUDI SPECIAL SESSION ON IMPROVING MPI USER AND DEVELOPER INTERACTION
<br>
<p>The IMUDI special session, to be held as a full-day meeting at the EuroMPI 2013 conference in Madrid, Spain, focuses on bringing together the MPI end-user and MPI implementor communities through discussions on MPI usage experiences, techniques, and optimizations. This meeting will focus on evaluating the MPI standard from the perspective of the MPI end-user (application and library developers) and address concerns and insights of MPI implementors and vendors. Unlike workshops associated with other conferences, the IMUDI session is still considered to be a part of the Euro MPI conference. Submissions will be reviewed separately to facilitate bringing together research publications falling into these &quot;special focus&quot; areas.
<br>
<p>More info at: <a href="http://press.mcs.anl.gov/imudi/">http://press.mcs.anl.gov/imudi/</a>
<br>
<p><p>ENERGY-EFFICIENT HIGH PERFORMANCE COMPUTING &amp; COMMUNICATION WORKSHOP (E2HPC2) 2013
<br>
<p>The first Energy-Efficient High Performance Computing &amp; Communication workshop will be co-located with EuroMPI 2013 in Madrid. Energy-awareness is now a main topic for HPC systems. The goal of this workshop is to discuss latest researches on the impact and possibles leverages of communications for such systems. E2HPC2 solicits original and non-published or under-review articles on the field of energy-aware communication in HPC environment. This workshop is co-located with EuroMPI as MPI is the main communication interface in those environments.
<br>
<p>More info at: <a href="http://www.irit.fr/~Georges.Da-Costa/e2hpc2.html">http://www.irit.fr/~Georges.Da-Costa/e2hpc2.html</a>
<br>
<p><p>PBIO 2013: INTERNATIONAL WORKSHOP ON PARALLELISM IN BIOINFORMATICS
<br>
<p>In Bioinformatics, we can find a variety of problems which are affected by huge processing times and memory consumption, due to the large size of biological data sets and the inherent complexity of biological problems. In fact, Bioinformatics is one of the most exciting research areas in which Parallelism finds application. Successful examples are mpiBLAST or ClustalW-MPI, among many others. In conclusion, Bioinformatics allows and encourages the application of many different parallelism-based technologies. The focus of this workshop is on MPI-based approaches, but we welcome any technique based on: multicore and cluster computing, supercomputing, grid computing, cloud computing, hardware accelerators as GPUs, FPGAs, or Cell processors, etc.
<br>
<p>More info at: <a href="http://arco.unex.es/mavega/pbio2013/">http://arco.unex.es/mavega/pbio2013/</a>
<br>
<p><p>SUBMISSION INFORMATION 
<br>
------------------------------- 
<br>
<p>Contributors are invited to submit a full paper as a PDF document not exceeding 6 pages in English. The title page should contain an abstract of at most 100 words and five specific, topical keywords. The paper must be formatted according to double-column ACM ICPS proceedings style. The usage of LaTeX for preparation of the contribution as well as the submission in camera ready format is strongly recommended. Style files can be found at<a href="http://www.acm.org/publications/icps-instructions/">http://www.acm.org/publications/icps-instructions/</a>. 
<br>
<p>New work that is not yet mature for a full paper, short observations, and similar brief announcements are invited for the poster session. Contributions to the poster session should be submitted in the form of a two page abstract. 
<br>
<p>All contributions will be fully peer reviewed by the program committee. Papers shall be submitted electronically via Easychair, see<a href="http://www.easychair.org/conferences/?conf=eurompi2013">http://www.easychair.org/conferences/?conf=eurompi2013</a>. 
<br>
<p><p>NVIDIA BEST PAPER AWARD
<br>
------------------------------- 
<br>
<p>The Program Committee of EuroMPI 2013 will give a best paper awards. Best Paper Awards will be given to the author(s) of a full paper presented at the conference, selected by the Organizing Committee. The Best Paper Award is a Tesla K20 computing processor, sponsored by NVIDIA, valued in $3,200.
