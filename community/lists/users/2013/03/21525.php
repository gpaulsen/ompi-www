<p>
Dear OMPI folks,
<br>
<p>according to this FAQ entry
<br>
<p><a href="http://www.open-mpi.org/faq/?category=mpi-apps#override-wrappers-after-v1.0">http://www.open-mpi.org/faq/?category=mpi-apps#override-wrappers-after-v1.0</a>
<br>
<p>one way to use the mpif90 compiler wrapper with another compiler than 
<br>
Open-MPI was built with is to set the envvar OMPI_FC to the other compiler.
<br>
<p>Using this simple toy code
<br>
<p><span class="quotelev1">&gt; program test
</span><br>
<span class="quotelev1">&gt;   implicit none
</span><br>
<span class="quotelev1">&gt;   include 'mpif.h'
</span><br>
<span class="quotelev1">&gt;   integer :: ierr
</span><br>
<span class="quotelev1">&gt;   call MPI_Init(ierr)
</span><br>
<span class="quotelev1">&gt;   print *, &quot;Hello&quot;
</span><br>
<span class="quotelev1">&gt;   call MPI_Finalize(ierr)
</span><br>
<span class="quotelev1">&gt; end program test
</span><br>
<p>I indeed see the expected (and documented) behaviour:
<br>
<p><span class="quotelev1">&gt; bash$ export OMPI_FC=ifort
</span><br>
<span class="quotelev1">&gt; bash$ mpif90 test.f90
</span><br>
<span class="quotelev1">&gt; bash$ mpirun -np 3 ./a.out
</span><br>
<p>Now, If I replace these two lines in the reproducer
<br>
<p><span class="quotelev1">&gt; implicit none
</span><br>
<span class="quotelev1">&gt; include 'mpif.h'
</span><br>
<p>with these two lines (which is actually the MPI-2 way of doing things, 
<br>
as Jeff himself praises in this blog entry: 
<br>
<a href="http://blogs.cisco.com/performance/a-bucket-full-of-new-mpi-fortran-features">http://blogs.cisco.com/performance/a-bucket-full-of-new-mpi-fortran-features</a>)
<br>
<p><span class="quotelev1">&gt; use mpi
</span><br>
<span class="quotelev1">&gt; implicit none
</span><br>
<p>I get this as the output of mpif90:
<br>
<p><span class="quotelev1">&gt; test.f90(7): error #7013: This module file was not generated by any 
</span><br>
<span class="quotelev1">&gt; release of this compiler.   [MPI]
</span><br>
<span class="quotelev1">&gt; use mpi
</span><br>
<span class="quotelev1">&gt; ----^
</span><br>
<span class="quotelev1">&gt; compilation aborted for test.f90 (code 1)
</span><br>
<p>There seems to be no language on this particular problem in the FAQs, so 
<br>
I assume this is either a documentation bug or an actual bug in the 
<br>
compiler wrappers. The approach is clearly marked as &quot;not recommended&quot;, 
<br>
but actually, this problem can be quite a showstopper because on 
<br>
laptops, people (like me) are often too lazy to compile Open-MPI (and 
<br>
consequently, valgrind and friends) multiple times, especially since the 
<br>
FAQ says that this should work :)
<br>
<p>I am using the system gcc of ubuntu 12.04 (4.6.3-1ubuntu5) and OMPI 
<br>
1.6.3. Of course everything works if I use a properly installed machine 
<br>
that provides OpenMPI-builds for different compilers via modules, and 
<br>
also the oldish &quot;include 'mpif.h'&quot; approach is a sufficient workaround.
<br>
<p>Thanks for any clarification, hope this reproducer helps,
<br>
<p>Cheers,
<br>
<p>Dominik
