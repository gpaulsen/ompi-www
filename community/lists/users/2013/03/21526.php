&gt; 
&gt; one way to use the mpif90 compiler wrapper with another compiler than
&gt; Open-MPI was built with is to set the envvar OMPI_FC to the other
compiler.
&gt; 
&gt; Using this simple toy code
&gt; 
&gt; &gt; program test
&gt; &gt;   implicit none
&gt; &gt;   include 'mpif.h'
&gt; &gt;   integer :: ierr
&gt; &gt;   call MPI_Init(ierr)
&gt; &gt;   print *, &quot;Hello&quot;
&gt; &gt;   call MPI_Finalize(ierr)
&gt; &gt; end program test
&gt; 
&gt; I indeed see the expected (and documented) behaviour:
&gt; 
&gt; &gt; bash$ export OMPI_FC=ifort
&gt; &gt; bash$ mpif90 test.f90
&gt; &gt; bash$ mpirun -np 3 ./a.out
&gt; 
&gt; Now, If I replace these two lines in the reproducer
&gt; 
&gt; &gt; implicit none
&gt; &gt; include 'mpif.h'
&gt; 
&gt; with these two lines (which is actually the MPI-2 way of doing things, as
Jeff
&gt; himself praises in this blog entry:
&gt; <a href="http://blogs.cisco.com/performance/a-bucket-full-of-new-mpi-fortran">http://blogs.cisco.com/performance/a-bucket-full-of-new-mpi-fortran</a>-
&gt; features)
&gt; 
&gt; &gt; use mpi
&gt; &gt; implicit none
&gt; 
&gt; I get this as the output of mpif90:
&gt; 
&gt; &gt; test.f90(7): error #7013: This module file was not generated by any
&gt; &gt; release of this compiler.   [MPI]
&gt; &gt; use mpi
&gt; &gt; ----^
&gt; &gt; compilation aborted for test.f90 (code 1)
&gt; 
&gt; There seems to be no language on this particular problem in the FAQs, so I
&gt; assume this is either a documentation bug or an actual bug in the compiler
&gt; wrappers. The approach is clearly marked as &quot;not recommended&quot;, but
&gt; actually, this problem can be quite a showstopper because on laptops,
&gt; people (like me) are often too lazy to compile Open-MPI (and consequently,
&gt; valgrind and friends) multiple times, especially since the FAQ says that
this
&gt; should work :)
&gt; 
&gt; I am using the system gcc of ubuntu 12.04 (4.6.3-1ubuntu5) and OMPI 1.6.3.
&gt; Of course everything works if I use a properly installed machine that
provides
&gt; OpenMPI-builds for different compilers via modules, and also the oldish
&gt; &quot;include 'mpif.h'&quot; approach is a sufficient workaround.
