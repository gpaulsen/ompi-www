<p>FIRST INTERNATIONAL WORKSHOP ON CLOUD FOR BIO (C4Bio)
<br>
<p>to be held as part of IEEE/ACM CCGrid 2014
<br>
<p>Chicago, USA, May 26-29, 2014
<br>
<p><a href="http://www.arcos.inf.uc3m.es/~c4bio2014">http://www.arcos.inf.uc3m.es/~c4bio2014</a>
<br>
<p>BACKGROUND
<br>
----------------------
<br>
<p>In the last 20 years, computational methods have become an important part of developing emerging technologies for the field of bioinformatics and biomedicine. Research areas such as biomodelling, molecular dynamics, genomics, neuroscience, cancer models, evolutionary biology, medical biology, biochemistry, biophysics, biotechnology, cell biology, nanobiotechnology, biological engineering, pharmacology, genetics therapy, or automatic diagnosis, rely heavily on large scale computational resources as they need to manage Tbytes or Pbytes of data with large-scale structural and functional relationships, TFlops or PFlops of computing power for simulating highly complex models, or many-task processes and workflows for processing and analyzing data.
<br>
<p>As applications require more and more computational resources every day in order to increase the knowledge of biological and health processes, bio disciplines create new challenges of scale for computation, storage, and interpretation of petascale data that are difficult to solve with single clusters. Cloud computing has the potential to help solve these problems by offering a utility model based on highly flexible computing and storage capabilities and abstraction layers that allows overcoming many of the constraints present in dedicated systems. It may also increase research productivity by allowing sharing applications, tools, and algorithms in an easy way.
<br>
<p>The aim of the workshop is to promote the synergies of the cloud computing and bio communities by exploring applications developed in clouds, cloud systems enhancements for bio, and new trends and needs shared between both communities. Thus, this workshop will feature articles that discuss the application of cloud computing to bioinformatics and biomedicine, including solutions to area problems and architectural adaptation of cloud systems to fit those problems.
