<span class="quotelev1">&gt; mailing lists, ...) and I certainly can't speak for them, but if we  
</span><br>
<span class="quotelev1">&gt; ask nicely, I'd be willing to bet that they would add some hosting  
</span><br>
<span class="quotelev1">&gt; services for a documentation project (if such additional resources  
