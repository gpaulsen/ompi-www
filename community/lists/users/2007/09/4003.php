</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; -----
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; The only current documentation we have is:
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; - the web FAQ
</span><br>
<span class="quotelev1">&gt; - the README in the tarball
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; What is conspicuously missing is a nice PDF and/or HTML tarball with  
</span><br>
<span class="quotelev1">&gt; comprehensive documentation.  But I think that FAQ/README also fit  
</span><br>
<span class="quotelev1">&gt; into the general category of documentation, so it might make sense to  
</span><br>
<span class="quotelev1">&gt; put all 3 of these items under the control of one group.  The obvious  
</span><br>
<span class="quotelev1">&gt; rationale here is that all three could stay in tighter sync if  
</span><br>
<span class="quotelev1">&gt; there's one group monitoring all 3.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; One point worth mentioning: Open MPI is all about community  
</span><br>
<span class="quotelev1">&gt; consensus, but &quot;s/he who implements usually wins&quot;.  :-)  So if we get  
</span><br>
<span class="quotelev1">&gt; an active group working on documentation, the FAQ could be totally re- 
</span><br>
<span class="quotelev1">&gt; done if the group so decides (for example).
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; All this being said, the OMPI developers *have* talked about  
</span><br>
<span class="quotelev1">&gt; documentation a bit over time.  Here's some of the points from prior  
</span><br>
<span class="quotelev1">&gt; discussions, in no particular order:
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; - It highly desirable to have documentation that can be output in  
</span><br>
<span class="quotelev1">&gt; multiple different forms (PDF, HTML, ...whatever).  If possible, the  
</span><br>
<span class="quotelev1">&gt; docs should be shipped in distribution tarballs and hosted on the  
</span><br>
<span class="quotelev1">&gt; OMPI web site.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; - LAM/MPI had two great docs: one for installation LAM/MPI and one  
</span><br>
<span class="quotelev1">&gt; for using LAM/MPI.  These might be good example documents for what  
</span><br>
<span class="quotelev1">&gt; Open MPI might want to do (see <a href="http://www.lam-mpi.org/using/docs/">http://www.lam-mpi.org/using/docs/</a>),  
</span><br>
<span class="quotelev1">&gt; regardless of the back-end technology used to generate the docs.   
</span><br>
<span class="quotelev1">&gt; Source LaTeX for these guides are available if it would be helpful (I  
</span><br>
<span class="quotelev1">&gt; wrote most of them).
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; - It would be most helpful if the documentation is written in a tool  
</span><br>
<span class="quotelev1">&gt; that has free editors, preferably cross-platform and available in  
</span><br>
<span class="quotelev1">&gt; multiple POSIX-like environments (Solaris, Linux, OS X).  MS Office  
</span><br>
<span class="quotelev1">&gt; was explicitly rejected because of its requirement for Windows/OS X  
</span><br>
<span class="quotelev1">&gt; (other Office clones were not really discussed).  LaTeX was discussed  
</span><br>
<span class="quotelev1">&gt; but wasn't favored due to the steep learning curve and general lack  
</span><br>
<span class="quotelev1">&gt; of experience with it outside of academia.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; - First documentation should be aimed towards users.  Developer  
</span><br>
<span class="quotelev1">&gt; documentation might follow.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; - Once upon a time, we developers started to use doxygen for  
</span><br>
<span class="quotelev1">&gt; documentation, but it has proven to be lousy for book-like entities  
</span><br>
<span class="quotelev1">&gt; (IONSHO).  Doxygen is decent for code documentation, but not documents.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; - A few recent discussions about documentation came to the conclusion  
</span><br>
<span class="quotelev1">&gt; that Docbook (www.docbook.org) looked promising, but we didn't get  
</span><br>
<span class="quotelev1">&gt; deep into details / investigating the feasibility.  One obvious Big  
</span><br>
<span class="quotelev1">&gt; Project using Docbook is Subversion (see <a href="http://svnbook.red">http://svnbook.red</a>- 
</span><br>
<span class="quotelev1">&gt; bean.com/).  Docbook-produced HTML and PDF seem to look both pretty  
</span><br>
<span class="quotelev1">&gt; and functional.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; - It would also be nice if sub-distributions of Open MPI could take  
</span><br>
<span class="quotelev1">&gt; the documentation and -- in some defined automated fashion -- be able  
</span><br>
<span class="quotelev1">&gt; to do the following:
</span><br>
<span class="quotelev1">&gt;      - insert their own &quot;chapters&quot; or &quot;sections&quot; that are specific to  
</span><br>
<span class="quotelev1">&gt; that sub-distribution (e.g., Sun ClusterTools have some Solaris- 
</span><br>
<span class="quotelev1">&gt; specific stuff, OFED have some OpenFabrics-specific stuff, etc.)
</span><br>
<span class="quotelev1">&gt;      - remove/&quot;turn off&quot; specific sections of documentation (e.g.,  
</span><br>
<span class="quotelev1">&gt; OFED would likely not include any documentation about Myricom  
</span><br>
<span class="quotelev1">&gt; networks [and vice versa])
</span><br>
<span class="quotelev1">&gt; This would go a long ways towards being able to keep the community  
</span><br>
<span class="quotelev1">&gt; documentation in sync with docs included in targeted/vendor OMPI  
</span><br>
<span class="quotelev1">&gt; releases.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; - The OMPI web site is almost entirely written in PHP and is mirrored  
</span><br>
<span class="quotelev1">&gt; around the world.  It would be *strongly* preferred if the web-site  
</span><br>
<span class="quotelev1">&gt; hosting of the docs is fully mirror-able (because assumedly docs are  
</span><br>
<span class="quotelev1">&gt; one of the things that users would want to browse the most).  Hence,  
</span><br>
<span class="quotelev1">&gt; requiring a new kind of server other than HTML/PHP would require  
</span><br>
<span class="quotelev1">&gt; very, very strong rationale.  :-)
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; - The technology of choice for displaying on the web site is PHP.   
</span><br>
<span class="quotelev1">&gt; But that still leaves open a wide variety of choices for serving docs  
</span><br>
<span class="quotelev1">&gt; via the web site, including (but not limited to):
</span><br>
<span class="quotelev1">&gt;      - just posting PDFs (although having HTML-based docs would  
</span><br>
<span class="quotelev1">&gt; certainly be nice)
</span><br>
<span class="quotelev1">&gt;      - a PHP-based package or home-grown PHP
</span><br>
<span class="quotelev1">&gt;      - generating HTML offline (via cron or whatever) and putting the  
</span><br>
<span class="quotelev1">&gt; results in the web site
</span><br>
<span class="quotelev1">&gt;      - ...etc.
</span><br>
<span class="quotelev1">&gt;
</span><br>
