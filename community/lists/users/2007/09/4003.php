<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; I would also be happy to host a teleconference if talking about all  
</span><br>
<span class="quotelev1">&gt; this start/admin stuff for an hour would save 1-2 weeks worth of  
</span><br>
