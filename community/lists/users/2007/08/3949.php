<br>
&nbsp;&nbsp;&nbsp;&nbsp;- Dr. Mark Seager, Lawrence Livermore National Laboratory
<br>
&nbsp;&nbsp;- 43 technical paper presentations
<br>
&nbsp;&nbsp;- Best Papers session
<br>
&nbsp;&nbsp;- 4 tutorial sessions
<br>
&nbsp;&nbsp;- 1 &quot;Green&quot; workshop
<br>
&nbsp;&nbsp;- Poster session
<br>
&nbsp;&nbsp;- HeteroPar'07 workshop
<br>
&nbsp;&nbsp;- Panel session: &quot;The Future of Multicore Technology&quot;
<br>
&nbsp;&nbsp;- Complimentary social event and live music 
