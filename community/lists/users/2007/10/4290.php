<p>Yes.  PGAPACK, developend in the mid-1990s by David Levine while at Argonne,
<br>
works perfectly well in parallel under various MPI implementations.
<br>
<p>I have been in contact with David and Argonne to coordinate a re-release
<br>
under a newer license [1], but we're not quite there yet, and I have
<br>
been the one holding this up. Hopefully more news 'soon' but I've been
<br>
mumbling that all summer while I kept busy...
<br>
<p>You may want to look at PGAPACK and study it for possible extensions and
<br>
refactorings, rather than to start again from scratch.
<br>
<p>Cheers, Dirk
<br>
<p>[1] The original license is more restrictive than it needs to be, and
<br>
everybody is in agreement that a more liberal and understood license would be
<br>
beneficial. The MPICH license, suggested by Argonne, works for everybody, and
<br>
is the most likely outcome.
