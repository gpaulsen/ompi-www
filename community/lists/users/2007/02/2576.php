CALL FOR PAPERS
<br>
<p>Workshop on Virtualization/Xen in High-Performance Cluster
<br>
and Grid Computing (XHPC'07)
<br>
<p>as part of The 16th IEEE International Symposium on High
<br>
Performance Distributed Computing (HPDC-16), June 27-29, 2007,
<br>
Monterey, California.
<br>
<p>and
<br>
<p>Workshop on Virtualization in High-Performance
<br>
Cluster and Grid Computing (VHPC'07)
<br>
<p>as part of The 13th International European Conference on Parallel
<br>
and Distributed Computing (Euro-Par 2007), August 28-31, 2007,
<br>
IRISA, Rennes, France.
<br>
<p>===============================================================
<br>
<p><p>Date: June 27-29, 2007 and August 28-31, 2007
<br>
<p>HPDC-16: <a href="http://www.isi.edu/hpdc2007/">http://www.isi.edu/hpdc2007/</a>
<br>
Euro-Par 2007: <a href="http://europar2007.irisa.fr/">http://europar2007.irisa.fr/</a>
