<p>==========================================================
<br>
<p><p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Hot Interconnects 15
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;IEEE Symposium on High-Performance Interconnects
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;August 22-24, 2007
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Stanford University
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Palo Alto, California, USA
<br>
<p>Hot Interconnects is the premier international forum for researchers and
<br>
developers of state-of-the-art hardware and software architectures and
<br>
implementations for interconnection networks of all scales, ranging from
<br>
on-chip processor-memory interconnects to wide-area networks. This yearly
<br>
conference is very well attended by leaders in industry and academia. The
<br>
atmosphere provides for a wealth of opportunities to interact with
<br>
individuals at the forefront of this field.
<br>
<p>Themes include cross-cutting issues spanning computer systems, networking
<br>
technologies, and communication protocols.  This conference is directed
<br>
particularly at new and exciting technology and product innovations in these
<br>
areas. Contributions should focus on real experimental systems, prototypes,
<br>
or leading-edge products and their performance evaluation. In addition to
<br>
those subscribing to the main theme of the conference, contributions are
<br>
also solicited in the topics listed below.
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Novel and innovative interconnect architectures
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Multi-core processor interconnects
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* System-on-Chip Interconnects
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Advanced chip-to-chip communication technologies
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Optical interconnects
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Protocol and interfaces for interprocessor communication
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Survivability and fault-tolerance of interconnects
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* High-speed packet processing engines and network processors
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* System and storage area network architectures and protocols
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* High-performance host-network interface architectures
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* High-bandwidth and low-latency I/O
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Tb/s switching and routing technologies
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Innovative architectures for supporting collective communication
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Novel communication architectures to support grid computing
<br>
<p><p>Submission Guideline
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;o Submission deadline: March 31, 2007
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;o Notification of acceptance: May 15, 2007
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;o Papers need sufficient technical detail to judge quality and
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;suitability for presentation.
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;o Submit title, author, abstract, and full paper (six pages,
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;double-column, IEEE format).
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;o Papers should be submitted electronically at the specified link
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;location found on <a href="http://www.hoti.org">http://www.hoti.org</a>
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;o For further information please see
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.hoti.org/hoti15/cfp.html">http://www.hoti.org/hoti15/cfp.html</a>
<br>
