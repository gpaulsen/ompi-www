On 12/19/07, Aurelien Bouteiller &lt;bouteill_at_[hidden]&gt; wrote:
<br>
<span class="quotelev1">&gt; I have quite different definitions than Jeff.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Distributed computing is encompassing all the &quot;parallel computing&quot;
</span><br>
<span class="quotelev1">&gt; models, including clusters, grids, master-slave, shared memory...
</span><br>
<span class="quotelev1">&gt; Everything that basically implies using several collaborating
</span><br>
<span class="quotelev1">&gt; processes to solve a problem (whatever collaborating means, network,
</span><br>
<span class="quotelev1">&gt; shared memory, RPC, data dependencies... ).
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Cluster computing simply refers simply to computations that occurs on
</span><br>
<span class="quotelev1">&gt; a cluster type machine. A cluster is -usually- a distributed memory
</span><br>
<span class="quotelev1">&gt; computer based on commodity hardware (but counter examples exist).  It
</span><br>
<span class="quotelev1">&gt; may include commodity network (like giga ethernet) or more specific
</span><br>
<span class="quotelev1">&gt; nics (like myrinet, infiniband, quadrics and so on). As said Jeff,
</span><br>
<span class="quotelev1">&gt; usually the best way to use such a machine is to have a kind of
</span><br>
<span class="quotelev1">&gt; tightly coupled application.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Grid computing refers to gathering several clusters (and sometimes
</span><br>
<span class="quotelev1">&gt; large databases and scientific instruments like telescopes that
</span><br>
<span class="quotelev1">&gt; generate data) and use them altogether. Compared to a cluster or a
</span><br>
<span class="quotelev1">&gt; supercomputer, this introduces several issues related to password
</span><br>
<span class="quotelev1">&gt; administration, user domains, firewall bypass, several different
</span><br>
<span class="quotelev1">&gt; scheduler collaborating, and quite slow network between the sites.
</span><br>
<p>I understand that a major difference between Distributed Computing and
<br>
Grid Computing is that whereas we *usually* have a set of trusted
<br>
nodes in the former, in the latter case, we generally have
<br>
geographically dispersed computers which use a separate authentication
<br>
mechanism to come together and form a Grid.
<br>
<p><p>Please correct me if I am wrong
<br>
