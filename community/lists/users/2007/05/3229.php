<br>
<p>With the rapid growth in computing and communications technology, the past
<br>
decade has witnessed a proliferation of powerful parallel and distributed
<br>
systems and an ever increasing demand for practice of high performance
<br>
computing and communications (HPCC). HPCC has moved into the mainstream of
<br>
computing and has become a key technology in determining future research and
<br>
development activities in many academic and industrial branches, especially
<br>
when the solution of large and complex problems must cope with very tight
<br>
timing schedules. The HPCC-07 conference provides a forum for engineers and
<br>
scientists in academia, industry, and government to address the resulting
<br>
profound challenges and to present and discuss their new ideas, research
<br>
results, applications and experience on all aspects of high performance
<br>
computing and communications. With this third event in this conference
<br>
series, HPCC-07 plans to continue the success of the previous two HPCC
<br>
conferences, held 2005 in Sorrento, Italy, and HPCC-06 held in Munich,
<br>
Germany, each with over 300 submitted papers and more than 100 
<br>
participants.
<br>
<p>TOPICS OF INTEREST
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Networking protocols, routing, algorithms
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Languages and compilers for HPC
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Parallel and distributed system architectures
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Parallel and distributed algorithms
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Embedded systems
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Wireless, mobile and pervasive computing
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Web services and Internet computing
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Peer-to-peer computing
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Grid computing
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Cluster computing
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Reliability, fault-tolerance, and security
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Performance evaluation and measurement
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Tools and environments for software development
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Distributed systems and applications
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* High-performance scientific and engineering computing
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Database applications and data mining
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Biological/molecular computing
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* Collaborative and cooperative environments
