************** Paper submission deadline extended to May 18, 2007 *************
<br>
<p><p>*******************************************************************************
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Call for Papers
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2007 IEEE International Conference on Cluster Computing
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(Cluster2007)
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;17 - 21 September 2007
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Austin, Texas, USA
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.cluster2007.org/">http://www.cluster2007.org/</a>
<br>
<p>*******************************************************************************
<br>
<p>In less than a decade, cluster computing has become the mainstream technology
<br>
for High Performance Computing and Information Technology. It has gained
<br>
this prominence by providing reliable, robust and cost-effective platforms
<br>
for solving many complex computational problems, accessing and visualizing
<br>
data, and providing information services. Cluster 2007 is hosted by Texas
<br>
Advanced Computing Center (TACC) in the culturally rich, high-tech city of
<br>
Austin, Texas. Here you will experience an open forum with fellow cluster
<br>
researchers, system designers and installers, and users for presenting and
<br>
discussing new directions, opportunities and ideas that will shape Cluster
<br>
Computing. Cluster 2007 welcomes paper and poster submissions on innovative
<br>
work from researchers in academia, industry and government describing original
<br>
research work in cluster computing.
<br>
<p>The ability to aggregate the computing power of thousands of processors is a
<br>
significant milestone in the scalability of commodity systems. Nevertheless,
<br>
the ability to use both small and large systems efficiently is an ongoing
<br>
effort in the areas of Networking, Management, Interconnects, and Application
<br>
Optimization. A continued vigilance and assessment of R&amp;D efforts is important
<br>
to insure that Cluster Computing will harness the new technological advances
<br>
in hardware and software to solve the challenges of our age, and the next
<br>
generation.
<br>
<p>Topics of interest are (but not limited to):
<br>
<p>&nbsp;&nbsp;- Cluster Software and Middleware
<br>
&nbsp;&nbsp;- Software Environments and Tools
<br>
&nbsp;&nbsp;- Single -System Image Services
<br>
&nbsp;&nbsp;- Parallel File Systems and I/O Libraries
<br>
&nbsp;&nbsp;- Standard Software for Clusters
<br>
&nbsp;&nbsp;- Cluster Networking
<br>
&nbsp;&nbsp;- High-Speed Interconnects
<br>
&nbsp;&nbsp;- High Performance Message Passing Libraries
<br>
&nbsp;&nbsp;- Lightweight Communication Protocols
<br>
&nbsp;&nbsp;- Applications
<br>
&nbsp;&nbsp;- Application Methods and Algorithms
<br>
&nbsp;&nbsp;- Adaptation to Multi-Core
<br>
&nbsp;&nbsp;- Data Distribution, Load Balancing &amp; Scaling
<br>
&nbsp;&nbsp;- MPI/OpenMP Hybrid Computing
<br>
&nbsp;&nbsp;- Visualization
<br>
&nbsp;&nbsp;- Performance Analysis and Evaluation
<br>
&nbsp;&nbsp;- Benchmarking &amp; Profiling Tools
<br>
&nbsp;&nbsp;- Performance Prediction &amp; Modeling
<br>
&nbsp;&nbsp;- Cluster Management
<br>
&nbsp;&nbsp;- Security and Reliability
<br>
&nbsp;&nbsp;- High Availability Solutions
<br>
&nbsp;&nbsp;- Resource and Job Management
<br>
&nbsp;&nbsp;- Administration and Maintenance Tools
<br>
<p>Paper Submission:
<br>
<p>Paper Format: Since the camera-ready version of accepted papers must be
<br>
compliant with the IEEE Xplore format for publication, submitted papers
<br>
must conform to the following Xplore layout, page limit, and font size. This
<br>
will insure a size consistency and a uniform layout for the reviewers. (With
<br>
minimal changes, accepted document can be styled for publication according
<br>
to Xplore requirements explained in the Xplore formatting guide, which is
<br>
also in Xplore format).
<br>
<p>&nbsp;&nbsp;- PDF files only.
<br>
&nbsp;&nbsp;- Maximum 10 pages for Technical Papers, maximum 6 pages for Posters.
<br>
&nbsp;&nbsp;- Single-spaced
<br>
&nbsp;&nbsp;- 2-column numbered pages in IEEE Xplore format
<br>
&nbsp;&nbsp;- (8.5x11-inch paper, margins in inches-- top: 0.75, bottom: 1.0,
<br>
&nbsp;&nbsp;&nbsp;&nbsp;sides:0.625, and between columns:0.25, main text: 10pt ).
<br>
&nbsp;&nbsp;- Format instructions are available for: LaTeX, Word document, PDF
<br>
&nbsp;&nbsp;&nbsp;&nbsp;files.
<br>
&nbsp;&nbsp;- Margin and placement guides are available in: Word, PDF and
<br>
&nbsp;&nbsp;&nbsp;&nbsp;postscript files.
<br>
&nbsp;&nbsp;- Concerning the final camera-ready version: Maximum of 2 extra pages
<br>
&nbsp;&nbsp;&nbsp;&nbsp;at $100/page. Camera-ready means PDF file must comply with IEEE
<br>
&nbsp;&nbsp;&nbsp;&nbsp;Xplore formatting and style for publication.
<br>
&nbsp;&nbsp;- A conversion tool kit for converting from Word, LaTeX, and
<br>
&nbsp;&nbsp;&nbsp;&nbsp;PostScript and checking compliance will be available by April 11.
<br>
&nbsp;&nbsp;&nbsp;&nbsp;See the Final Submission section then.
<br>
&nbsp;&nbsp;- Electronic Submission: Only web-based submission is accepted.
<br>
&nbsp;&nbsp;&nbsp;&nbsp;The URL will be announced two weeks before the submission deadline,
<br>
&nbsp;&nbsp;&nbsp;&nbsp;on the Cluster2007 web page.
<br>
<p>In addition to the normal technical paper sessions, we plan to organize vendor
<br>
sessions and industrial exhibitions. Companies interested in participating
<br>
in the vendor sessions or presenting their exhibits at the meeting or both,
<br>
should contact the Exhibits Chair member, Ivan R. Judson (judson_at_[hidden])
<br>
by July 13, 2007.
