<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- Conference held at the William Hewlett Teaching Center at
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Stanford University.
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- Papers selected will be published in proceedings by the
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;IEEE Computer Society.
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- Presentations are 30-minute talks in a single-track format.
