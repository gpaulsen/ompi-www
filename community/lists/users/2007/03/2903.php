</span><br>
<span class="quotelev3">&gt;&gt;&gt; of the report, is that the code size is going to be a problem as data 
</span><br>
<span class="quotelev3">&gt;&gt;&gt; and code must live in the same 256KB in each SPE.  They mention dynamic 
</span><br>
<span class="quotelev3">&gt;&gt;&gt; overlay loading, which is also how we deal with large code size, but 
</span><br>
<span class="quotelev3">&gt;&gt;&gt; things get tricky and slow with the potentially needed save and restore 
</span><br>
<span class="quotelev3">&gt;&gt;&gt; of registers and LS. 
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     
</span><br>
<span class="quotelev3">&gt;&gt;&gt;       
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; I did some checking on this.   Apparently the trunk of GCC and the 
</span><br>
<span class="quotelev1">&gt; latest GNU Binutils handle overlays.   Because the SPU compiler knows of 
</span><br>
<span class="quotelev1">&gt; its limits address space, the ELF object code sections reflect this, and 
</span><br>
<span class="quotelev1">&gt; the the linker can transparently generate stubs to trigger the 
</span><br>
<span class="quotelev1">&gt; loading.   GCC also has options like -ffunction-sections that enable the 
</span><br>
<span class="quotelev1">&gt; linker to optimize for locality. 
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; So even though the OpenMPI shared libraries in total appear to have a 
</span><br>
<span class="quotelev1">&gt; footprint about four times too big for code alone (don't know about the 
</span><br>
<span class="quotelev1">&gt; typical stack &amp; heap requirements), perhaps it's still doable without a 
</span><br>
<span class="quotelev1">&gt; big effort to strip down OpenMPI?
</span><br>
<span class="quotelev1">&gt;   
</span><br>
But loading an overlay can be quite expensive depending on how much 
<br>
needs to be loaded and how much user data/code needs to be restored.  If 
<br>
the user is trying to use most of the LS for data, which is perfectly 
<br>
sane and reasonable, then you might have to load multiple overlays to 
<br>
complete a function. We've also been having issues with mixing manual 
<br>
overlay loading of our code with the autoloading generated by the compiler.
<br>
<p>Regardless, it would be interesting to see if this can even be made to 
<br>
work.  If so, it might really help people get apps up on Cell since it 
<br>
can be reasonably thought of as a cluster on a chip, backed by a larger 
<br>
address space.
