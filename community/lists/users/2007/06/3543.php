<p>Call for Posters:
<br>
<p>The IEEE Cluster 2007 Conference (www.cluster2007.org) invites poster
<br>
submissions from engineers and scientists in academia, industry, and
<br>
the national laboratories that describe original research in all areas
<br>
of cluster computing. Student submissions are particularly welcomed.
<br>
<p>The Poster submission deadline is July 22, 2007.
<br>
<p>Topics of interest include, but are not limited to:
<br>
Cluster Software and Middleware
<br>
&nbsp;&nbsp;&nbsp;Software Environments and Tools
<br>
&nbsp;&nbsp;&nbsp;Single-System Image Services
<br>
&nbsp;&nbsp;&nbsp;Parallel File Systems and I/O Libraries
<br>
&nbsp;&nbsp;&nbsp;Standard Software for Clusters
<br>
Cluster Networking
<br>
&nbsp;&nbsp;&nbsp;High-Speed Interconnects
<br>
&nbsp;&nbsp;&nbsp;High Performance Message Passing Libraries
<br>
&nbsp;&nbsp;&nbsp;Lightweight Communication Protocols
<br>
Applications
<br>
&nbsp;&nbsp;&nbsp;Application Methods and Algorithms
<br>
&nbsp;&nbsp;&nbsp;Adaptation to Multi-Core
<br>
&nbsp;&nbsp;&nbsp;Data Distribution, Load Balancing &amp; Scaling
<br>
&nbsp;&nbsp;&nbsp;MPI/OpenMP Hybrid Computing
<br>
&nbsp;&nbsp;&nbsp;Visualization
<br>
Performance Analysis and Evaluation
<br>
&nbsp;&nbsp;&nbsp;Benchmarking &amp; Profiling Tools
<br>
&nbsp;&nbsp;&nbsp;Performance Prediction &amp; Modeling
<br>
Cluster Management
<br>
&nbsp;&nbsp;&nbsp;Security and Reliability
<br>
&nbsp;&nbsp;&nbsp;High Availability Solutions
<br>
&nbsp;&nbsp;&nbsp;Resource and Job Management
<br>
&nbsp;&nbsp;&nbsp;Administration and Maintenance Tools
<br>
<p>Please submit proposals no longer than 6 pages describing your
<br>
research and the results you wish to present in your poster. All
<br>
poster proposals will be peer-reviewed. Submission details are
<br>
available at www.cluster2007.org/?m_b_c=posterSubmission .
<br>
