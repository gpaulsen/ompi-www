<p>
In the FAQ &lt;<a href="http://www.open-mpi.org/faq/?category=mpi-apps">http://www.open-mpi.org/faq/?category=mpi-apps</a>&gt;, section  
<br>
labeled:
<br>
<p>12. How do I build BLACS with Open MPI?
<br>
<p>INTFACE = -Df77IsF2C
<br>
<p>That INTFACE value is only for G77, G95, and related compilers.
<br>
<p>For the Intel Fortran compiler it is: -DAdd_
<br>
<p><p>I have successfully built the combination of OpenMPI 1.2.3, ATLAS,  
<br>
BLACS, ScalaPack, and MUMPS using the Intel Fortran compiler on two  
<br>
different Debian Linux systems (3.0r3 on AMD Opterons and 4.0r0 on  
<br>
Intel Woodcrest/MacPro).
