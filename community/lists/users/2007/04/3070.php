On Mar 30, 2007, at 4:23 PM, Rohit_Singh_at_[hidden] wrote:
&gt; I'm somewhat new to OpenMPI, but I'm currently evaluating it as a
&gt; communications mechanism between Windows and Unix servers.
&gt;
&gt; I noticed that under your FAQs (
&gt; <a href="http://www.open-mpi.org/faq/?category=supported-systems">http://www.open-mpi.org/faq/?category=supported-systems</a>), it says:
&gt;
&gt;       There are plans to support Microsoft Windows in the not-distant
&gt; future.
&gt;
&gt; When is the not-distant future?  Is it in scope for this year?
&gt; Will the
&gt; Windows support require a Unix emulation layer like Cygwin?
&gt;
&gt; I do apologize if the information I'm requesting here is
&gt; confidential in
&gt; nature.
The Windows support is planned, but we don't have a definite time
table.  Much of the work has been done, although there are still
pieces missing.  I believe remote launching is one of those pieces.
The Windows support will be native and will not require Cygwin (which
is part of what is taking us so long -- it's a bit project to move a
UNIX-based code like Open MPI to Windows without sacrificing too much
in maintainability and performance.
Hope this helps,
