<p>CONFERENCE VENUE
<br>
<p>The conference will be held in the historical, cultural and
<br>
economic center of Paris, the capital of France.
<br>
<p>The city, which is renowned for its neo-classical architecture,
<br>
hosts many museums and galleries and has an active nightlife. The
<br>
symbol of Paris is the 324 metre (1,063 ft) Eiffel Tower on the
<br>
banks of the Seine. Dubbed &quot;the City of Light&quot; (la Ville Lumiere)
<br>
