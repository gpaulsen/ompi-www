Actually, there is an unofficial processor affinity API on Mac OS X,
<br>
but it is supplied only with the CHUD framework.  I suppose as a
<br>
further barrier to using this API in code outside of Apple, the header
<br>
files for this API are only available with the standalone CHUD
<br>
installer.  See:
<br>
<a href="http://lists.apple.com/archives/xcode-users/2005/May/msg00939.html">http://lists.apple.com/archives/xcode-users/2005/May/msg00939.html</a>
<br>
<p>There is a blog posting that describes how to use this API here:
<br>
<a href="http://ridiculousfish.com/blog/archives/2007/02/17/barrier/">http://ridiculousfish.com/blog/archives/2007/02/17/barrier/</a>
