<p>So your tests show:
<br>
1. &quot;Shared library in FORTRAN + MPI executable in FORTRAN&quot; works.
<br>
2. &quot;Shared library in C++ + MPI executable in FORTRAN &quot; does not work.
<br>
<p>It seems to me that the symbols in C library are not really recognized by
<br>
FORTRAN executable as you thought. What compilers did yo use to built
<br>
OpenMPI?
<br>
<p>Different compiler has different convention to handle symbols. E.g. if there
<br>
is a variable &quot;var_foo&quot; in your FORTRAN code, some FORTRN compiler will save
<br>
&quot;var_foo_&quot; in the object file by default; if you want to access &quot;var_foo&quot; in
<br>
C code, you actually need to refer &quot;var_foo_&quot; in C code. If you define
<br>
&quot;var_foo&quot; in a module in the FORTAN compiler, some FORTRAN compiler may
<br>
append the module name to &quot;var_foo&quot;. 
<br>
So I suggest to check the symbols in the object files generated by your
<br>
FORTAN and C compiler to see the difference.
