For beginners, it is worth picking up some books.  I have used Using
<br>
MPI - 2nd Edition: Portable Parallel Programming with the Message
<br>
Passing Interface by Gropp, Lusk, and Skjellum.
<br>
<p>The reference for MPI is available online at
<br>
<a href="http://www.mpi-forum.org/docs/docs.html">http://www.mpi-forum.org/docs/docs.html</a>, but hard copies are nice to
<br>
have: MPI: The Complete Reference by Snir and Gropp.
