Hi All,
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;I wanted to switch from mpich2/mvapich2 to OpenMPI, as OpenMPI
<br>
supports both ethernet and infiniband. Before doing that I tested an
<br>
application 'GROMACS' to compare the performance of MPICH2 &amp; OpenMPI. Both
<br>
have been compiled with GNU compilers.
<br>
<p>After this benchmark, I came to know that OpenMPI is slower than MPICH2.
<br>
<p>This benchmark is run on a AMD dual core, dual opteron processor. Both have
<br>
compiled with default configurations.
<br>
<p>The job is run on 2 nodes - 8 cores.
<br>
<p>OpenMPI - 25 m 39 s.
<br>
MPICH2  -  15 m 53 s.
<br>
<p>Any comments ..?
<br>
