On May 30, 2008, at 9:55 AM, Andreas Sch&#228;fer wrote:
<br>
<p><span class="quotelev1">&gt; I've never really dig into Open MPI's guts, not because I wasn't
</span><br>
<span class="quotelev1">&gt; interested, but mainly because the time required to get my bearings
</span><br>
<span class="quotelev1">&gt; seemed just too much. Until now. I've watched a couple of the videos
</span><br>
<span class="quotelev1">&gt; while coding and it was pretty awesome. Easy to understand, structured
</span><br>
<span class="quotelev1">&gt; and well spoken.
</span><br>
<p>Good!  I'm glad you've found them useful.
<br>
<p><span class="quotelev2">&gt;&gt; - Do you like the format?
</span><br>
<span class="quotelev2">&gt;&gt;     - Is the (slides+narration) format useful?
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Yes, I like it a lot. I guess a pure podcast would be insufficient for
</span><br>
<span class="quotelev1">&gt; complex issues where you simply need diagrams.
</span><br>
<p>That was definitely my thought here -- pictures can be worth a million  
<br>
words, etc.
<br>
<p><span class="quotelev1">&gt; Maybe a small
</span><br>
<span class="quotelev1">&gt; suggestion: maybe it's just me, but I'd actually prefer (even) leaner
</span><br>
<span class="quotelev1">&gt; slides. Currently you're basically duplicating on screen what you're
</span><br>
<span class="quotelev1">&gt; saying, which is good when you're a nervous, moumbling college student
</span><br>
<span class="quotelev1">&gt; and might lose your audience somewhere. But when you're an experenced
</span><br>
<span class="quotelev1">&gt; speaker (which you obviously are), the audience does rarely need this
</span><br>
<span class="quotelev1">&gt; redundancy and might rather get confused when trying to digest both
</span><br>
<span class="quotelev1">&gt; streams of information (visual and auditory) simultaneously. But this
</span><br>
<span class="quotelev1">&gt; is of course a question of personal preference.
</span><br>
<p>Thanks for the compliment snuggled in there.  :-)
<br>
<p>Yes, this might be a style thing -- I have found that at least some  
<br>
people like to have slides that are more-or-less what the speaker  
<br>
actually said for two reasons:
<br>
<p>- so that the visuals and audio agree with each other -- it's not two  
<br>
different through processes while you're trying to absorb the  
<br>
information.  Sure, some people read ahead on the slide and get bored  
<br>
because the speaker eventually catches up, but at least in my  
<br>
experience, these people are a minority.
<br>
<p>- more importantly, however, the audience likes to take the slides  
<br>
away and when they actually look at them 6 weeks after the lecture,  
<br>
they might actually remember the content better because they received  
<br>
the same information via two forms of sensory input (audio + visual).
<br>
<p><span class="quotelev2">&gt;&gt;     - Would terminal screen-scrape sessions be useful?
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; I'd prefer how-to pages for this, as you can copy&amp;paste the commands
</span><br>
<span class="quotelev1">&gt; directly into your own shell.
</span><br>
<p>Good point.
<br>
<p><span class="quotelev2">&gt;&gt;     - ...other [low-budget] suggestions?
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Maybe an a tad higher audio bitrate. And some people don't like the
</span><br>
<span class="quotelev1">&gt; .mov format, but that isn't really important.
</span><br>
<p><p>Ok, I can bump up the audio rate and see what happens to the filesize  
<br>
(that was my prime concern, actually).  Plus it *is* just the builtin  
<br>
microphone on my Mac, so it may not be the greatest sound quality to  
<br>
begin with.  :-)
<br>
<p>As for .mov, yes, this is definitely a compromise.  I tried uploading  
<br>
the videos to YouTube and Google Video and a few others, but a) most  
<br>
have a time or file size restriction (e.g., 10 mins max) -- I was not  
<br>
willing to spend the extra work to split up the videos into multiple  
<br>
segments, and b) they down-res'ed the videos so much as to make the  
<br>
slides look crappy and/or unreadable.  So I had to go with the video  
<br>
encoder that I could get for darn little money (Cisco's a big company,  
<br>
but my budget is still tiny :-) ).  That turned out to be a fun little  
<br>
program called iShowU for OS X that does screen scraping + audio  
<br>
capture.  It outputs Quicktime movies, so that was really my only  
<br>
choice.
<br>
<p>Is it a real hardship for people to install the QT player?  Are there  
<br>
easy-to-install convertors?  I'm not opposed to hosting it in multiple  
<br>
formats if it's easy and free to convert them.
