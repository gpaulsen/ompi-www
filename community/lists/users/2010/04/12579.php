<p>
<span class="quotelev2"> &gt;&gt; However, there are cases when being able to specify the hostfile is
</span><br>
<span class="quotelev2"> &gt;&gt; important (hybrid jobs, users with MPICH jobs, etc.).
</span><br>
<p><span class="quotelev1"> &gt;[I don't understand what MPICH has to do with it.]
</span><br>
<p>This was just an example of how the different behavior of OMPI 1.4 may 
<br>
cause problems. The MPICH library is not the subject of discussion. 
<br>
MPICH requires the use of hostfile, which is generated by SGE, and 
<br>
having it in the submission for an Open MPI 1.2.x job has an expected 
<br>
effect. This is different for Open MPI 1.4.x, which appears not 
<br>
interpreting the host file properly.
<br>
<p><span class="quotelev2"> &gt;&gt; For example,
</span><br>
<span class="quotelev2"> &gt;&gt; with Grid Engine I can request four 4-core nodes, that is total of 16
</span><br>
<span class="quotelev2"> &gt;&gt; slots. But I also want to specify how to distribute processes on the
</span><br>
<span class="quotelev2"> &gt;&gt; nodes, so I create the file 'hosts'
</span><br>
<span class="quotelev2"> &gt;&gt;
</span><br>
<span class="quotelev2"> &gt;&gt; node01 slots=1
</span><br>
<span class="quotelev2"> &gt;&gt; node02 slots=1
</span><br>
<span class="quotelev2"> &gt;&gt; node03 slots=1
</span><br>
<span class="quotelev2"> &gt;&gt; node04 slots=1
</span><br>
<span class="quotelev2"> &gt;&gt;
</span><br>
<span class="quotelev2"> &gt;&gt; and modify the line in the submission script to:
</span><br>
<span class="quotelev2"> &gt;&gt; mpirun -hostfile hosts ./program
</span><br>
<p><span class="quotelev1"> &gt; Regardless of any open-mpi bug, I'd have thought it was easier just to
</span><br>
<span class="quotelev1"> &gt; use -npernode in that case. What's the problem with that? It seems to
</span><br>
<span class="quotelev1"> &gt; me best generally to control the distribution of processes with mpirun
</span><br>
<span class="quotelev1"> &gt; on the SGE-allocated nodes than to concoct host files as we used to do
</span><br>
<span class="quotelev1"> &gt; here, e.g. to get -byslot v. -bynode behaviour (or vice versa).
</span><br>
<p>This is exactly what I am doing -- controlling distribution of processes 
<br>
with mpirun on the SGE-allocated nodes, by supplying the hostfile. Grid 
<br>
Engine allocates nodes and generates a hostfile, which I then can modify 
<br>
however I want to, before running the mpirun command. Moreover, it gives 
<br>
more control, by allowing to create specific SGE parallel environments, 
<br>
where the process distribution is predetermined -- one less worry for 
<br>
users playing with mpirun options.
<br>
<p>The example in my initial email was deliberately simplified to 
<br>
demonstrate the problem.
<br>
<p>= Serge
