<p>
I have some simple questions for all you users out there about the OMPI FAQ.  I ask because we see a LOT of you end up on the OMPI FAQ in our web statistics (most users who search either end up on the FAQ and/or on the web archives of the mailing list).  Hence, I'd like to know if we can improve the FAQ from a usability standpoint.
<br>
<p>1. Is the FAQ useful in its current form?  More specifically:
<br>
<p>- I personally find it a little difficult to web search for something and then end up on a single FAQ page with a LOT of information on it (e.g., the text for all the questions/answers in that category).  I.e., if I'm searching for something specific, it would be useful to end up on a page with *just that one FAQ question/answer*.
<br>
<p>- OTOH, if I don't know exactly what I'm looking for, it is useful to see a whole page of FAQ questions and answers so that I can scan through them all to find what I'm looking for (vs. clicking through a million different individual pages).
<br>
<p>2. We wrote all the PHP for the OMPI FAQ ourselves (it's not driven by a database; the content is all in individual text files).  Back when we started, we surveyed the web FAQ systems and found each of them lacking for one reason or another (I don't remember the details), and therefore wrote our own PHP stuff.  Do people have other FAQ web systems that they'd recommend these days?
<br>
<p>3. Are there other features from an FAQ that you would like to see in the OMPI FAQ?
<br>
<p>I ask these questions because a) the current system has annoyed me a few too many times recently for various limitations, and b) I'm wondering if there is something better out there -- better searching, more web-2.0-ish, ...whatever.  We're certainly not tied to the existing FAQ system -- the current set of questions and answers is fairly easy to extract from the PHP, so we could move it to another system if it would be desirable.
