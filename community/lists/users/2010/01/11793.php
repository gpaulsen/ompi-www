On Thu, 14 Jan 2010 21:55:06 -0500, Jeff Squyres &lt;jsquyres_at_[hidden]&gt; wrote:
<br>
<span class="quotelev1">&gt; That being said, you could sign up on it and then set your membership to receive no mail...?
</span><br>
<p>This is especially dangerous because the Open MPI lists munge the
<br>
Reply-To header, which is a bad thing
<br>
<p>&nbsp;&nbsp;<a href="http://www.unicom.com/pw/reply-to-harmful.html">http://www.unicom.com/pw/reply-to-harmful.html</a>
<br>
<p>But lots of mailers have poor default handling of mailing lists, so it's
<br>
complicated.
<br>
<p>With munging, a mailer's &quot;reply-to-sender&quot; function will send mail
<br>
*only* to the list and &quot;reply-to-all&quot; will send it to the list and any
<br>
other recipients, but *not* the sender (unless the mailer does special
<br>
detection of munged reply-to headers).  This makes it rather difficult
<br>
to participate in a discussion without receiving mail from the list, or
<br>
even to reliably filter list traffic (you have to write filter rules
<br>
that walk the References tree to find if it is something that would be
<br>
interesting to you, and then you get false positives from people who
<br>
reply to an existing thread when they wanted to make a new thread).
<br>
