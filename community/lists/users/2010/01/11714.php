Hi,
<br>
<p>I can only speak to Sun's use of uDAPL as it relates to MPI.
<br>
<p>&nbsp;From what I can tell Sun is the only group interested in the uDAPL BTL 
<br>
in Open MPI at this point. In contrast, Open MPI has many active members 
<br>
interested in the development of the OpenIB BTL which uses the user 
<br>
verbs protocol. Because of the community commitment Sun's MPI product 
<br>
based on Open MPI, ClusterTools, will also be moving away from the uDAPL 
<br>
BTL in favor of the OpenIB BTL. As such we expect our development of the 
<br>
uDAPL BTL to drop off.
<br>
<p>-DON
