<p>
I'd suggest looking into TotalView (<a href="http://www.totalviewtech.com">http://www.totalviewtech.com</a>) and/or DDT
<br>
(<a href="http://www.allinea.com/">http://www.allinea.com/</a>).  I've used TotalView pretty extensively and found
<br>
it to be pretty easy to use.  They are both commercial, however, and not
<br>
cheap.
<br>
<p>As far as I know, there isn't a whole lot of open source support for
<br>
parallel debugging. The Parallel Tools Platform of Eclipse claims to provide
<br>
a parallel debugger, though I have yet to try it (
<br>
<a href="http://www.eclipse.org/ptp/">http://www.eclipse.org/ptp/</a>).
<br>
