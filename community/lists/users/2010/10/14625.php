<p>
Open MPI users --
<br>
<p>I took a little heat at the last the MPI Forum for not having Open MPI be fully complaint with MPI-2.2 yet (OMPI is compliant with MPI-2.1).  Specifically, there's still 4 open issues in Open MPI that are necessary for full MPI-2.2 compliance:
<br>
<p><a href="https://svn.open-mpi.org/trac/ompi/query?status=accepted&amp;status=assigned&amp;status=new&amp;status=reopened&amp;summary=~MPI-2.2&amp;col=id&amp;col=summary&amp;col=status&amp;col=type&amp;col=priority&amp;col=milestone&amp;col=version&amp;order=priority">https://svn.open-mpi.org/trac/ompi/query?status=accepted&amp;status=assigned&amp;status=new&amp;status=reopened&amp;summary=~MPI-2.2&amp;col=id&amp;col=summary&amp;col=status&amp;col=type&amp;col=priority&amp;col=milestone&amp;col=version&amp;order=priority</a>
<br>
<p>We haven't made these items a priority because -- to be blunt -- no one really has been asking for them.  No one has come forward and said &quot;I *must* have these features!&quot; (to be fair, they're somewhat obscure features).  
<br>
<p>Other than not having the obvious &quot;OMPI is MPI-2.2 compliant&quot; checkmark for marketing reasons, is there anyone who *needs* the functionality represented by those still-open tickets?
<br>
<p>Thanks for your input.
