<p>
Open MPI's fault tolerance is still somewhat rudimentary; it's a complex topic within the entire scope of MPI.  There has been much research into MPI and fault tolerance over the years; the MPI Forum itself is grappling with terms and definitions that make sense.  It's by no means a &quot;solved&quot; problem.
<br>
<p>It's unfortunately unsurprising that Open MPI may hang in the case of a node crash.  I wish that I had a better answer for you, but I don't.  :-\
