<p>
Who's going to SC'10 this year?  (/me raises hand)  If you're there, be sure to drop by and say hello (I'll be in and around the Cisco Booth). 
<br>
<p>Dr. George Bosilca (U. Tennessee Knoxville) and I will host our normal &quot;Open MPI State of the Union&quot; BOF on Wednesday at 12:15pm.  It's a great place to sit, eat your lunch, and hear about current and future directions in Open MPI:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://sc10.supercomputing.org/schedule/event_detail.php?evid=bof138">http://sc10.supercomputing.org/schedule/event_detail.php?evid=bof138</a>
<br>
<p>Cisco is hosting several Open MPI organizations to give short 10-15 minute talks on Open MPI (and related) topics in the Cisco booth.  Drop by the Cisco booth (right next to the main SciNet NOC) to see the exact schedule:
<br>
<p>&nbsp;* Dr. Brice Goglin, INRIA: Hardware Locality is good for you
<br>
&nbsp;* Dr. Josh Hursey, ORNL: A Fault Tolerant MPI Standard for HPC Applications and Libraries
<br>
&nbsp;* Dr. Rainer Keller, HLRS: Finding memory errors in MPI applications
<br>
&nbsp;* Dr. Edgar Gabriel, U. Houston: VolpexMPI: robust execution of MPI applications through process replication
<br>
<p>There will also be an MPI Forum BOF on Tuesday at 12:15 where Dr. Rich Graham (Oak Ridge National Labs) will present the current state of the Forum's deliberations on MPI-3.
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://sc10.supercomputing.org/schedule/event_detail.php?evid=bof122">http://sc10.supercomputing.org/schedule/event_detail.php?evid=bof122</a>
<br>
<p>Many other Open MPI organizations who will also have a presence at SC; be sure to stop by their booths and say hello.
<br>
<p>See you there!
