<p><span class="quotelev1">&gt; After some lurking and reading, I plan this:
</span><br>
<span class="quotelev1">&gt;   Debian (lenny)
</span><br>
<span class="quotelev1">&gt;   + fai                   - for compute-node operating system install
</span><br>
<span class="quotelev1">&gt;   + Torque                - job scheduler/manager
</span><br>
<span class="quotelev1">&gt;   + MPI (Intel MPI)       - for the application
</span><br>
<span class="quotelev1">&gt;   + MPI (OpenMP)          - alternative MPI
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; Does anyone see holes in this plan?
</span><br>
<p>HPC is very much a &quot;what is best for *your* requirements&quot; kind of environment.  There are many different recipes out there for different kinds of HPC environments.
<br>
<p>What you listed above is a reasonable list of meta software packages.  You might want to install them, run with them for a while, and be prepared to change if you find something else that you like better (you might want to make sure that all those software packages support Debian, for example).
