Hello OpenMPI developers,
<br>
<p>I am searching for a way to discover _all_ configure options of an 
<br>
OpenMPI installation.
<br>
<p>Background: in a existing installation, the ompi_info program helps to 
<br>
find out a lot of informations about the installation. So, &quot;ompi_info 
<br>
-c&quot; shows *some* configuration options like CFLAGS, FFLAGS et cetera. 
<br>
Compilation directories often does not survive for long time (or are not 
<br>
shipped at all, e.g. with SunMPI)
<br>
<p>But what about --enable-mpi-threads or --enable-contrib-no-build=vt for 
<br>
example (and all other possible) flags of &quot;configure&quot;, how can I see 
<br>
would these flags set or would not?
<br>
<p>In other words: is it possible to get _all_ flags of configure from an 
<br>
&quot;ready&quot; installation in without having the compilation dirs (with 
<br>
configure logs) any more?
