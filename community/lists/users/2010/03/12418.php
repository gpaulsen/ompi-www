<p>Please upgrade to Open MPI if possible.  :-)
<br>
<p><span class="quotelev1">&gt; Here is the problem. I am using a 18
</span><br>
<span class="quotelev1">&gt; node cluster, each node has 2 CPU and each CPU supports up to 2
</span><br>
<span class="quotelev1">&gt; threads. So I assume I can use 18*4 number of processors. As running
</span><br>
<span class="quotelev1">&gt; the following code, an error message will always pops up for np=30 or
</span><br>
<span class="quotelev1">&gt; np=60.
</span><br>
<p>Depending on your CPU type and application behavior, using hyperthreads may be more of a hinderance than a help.
<br>
<p><span class="quotelev1">&gt; But works fine for np=12, np=1. The error message is always the
</span><br>
<span class="quotelev1">&gt; same, something like: one of  the processor n15, exit with (0), ip
</span><br>
<span class="quotelev1">&gt; 192......,
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; Here is a part of the code, where the n15 exit. All other PE can
</span><br>
<span class="quotelev1">&gt; finish writing the file, except PE15. Then I see the error message
</span><br>
<span class="quotelev1">&gt; about n15 and the written of file by PE15 is not completed.  An quick
</span><br>
<span class="quotelev1">&gt; question here, is PE15 necessarily generated by node 15 on the
</span><br>
<span class="quotelev1">&gt; cluster? Appreciate if anyone would share experiences in debuging
</span><br>
<span class="quotelev1">&gt; errors like this.
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; code:
</span><br>
<span class="quotelev1">&gt; ....
</span><br>
<span class="quotelev1">&gt; sprintf(p_obsfile,&quot;%s%d&quot;,obsfile,my_rank); //my_rank is processor ID,
</span><br>
<span class="quotelev1">&gt; each PE opens a different file
</span><br>
<p>If each MPI process is opening a separate file, then it may not be a file issue that is causing the problem.  For example, if each process opens /dev/null, do you have the same problem?
<br>
<p><span class="quotelev1">&gt;         if ((fp=fopen(p_obsfile,&quot;w&quot;))==NULL)
</span><br>
<span class="quotelev1">&gt;                 printf(&quot;PE_%d: The file %s cannot be opened\n&quot;,my_rank,p_obsfile);
</span><br>
<p>I do note that you don't have an escape clause here -- if you fail to open the file, you still fall through and try to write to the file.
<br>
<p><span class="quotelev1">&gt;         for (int id=loc*my_rank;id&lt;loc*(my_rank+1);id++){  // loc=TotalNum/NumofPE
</span><br>
<span class="quotelev1">&gt;                 //call a function to calculate U, the function will return the
</span><br>
<span class="quotelev1">&gt; finishing message
</span><br>
<span class="quotelev1">&gt;                // no communication is needed among processors
</span><br>
<span class="quotelev1">&gt;                 for (int j=0;j&lt;NUM;j++)
</span><br>
<span class="quotelev1">&gt;                         fprintf (fp, &quot;%f\n&quot;,U[j]); //output updated U
</span><br>
<span class="quotelev1">&gt;         }
</span><br>
<p>I think you just want to try standard debugging stuff here -- are you going beyond the end of the U array?  And so on.  Perhaps try running your app through valgrind, or under a debugger, etc.  Do you get corefiles from the run?  And so on.
