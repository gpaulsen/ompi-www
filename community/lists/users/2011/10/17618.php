<p>
Open MPI (trunk/1.7 - not 1.4 or 1.5) provides an application level
<br>
interface to request a checkpoint of an application. This API is
<br>
defined on the following website:
<br>
&nbsp;&nbsp;<a href="http://osl.iu.edu/research/ft/ompi-cr/api.php#api-cr_checkpoint">http://osl.iu.edu/research/ft/ompi-cr/api.php#api-cr_checkpoint</a>
<br>
<p>This will behave the same as if you requested the checkpoint of the
<br>
job from the command line.
<br>
