<br>
Fortran libraries.
<br>
<p>If you want to compile Open MPI, you need to install a Fortran compiler, 
<br>
for example, Intel Fortran Compiler. And in the CMake GUI, you'll be 
<br>
able to enable building MPI Fortran bindings.
