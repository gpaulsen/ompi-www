On Feb 2, 2011, at 1:44 PM, Jeffrey A Cummings wrote:
<br>
<p><span class="quotelev1">&gt; I've encountered a supposed OpenMPI version of 1.4-4.  Is the hyphen a typo or is this syntax correct and if so what does it mean? 
</span><br>
<p>Is this an RPM version number?  It's fairly common for RPMs to add &quot;-X&quot; at the end of the version number.  The &quot;X&quot; indicates the RPM version number (i.e., the version number of the packaging -- not the package itself).
<br>
<p>Open MPI's version number scheme is explained here:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.open-mpi.org/software/ompi/versions/">http://www.open-mpi.org/software/ompi/versions/</a>
