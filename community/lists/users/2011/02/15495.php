<br>
<p>Is this an RPM version number?  It's fairly common for RPMs to add &quot;-X&quot; at 
<br>
the end of the version number.  The &quot;X&quot; indicates the RPM version number 
<br>
(i.e., the version number of the packaging -- not the package itself).
<br>
<p>Open MPI's version number scheme is explained here:
<br>
