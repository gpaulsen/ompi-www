<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://icl.eecs.utk.edu/heteropar2011/">http://icl.eecs.utk.edu/heteropar2011/</a>
<br>
======================================================================
<br>
<p>&nbsp;* Submission of manuscripts:               Monday,  June 20, 2011
<br>
&nbsp;* Notification of acceptance:              Friday,  July   8, 2011
<br>
&nbsp;* Deadline for final version:              Friday,  July  29, 2011
<br>
&nbsp;* Date of workshop:                        Tuesday, August 29, 2011
<br>
<p>Heterogeneity is emerging as one of the most profound and
<br>
challenging characteristics of today's parallel environments. From the
<br>
macro level, where networks of distributed computers, composed by
<br>
diverse node architectures, are interconnected with potentially
<br>
heterogeneous networks, to the micro level, where deeper memory
<br>
hierarchies and various accelerator architectures are increasingly
<br>
common, the impact of heterogeneity on all computing tasks is
<br>
increasing rapidly. Traditional parallel algorithms, programming
<br>
environments and tools, designed for legacy homogeneous
<br>
multiprocessors, can at best achieve on a small fraction of the
<br>
efficiency and potential performance we should expect from parallel
<br>
computing in tomorrow's highly diversified and mixed environments. New
<br>
ideas, innovative algorithms, and specialized programming environments
<br>
and tools are needed to efficiently use these new and multifarious
<br>
parallel architectures. The workshop is intended to be a forum for
<br>
researchers working on algorithms, programming languages, tools, and
<br>
theoretical models aimed at efficiently solving problems on
<br>
heterogeneous networks.
<br>
<p>Authors are encouraged to submit original, unpublished research or
<br>
overviews on Algorithms, Models and Tools for Parallel Computing on
<br>
Heterogeneous Platforms. Manuscripts should be limited to 10 pages in
<br>
Springer LNCS style sheet and submitted through the EasyChair
<br>
Conference System: <a href="https://www.easychair.org/conferences/?conf=heteropar11">https://www.easychair.org/conferences/?conf=heteropar11</a>.  
<br>
Accepted papers that are presented at the workshop will be published
<br>
in revised form in a special Euro-Par Workshop Volume in the Lecture
<br>
Notes in Computer Science (LNCS) series after the Euro-Par conference.
<br>
<p><p>The topics to be covered include but are not limited to:
<br>
<p>&nbsp;&nbsp;&nbsp;* Heterogeneous parallel programming paradigms and models;
<br>
<p>&nbsp;&nbsp;&nbsp;* Performance models and their integration into the design of efficient
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;parallel algorithms for heterogeneous platforms;
<br>
<p>&nbsp;&nbsp;&nbsp;* Performance models and their integration into the design of
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;efficient parallel algorithms for heterogeneous platforms;
<br>
<p>&nbsp;&nbsp;&nbsp;* Parallel algorithms for heterogeneous or hierarchical systems,
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;including manycores and hardware accelerators (FPGAs, GPUs, etc.);
<br>
<p>&nbsp;&nbsp;&nbsp;* Parallel algorithms for efficient problem solving on
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;heterogeneous platforms (numerical linear algebra, nonlinear
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;systems, fast transforms, computational biology, data mining,
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;multimedia, etc.);
<br>
<p>&nbsp;&nbsp;&nbsp;* Software engineering for heterogeneous parallel systems;
<br>
<p>&nbsp;&nbsp;&nbsp;* Applications on heterogeneous platforms;
<br>
<p>&nbsp;&nbsp;&nbsp;* Integration of parallel and distributed computing on
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;heterogeneous platforms;
<br>
<p>&nbsp;&nbsp;&nbsp;* Experience of porting parallel software from supercomputers to
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;heterogeneous platforms;
<br>
<p>&nbsp;&nbsp;&nbsp;* Fault tolerance of parallel computations on heterogeneous
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;platforms;
<br>
<p>&nbsp;&nbsp;&nbsp;* Algorithms, models and tools for grid, desktop grid, cloud, and
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;green computing.
