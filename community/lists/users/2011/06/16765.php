Greetings Alan.  Sorry for the delay in replying.
<br>
<p>We don't have a definite timeline for when 1.5 will transition to become 1.6 (i.e., the &quot;super stable&quot; equivalent of the 1.5 series).  We are looking at it more in terms of features -- when we have the desired set of features (which, as a natural side-effect, is usually paired with enough elapsed time for the v1.5 series code to become mature), we'll transition it.
<br>
<p>We're looking for one more major feature in the v1.5 series: a bunch of process affinity improvements (which ends up touching quite a bit of code, which is why it's taking some time).
<br>
