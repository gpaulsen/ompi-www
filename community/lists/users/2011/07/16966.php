&nbsp;&nbsp;&nbsp;**********************************
<br>
<p><p>BACKGROUND AND TOPICS
<br>
<p>EuroMPI is the primary meeting where the users and developers of MPI and other
<br>
message-passing programming environments can interact. The 18th European MPI
<br>
Users' Group Meeting will be a forum for the users and developers of MPI, but
<br>
also welcome hybrid programing models that combine message passing with
<br>
programming of modern architectures such as multi-core, or accelerators.
<br>
<p>Through the presentation of contributed papers, poster presentations and
<br>
invited talks, attendees will have the opportunity to share ideas and
<br>
experiences to contribute to the improvement and furthering of message-passing
<br>
and related parallel programming paradigms.
<br>
<p>Topics of interest for the meeting include:
<br>
<p>&nbsp;&nbsp;&nbsp;Algorithms using the message-passing paradigm
<br>
&nbsp;&nbsp;&nbsp;Applications in science and engineering based on message-passing
<br>
&nbsp;&nbsp;&nbsp;User experiences in programming heterogeneous systems using MPI
<br>
&nbsp;&nbsp;&nbsp;Tools and environments for programming heterogeneous systems using MPI
<br>
&nbsp;&nbsp;&nbsp;MPI implementation issues and improvements
<br>
&nbsp;&nbsp;&nbsp;Latest extensions to MPI
<br>
&nbsp;&nbsp;&nbsp;MPI for high-performance computing, clusters and grid environments
<br>
&nbsp;&nbsp;&nbsp;New message-passing and hybrid parallel programming paradigms
<br>
&nbsp;&nbsp;&nbsp;Interaction between message-passing software and hardware
<br>
&nbsp;&nbsp;&nbsp;Fault tolerance in message-passing programs
<br>
&nbsp;&nbsp;&nbsp;Performance evaluation of MPI applications
<br>
&nbsp;&nbsp;&nbsp;Tools and environments for MPI
<br>
<p>Four invited talks will be given by
<br>
<p>Pete Beckman, Director, Exascale Technology and Computing Institute
<br>
George Bosilca, Research Professor, Innovative Computing Laboratory,UTK
<br>
Michael Resch, Director, High Performance Computing Center Stuttgart (HLRS)
<br>
Sudip Dosanjh, Senior Manager, Sandia National Laboratories
<br>
<p>In the scientific program 29 selected papers will be presented, including two best papers, and 9 poster/short presentations.
<br>
<p>The Special Session on Improving MPI User And Developer Interaction (IMUDI) will be held in conjunction with EuroMPI (<a href="http://press.mcs.anl.gov/imudi2011/cfp/">http://press.mcs.anl.gov/imudi2011/cfp/</a>) on Wednesday 21st September. In IMUDI two invited talks will be given and three papers will be presented.
