<p>
You may or may not realize it, but Open MPI has several mirror web sites around the world -- see <a href="https://www.open-mpi.org/community/mirrors/">https://www.open-mpi.org/community/mirrors/</a>
<br>
<p>One of the primary reasons we started the web site mirror program was to help out those for whom bandwidth is expensive.  I.e., the idea was that users could use a local mirror and greatly decrease both web site latency/increase web site responsiveness, and also decrease bandwidth costs (i.e., downloading lots of pages repeatedly from the USA).
<br>
<p>Is this still an issue for users?
<br>
<p>I ask because Open MPI is likely to move its electronic hosting within the next few months, and the mirroring program came up: none of us know if there is a real need for this program any more.
