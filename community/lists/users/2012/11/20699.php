<span class="quotelev1">&gt; Details on MPI-3.0: 
</span><br>
<span class="quotelev1">&gt; ------------------- 
</span><br>
<span class="quotelev1">&gt; MPI-3.0 is a new version of the MPI standard that better serves the
</span><br>
<span class="quotelev1">&gt; needs of the parallel computing community for better platform and
</span><br>
<span class="quotelev1">&gt; application support.
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; Major improvements are:
</span><br>
<span class="quotelev1">&gt; - Improvements to the one-sided communication:
</span><br>
<span class="quotelev1">&gt;    -- New window allocation methods
</span><br>
<span class="quotelev1">&gt;    -- New RMA interfaces
</span><br>
<span class="quotelev1">&gt;    -- Special support for clusters of shared memory nodes
</span><br>
<span class="quotelev1">&gt; - Nonblocking collectives
</span><br>
<span class="quotelev1">&gt; - Scalable sparse collectives (neighbor communication in topoligies)
</span><br>
<span class="quotelev1">&gt; - Thread-safe message probe
</span><br>
<span class="quotelev1">&gt; - New MPI Fortran binding that is the first time consistent
</span><br>
<span class="quotelev1">&gt;   with the Fortran standard: the new mpi_f08 module
</span><br>
<span class="quotelev1">&gt; - Enhancements for the existing Fortran mpi module
</span><br>
<span class="quotelev1">&gt; - Non-collective communicator creation
</span><br>
<span class="quotelev1">&gt; - Support for large counts (larger than 32 bit values)
</span><br>
<span class="quotelev1">&gt; - Enhancements to MPI_Init, etc.
</span><br>
<span class="quotelev1">&gt; - Additional new tools interface
</span><br>
<span class="quotelev1">&gt; - The MPIR process acquisition interface (an extra document).
</span><br>
<span class="quotelev1">&gt; Major removals are:
</span><br>
<span class="quotelev1">&gt; - Some MPI-1.1 functionality that was deprecated since MPI-2.0.
</span><br>
<span class="quotelev1">&gt; - For C++ programming, the C++ bindings were substituted 
</span><br>
<span class="quotelev1">&gt;   by the C bindings. For this, several MPI-2.2 errata items 
</span><br>
<span class="quotelev1">&gt;   were defined.
</span><br>
<span class="quotelev1">&gt; 
