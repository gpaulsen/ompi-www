Hi folks
<br>
<p>With support from EMC, several of us in the Open MPI community (including LANL, Cisco, HLRS, and Oracle) have integrated Java bindings into the Open MPI code. The new bindings are not part of a formal release as this time (will be in the upcoming 1.7 series), but can be obtained from the Open MPI web site at:
<br>
<p><a href="http://www.open-mpi.org/nightly/trunk/">http://www.open-mpi.org/nightly/trunk/</a>
<br>
<p>Answers to questions (e.g., why), plus instructions on how to build the binding support, build and run applications, etc. can be found in our FAQ section:
<br>
<p><a href="http://www.open-mpi.org/faq/?category=java">http://www.open-mpi.org/faq/?category=java</a>
<br>
<p>Please feel free to post questions and bug reports to the OMPI user mailing list.
