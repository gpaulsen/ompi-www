<br>
<p>Intel MPI Benchmarks suite (<a href="http://software.intel.com/en-us/articles/intel-mpi-benchmarks/">http://software.intel.com/en-us/articles/intel-mpi-benchmarks/</a>) will probably measure more things about your MPI environment than you'd ever need to know :)
<br>
<p>NetPIPE (<a href="http://www.scl.ameslab.gov/netpipe/">http://www.scl.ameslab.gov/netpipe/</a>) also has an MPI version. It measures point-to-point bandwidth and latency and has the option to test the effect of using unaligned memory buffers.
