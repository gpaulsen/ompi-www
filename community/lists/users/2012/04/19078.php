<p>
I've released a new version of ppe-ompi 
<br>
&lt;<a href="http://norbl.com/ppe-ompi/ppe-ompi.html">http://norbl.com/ppe-ompi/ppe-ompi.html</a>&gt;, which is a system for running 
<br>
an Open MPI network on EC2 (Amazon's cloud computing service). Download 
<br>
here &lt;<a href="http://code.google.com/p/ppe-ompi/downloads/list">http://code.google.com/p/ppe-ompi/downloads/list</a>&gt;.
<br>
<p><p>Release notes:
<br>
<p>&nbsp;&nbsp;* AMIs with Open MPI 1.4.5 are available.
<br>
&nbsp;&nbsp;* EBS volumes can be attached to instances using the ec2 network manager.
<br>
&nbsp;&nbsp;* A simple ssh shell for access to an ec2 instance is built into the
<br>
&nbsp;&nbsp;&nbsp;&nbsp;ec2 network manager.
<br>
&nbsp;&nbsp;* Amazon client configuration parameters can be set in the ec2 network
<br>
&nbsp;&nbsp;&nbsp;&nbsp;manager. Some of the parameters may be needed to access AWS if you
<br>
&nbsp;&nbsp;&nbsp;&nbsp;are using a proxy server.
<br>
&nbsp;&nbsp;* The network specification manager shows more information about
<br>
&nbsp;&nbsp;&nbsp;&nbsp;instance types and AMIs.
<br>
