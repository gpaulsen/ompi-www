<p>
Hi all
<br>
<p>For those of you who are interested in how Open MPI works, Greenplum/EMC recorded a set of talks last week covering several relevant topics. These talks are intended to help new developers become familiar with the more common parts of the code base. Greenplum is happy to make these available to the general community, and so we have posted them on the Open MPI web site at:
<br>
<p><a href="http://www.open-mpi.org/video/">http://www.open-mpi.org/video/</a>
<br>
<p>In addition, last week's talks included one from Moe Jette on the Slurm resource manager, and another by yours truly on the porting of Map-Reduce to the OMPI code base. The following talks are available online or for download (including the slides):
<br>
<p><p>Open MPI: Overview/Architecture by Jeff Squyres
<br>
<p>Detailed overview of the Open MPI data transfer system by Brian Barrett
<br>
<p>Overview of parallel I/O in Open MPI by Edgar Gabriel
<br>
<p>Architecture, configuration, and use of Slurm by Moe Jette
<br>
<p>Overview of Greenplum's port of Apache Hadoop\s Map-Reduce system to Open MPI by Ralph Castain
<br>
<p>We hope these prove useful to you. Future videos on ORTE's design and messaging system are planned - requests for additional topics are welcome.
<br>
Ralph
