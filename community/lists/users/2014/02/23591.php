<br>
<p>&nbsp;BACKGROUND
<br>
Since 2011, the most powerful supercomputers systems ranked in the Top500
<br>
list are hybrid systems composed of thousands of nodes that includes CPUs
<br>
and accelerators, as Xeon Phi and GPUs. Programming and deploying
<br>
applications on those systems is still a challenging work, due to the
<br>
complexity of the system and the need to mix several programming interfaces
<br>
(MPI, CUDA, Intel Xeon Phi) in the same application.This Workshop is aimed
<br>
to explore the state of the art of developing applications in accelerated
<br>
massive HPC architectures, including practical issues of hybrid usage models
<br>
with MPI, OpenMP, and other accelerators programming models. The idea is to
<br>
publish novel work on the use of available programming interfaces (MPI,
<br>
CUDA, Intel Xeon Phi) and tools for code development, application
<br>
performance optimizations, application deployment on accelerated systems, as
<br>
well as the advantages and limitations of accelerated HPC systems.
<br>
Experiences with real-world applications, including scientific computing,
<br>
numerical simulations, healthcare, energy, data-analysis, etc. are also
<br>
encouraged.
<br>
<p>TOPICS
<br>
Areas of interest of the workshop include, but are not limited to: 
<br>
<p>Tools, libraries, and environments for accelerators.
<br>
Hybrid and heterogeneous programming with MPI and accelerators.
<br>
Performance evaluation scientific applications based on accelerators.
<br>
Automatic performance tuning of scientific applications with accelerators.
<br>
Integrating accelerators on existing HPC middlewares.
<br>
Run-times for accelerators.
<br>
Energy efficient HPC solutions based on accelerators.
<br>
Storage cache solutions based on SSD accelerators.
<br>
Parallel data analysis for MPI and SSD.
<br>
Real-world scientific and engineering applications using accelerated HPC.
<br>
Future trends and prospects for accelerated HPC.
