It isn't a development tarball - it's the current state of the release branch and is therefore managed much more strictly than the developer trunk. We are preparing it now for release candidate. I have about a dozen CMR's waiting for final review before moving across to 1.8.2, and then we'll begin the release process.
<br>
<p>Most of those changes involve closing memory leaks, and a few deal with more esoteric use-cases, so I think most users would probably be okay at the moment. The nightly is passing rather cleanly, actually, so I'm feeling good that the ETA for release isn't far off. Maybe not end June as last-minute things always surface in the RC process, but likely early July.
