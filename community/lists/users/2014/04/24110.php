If your examples are anything more than trivial code, we'll probably need a signed contribution agreement.  This is a bit of a hassle, but it's an unfortunate necessity so that we can ensure that the Open MPI code base stays 100% open source / unencumbered by IP restrictions:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.open-mpi.org/community/contribute/">http://www.open-mpi.org/community/contribute/</a>
<br>
<p>That being said, perhaps you'd like to author some FAQ items in the Java category on the Open MPI web site?  There's not very much there right now; it could probably benefit from some examples, etc. (<a href="http://www.open-mpi.org/faq/?category=java">http://www.open-mpi.org/faq/?category=java</a>).
