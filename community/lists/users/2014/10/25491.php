<p>
Since many of you are interested in MPI, I wanted to bring to your attention two new tutorial books on MPI:
<br>
<p>1. Using MPI: Portable Parallel Programming with the Message-Passing Interface, 3rd edition
<br>
&nbsp;&nbsp;&nbsp;by William Gropp, Ewing Lusk, and Anthony Skjellum
<br>
&nbsp;&nbsp;&nbsp;This is an updated version of their earlier book, and covers basic MPI. 
<br>
&nbsp;&nbsp;&nbsp;<a href="http://www.amazon.com/Using-MPI-Programming-Message-Passing-Engineering/dp/0262527391/">http://www.amazon.com/Using-MPI-Programming-Message-Passing-Engineering/dp/0262527391/</a>
<br>
<p>2. Using Advanced MPI: Modern Features of the Message-Passing Interface
<br>
&nbsp;&nbsp;&nbsp;by William Gropp, Torsten Hoefler, Rajeev Thakur, and Ewing Lusk
<br>
&nbsp;&nbsp;&nbsp;This is a new book on advanced features of MPI, including the new features in MPI-3.
<br>
&nbsp;&nbsp;&nbsp;<a href="http://www.amazon.com/Using-Advanced-MPI-Message-Passing-Engineering/dp/0262527634/">http://www.amazon.com/Using-Advanced-MPI-Message-Passing-Engineering/dp/0262527634/</a>
