<p>
Hi Ralph and list
<br>
<p>I am no developer, but my impression is that,
<br>
paraphrasing Mark Twain,
<br>
the reports about the death of the
<br>
Environment Modules (<a href="http://modules.sourceforge.net/">http://modules.sourceforge.net/</a>)
<br>
package have been exaggerated.
<br>
That presumed death/obsolescence has been
<br>
repeated a few times on the OMPI list,
<br>
which has a big intersection with the Environment Modules
<br>
actual and potential users,
<br>
a statement which IMHO is inaccurate, to say the least.
<br>
<p>The Environment Modules repository has 2014 entries:
<br>
<p><a href="http://sourceforge.net/p/modules/git/ci/master/tree/">http://sourceforge.net/p/modules/git/ci/master/tree/</a>
<br>
<p>The Environment Modules mailing list is active as well:
<br>
<p><a href="https://lists.sourceforge.net/lists/listinfo/modules-interest">https://lists.sourceforge.net/lists/listinfo/modules-interest</a>
<br>
<a href="http://sourceforge.net/p/modules/mailman/modules-interest/">http://sourceforge.net/p/modules/mailman/modules-interest/</a>
<br>
<p>The Environment Modules package user base is not negligible,
<br>
including many universities, research centers, national labs,
<br>
ans private companies, in the US and around the world.
<br>
How does the user base of LMod compare?
<br>
<p>Regardless of any virtues that LMod may have,
<br>
currently I don't see any reason to switch to LMod,
<br>
install everything over again, troubleshoot it,
<br>
learn Lua, migrate my modules from Tcl,
<br>
educate my users and convince them to use a new
<br>
package to achieve the same exact thing that they currently have,
<br>
and in the end gain little if any
<br>
relevant/useful/new functionality.
<br>
<p>My two cents of opinion
<br>
Gus Correa
