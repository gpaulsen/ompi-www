<p>
Researchers / academics: I have a question for you.
<br>
<p>Would it be useful for Open MPI to publish Digital Object Identifier (DOI) or Research Resource Identifier (RRI) to identify releases?
<br>
<p>Per articles like <a href="http://www.nature.com/news/the-unsung-heroes-of-scientific-software-1.19100">http://www.nature.com/news/the-unsung-heroes-of-scientific-software-1.19100</a>, would it be helpful to publish a DOI / RRI for each Open MPI release?  And/or nightly snapshot tarballs (e.g., if a paper used some results from a specific nightly tarball, or somesuch)?  I.e., does creating+publishing a DOI/RRI need to be something that is part of the release process?  ...or is the DOI effectively the git hash of the tag in ompi-release?
<br>
<p>More generally: are DOIs / RRIs helpful in citing specific software packages and/or versions in papers?  If so, do we need to do anything to support them in Open MPI releases?
<br>
<p>(...since I left academia long ago, I admit to not paying too close attention to this kind of stuff, so forgive me if I'm asking nonsensical questions...)
