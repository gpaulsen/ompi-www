Hi Mudit
<br>
<p>We welcome anyone interested in contributing! You have a couple of choices on method, depending on how the magnitude of contribution and whether or not you want formal credit for it in the &#226;&#128;&#156;authors&#226;&#128;&#157; section:
<br>
<p>* you can just do some work on a fork of the master repo and generate a pull request. We&#226;&#128;&#153;ll look at the code, perhaps make suggestions, and then a developer can commit it. We&#226;&#128;&#153;ll include a &#226;&#128;&#156;thanks to Mudit&#226;&#128;&#157; message, but you won&#226;&#128;&#153;t be listed as an official developer. We don&#226;&#128;&#153;t accept major changes that way, though - just minor stuff like a small bug fix.
<br>
<p>* you can sign and return the individual contributors form at . This adds you to the developer&#226;&#128;&#153;s list - but we still ask that you follow the same steps as above when submitting code to the repo, especially when becoming more familiar with the system.
<br>
<p>There are a number of on-going efforts you can become involved with, depending on your area of interest. Are you primarily interested in networking transports, implementing MPI collective algorithms, launch and runtime support, &#226;&#128;&#166;?
<br>
