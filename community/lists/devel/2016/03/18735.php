<br>
<p>This finally gives us a way to merge ompi and ompi-release!
<br>
<p>I think we'll want to do some testing, and talk through/figure out the mechanics and logistics of actually merging these together, but it's probably only a matter of time before we can finally end the duality of ompi/ompi-release.
