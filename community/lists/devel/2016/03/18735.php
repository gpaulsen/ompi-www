<p>Specifically:
<br>
<p>-----
<br>
User and team restrictions
<br>
<p>Sometimes merges to a protected branch are best left to a release manager or a team of people responsible for the stability of that particular branch. Organizations can now specify which members and teams are able to push to a protected branch. Note that organization and repository administrators are always able to push.
<br>
