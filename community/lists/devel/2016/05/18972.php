<p>
FYI -- Github announced changes to their pricing plans today (<a href="https://github.com/blog/2164-introducing-unlimited-private-repositories">https://github.com/blog/2164-introducing-unlimited-private-repositories</a>).  They have a new &quot;per user&quot; pricing model, which makes sense for some organizations.
<br>
<p>It does *not* make sense for us -- we are paying $300/year (i.e., $25/mo) to have a max of 10 private repos, and unlimited users.  We basically have one important private repo (the tests repo).
<br>
<p>We have a *LOT* of users.  Recall that one of the deciding factors in choosing Github over Bitbucket was overall cost (Bitbucket charges per user).
<br>
<p>Luckily, Github is willing to let us stay on the &quot;10 private repos/unlimited users&quot; plan for a fixed $300/year.  See below.
