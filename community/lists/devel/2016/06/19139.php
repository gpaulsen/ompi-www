<p>
Heads up for those who were not on the webex today: our faculty sponsor at Indiana University (IU), Dr. Andrew Lumsdaine, is moving to a different organization.
<br>
<p>IU has been incredibly helpful over the past 12 years (!) of the Open MPI project: they have provided all kinds of hosting and infrastructure to the Open MPI community, completely free of charge.  We are deeply grateful for all the time, effort, and resources that have been freely given to the Open MPI project.  Thank you, IU!
<br>
<p>However, now that Andrew is leaving IU, we will need to move our infrastructure elsewhere -- probably within the next 3 months.
<br>
<p>Discussions are now occurring about the logistics of how to move this infrastructure.  We'll likely get some wiki pages up detailing what needs to move, how, ...etc.  Stay tuned.
<br>
<p>Two main notes:
<br>
<p>1. As we move our infrastructure to new location(s), some user-noticeable behavior may change (e.g., functionality in the web sites and/or mailing lists).  We'll try to give heads up when this happens, but just be aware that new infrastructure may mean different capabilities.
<br>
<p>2. We've been fortunate that all of our costs so far have essentially been $0, but we're now going to start incurring infrastructure hosting costs as a community.  Who will pay these costs?
<br>
<p>Because of #2, today on the webex, we proposed moving to the MPI Forum model of funding: charging a small registration fee for the Open MPI face-to-face developer meetings.  All that money will go into some kind of community account somewhere, and we'll use that money to pay community bills (e.g., github and other hosting/infrastructure fees).
<br>
<p>Unless there is serious objection, we plan to start using this model for the upcoming Open MPI developer meeting (i.e., there will be a registration fee).
