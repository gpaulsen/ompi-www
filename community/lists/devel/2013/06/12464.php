<p>
Request for Change:
<br>
<p>What: Add support for Mellanox Technologies' next-generation non-blocking collectives, code-named &quot;libhcoll&quot;. This comes in the form of a new &quot;hcoll&quot; component to the &quot;coll&quot; framework.
<br>
<p>Where: Trunk and 1.7
<br>
<p>When: July 1
<br>
<p>Why: In support of MPI 3, Mellanox Technologies will make available its next generation collectives library, &quot;libhcoll&quot;,  in MOFED 2.0 releases and higher starting in the late 2013 timeframe. &quot;Libhcoll&quot; adds support for truly asynchronous non-blocking collectives on supported HCAs (Connect X-3 and higher) via Mellanox Technologies' CORE-Direct technology. &quot;Libhcoll&quot; also adds support for hierarchical collectives and features a highly scalable infrastructure battle tested and proven on some of the world's largest HPC systems.
