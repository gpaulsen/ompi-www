</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt;         1. The MPI_T control variables map directly to OMPI's MCA
</span><br>
<span class="quotelev1">&gt;         params, so we automatically expose oodles of cvars through
</span><br>
