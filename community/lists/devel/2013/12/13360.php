<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt;     With MPICH, we had to examine the code for things that were
</span><br>
<span class="quotelev1">&gt;     configurable.  It seems to me that in the case of Open MPI, most
</span><br>
<span class="quotelev1">&gt;     of the work is done and, as you point out, it may just be
