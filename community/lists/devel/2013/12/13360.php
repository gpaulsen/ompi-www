<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt;     Right now we can modify MPI runtime parameters with IBM-MPI or
</span><br>
<span class="quotelev1">&gt;     Open MPI.  These require full restarts, since they are set as
</span><br>
<span class="quotelev1">&gt;     environment variables and are not modifiable after MPI_INIT.  With
</span><br>
<span class="quotelev1">&gt;     your MPIT implementation, we can do the same programmatically but
</span><br>
<span class="quotelev1">&gt;     cannot avoid the restarts or full runs.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt;     We already did what you describe at the end of 1., but with a (1
</span><br>
<span class="quotelev1">&gt;     year old) snapshot of MPICH.  The idea was to identify which
</span><br>
<span class="quotelev1">&gt;     variables could be made modifiable at runtime, and whether there
</span><br>
<span class="quotelev1">&gt;     was any attainable performance as a result of tuning them.  We
</span><br>
<span class="quotelev1">&gt;     only explored point to point and collective communication
</span><br>
<span class="quotelev1">&gt;     parameters, and the results are encouraging.  There was no
</span><br>
<span class="quotelev1">&gt;     technical reason when picking MPICH for the first prototype.
