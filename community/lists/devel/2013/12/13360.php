<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt;     I think you made a good guess on what we would like to do here.
</span><br>
<span class="quotelev1">&gt;      We are working on automatic tuning based on both modeling and
</span><br>
<span class="quotelev1">&gt;     empirical data.  One of our aims is to accelerate the data
</span><br>
<span class="quotelev1">&gt;     collection part (in this case related to MPI settings), by doing
</span><br>
<span class="quotelev1">&gt;     it online without the need of full application runs or restarts.
