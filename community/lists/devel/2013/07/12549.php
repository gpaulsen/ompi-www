<p>
According to <a href="https://svn.open-mpi.org/trac/ompi/wiki/Admistrative%20rules">https://svn.open-mpi.org/trac/ompi/wiki/Admistrative%20rules</a>, it is time for our annual review of Open MPI SVN accounts of these SVN repos: hwloc, mtt, ompi-docs, ompi-tests, ompi-www, ompi.
<br>
<p>*** Organizations must reply by COB Friday, 12 July, 2013 ***
<br>
*** No reply means: delete all of my organization's SVN accounts
<br>
<p>Each organization must reply and specify which of their accounts can stay and which should go.  I cross-referenced the SVN logs from all of our SVN repositories to see who has not committed anything in the past year.  
<br>
<p>*** I strongly recommend deleting accounts who have not committed in the last year.
<br>
*** Other accounts can be deleted, too (e.g., those who have left a given organization).
