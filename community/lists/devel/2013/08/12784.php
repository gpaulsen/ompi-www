<p>
Note that according to <a href="http://www.ohloh.net/p/openmpi">http://www.ohloh.net/p/openmpi</a>, OMPI is over 3/4M lines of code.  I doubt you will be able to get a thorough understanding of *all* of OMPI in a semester or two -- indeed, I doubt that any one Open MPI core developer has a thorough understanding of the whole code base (I know I don't!).
<br>
<p>I don't say these things to discourage you; I actually just want to encourage you to set realistic expectations for what you'll be able to do.
<br>
<p>Are there any particular areas that you'd like to focus on?  MPI -- and the Open MPI implementation -- covers a *lot* of different subsystems and concepts.
