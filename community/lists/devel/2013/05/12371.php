<p>
Hi,
<br>
<p>Briefly, I'm Thomas.  I work at ORNL.  I changed autogen.pl on my
<br>
first commit to OMPI trunk. (Insert rookie joke here.  :-D)
<br>
<p>The changes in r28456 for autogen.pl were pretty basic/minor.  I
<br>
apologize for not sending a follow-up email to devel mailing list
<br>
outlining the changes -- poor netiquette on my part. :-/
<br>
<p>There were four changes included in the patch.  They related
<br>
mainly to the recent changes for MCA frameworks.  I'll give a little
<br>
more description below.
<br>
<p>Ralph, I also included your feedback and a response for #2.  Let me
<br>
know if this makes sense as I think it provides the &quot;right&quot; behavior
<br>
but want to double check.  Thanks.
<br>
<p><p><span class="quotelev1">&gt;  1) Add ifdef guard to project's autogenerated &quot;frameworks.h&quot; header
</span><br>
<span class="quotelev1">&gt;     file, e.g., &quot;opal/inlude/opal/frameworks.h&quot; would have
</span><br>
<span class="quotelev1">&gt;     &quot;OPAL_FRAMEWORKS_H&quot;.
</span><br>
<p>This one simply adds and ifdef to top of auto-generated file, so if code
<br>
includes the &quot;framework.h&quot; file you avoid multiple includes of same file.
<br>
This is generic to the given project so the &quot;opal/&quot; project would generate
<br>
something like:
<br>
<p>&nbsp;&nbsp;&nbsp;$ cat opal/include/opal/frameworks.h
<br>
&nbsp;&nbsp;&nbsp;/*
<br>
&nbsp;&nbsp;&nbsp;&nbsp;* This file is autogenerated by autogen.pl. Do not edit this file by hand.
<br>
&nbsp;&nbsp;&nbsp;&nbsp;*/
<br>
&nbsp;&nbsp;&nbsp;#ifndef OPAL_FRAMEWORKS_H
<br>
&nbsp;&nbsp;&nbsp;#define OPAL_FRAMEWORKS_H
<br>
<p>&nbsp;&nbsp;&nbsp;#include &lt;opal/mca/base/mca_base_framework.h&gt;
<br>
<p>&nbsp;&nbsp;&nbsp;extern mca_base_framework_t opal_backtrace_base_framework;
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;...&lt;snip&gt;...
<br>
<p>&nbsp;&nbsp;&nbsp;#endif /* OPAL_FRAMEWORKS_H */
<br>
<p>This would also be done for &quot;ompi/&quot; and &quot;orte/&quot; project directories.
<br>
<p><p><p><p><span class="quotelev1">&gt;  2) Avoid adding &quot;ignored&quot; frameworks to the autogenerated &quot;frameworks.h&quot;
</span><br>
<span class="quotelev1">&gt;     header file.
</span><br>
<p>This simply applies the same ignored() function that is used elsewhere in
<br>
the autogen.pl script for omitting &quot;ignored&quot; MCA directories from the
<br>
processing.  This just picks up the &quot;.ompi_ignore&quot; (and/or &quot;.ompi_unignore) 
<br>
files.   The intent being that if you ignore a component (subdir) that will
<br>
be removed from the list, but you could also remove an entire framework if
<br>
you put the ignore file in the top-level of the framework.
<br>
<p>The intent being that if for whatever reason you ignore a framework in the
<br>
&quot;${project}/mca/&quot; space, you will not have it automatically show up in the
<br>
project's &quot;frameworks.h&quot; file.
<br>
<p>On Tue, 7 May 2013, Ralph Castain wrote:
<br>
<p><span class="quotelev2">&gt;&gt; We use the frameworks.h file to &quot;discover&quot; the frameworks in
</span><br>
<span class="quotelev2">&gt;&gt; ompi_info.  Even if no components are built for that framework,
</span><br>
<span class="quotelev2">&gt;&gt; there still are MCA params relating to the base of that framework.
</span><br>
<span class="quotelev2">&gt;&gt; Sounds silly, I know - but there may be reasons to access those
</span><br>
<span class="quotelev2">&gt;&gt; params - e.g., to set verbosity to verify that no components are
</span><br>
<span class="quotelev2">&gt;&gt; being selected.
</span><br>
<span class="quotelev2">&gt;&gt;
</span><br>
<span class="quotelev2">&gt;&gt; I think we need those frameworks to be listed...
</span><br>
<p><p>Ok, I didn't realize the 'ompi_info' aspect.  Good to know.
<br>
However, I think honouring the ignore behavior is good in this case
<br>
b/c if you drop an ignore file in a framework, you won't get any
<br>
other autogen touches (i.e., no Makefile's are autogenerated).  So
<br>
it seems that having no Makefiles but including the framework in the
<br>
&quot;framework.h&quot; would break regardless.  Again, this is more of a
<br>
safety guard.
<br>
<p><p><p><p><span class="quotelev1">&gt; 3) Avoid adding non-MCA projects to the autogenerated
</span><br>
<span class="quotelev1">&gt; 'mca_project_list', which maintains existing support for &quot;projects&quot; 
</span><br>
<span class="quotelev1">&gt; with new MCA framework enhancements.  Moves this down to mca_run_global().
</span><br>
<p><p>This was just a bit of shifting code and didn't sound like there was
<br>
any discussion on this point.  This is a &quot;do no harm&quot; factor to
<br>
support pre-existing functionality.  The gist is that if you have a
<br>
&quot;project&quot; in the build directory that doesn't have an MCA directory 
<br>
structure, just avoid adding it to the list of MCA projects.
<br>
<p><p><p><span class="quotelev1">&gt; 4) Add small loop at end to add projects with a &quot;config/&quot; subdir
</span><br>
<span class="quotelev1">&gt;    to the list of includes for 'autoreconf'.
</span><br>
<p><p>This again is a &quot;do no harm&quot; factor to support pre-exising
<br>
functionality.  If you have a &quot;${project}/config/&quot; directory.  This
<br>
appends  the &quot;-I ${project}/config/&quot; to the autoreconf list.
<br>
If you do not have a &quot;${project}/config/&quot; dir, there is no change.
<br>
<p><p>Again, I hope that gives more context/description to the changes
<br>
included in the autogen.pl patch.  In the future, I'll try to do
<br>
a better job of sending a heads up to the devel list.
<br>
<p>Thanks,
<br>
--tjn
