<p>
Hi folks
<br>
<p>When we moved to Github, we decided that we would use their &#226;&#128;&#156;pull requests&#226;&#128;&#157; to replace our RFC process. Our thinking at the time was that everyone would receive these, and so would know that something had been proposed.
<br>
<p>What we hadn&#226;&#128;&#153;t really anticipated was the volume of PRs that would be generated. Quite frankly, it has become hard to sift thru them all to identify those that involve significant change from those involving minor bug fixes.
<br>
<p>Josh and I were kicking this around last week at SC&#226;&#128;&#153;15, and after some consideration, I thought it makes sense to at least propose a couple of modifications that might help people to track what&#226;&#128;&#153;s going on:
<br>
<p>(a) revive the RFC for significant changes. If the PR touches core code, or involves a change that exceeds an isolated bug fix, it would help if people announced it on the devel mailing list with &#226;&#128;&#156;RFC&#226;&#128;&#157; in the subject line, an explanation appropriate in length to the corresponding change, and a pointer to the PR. We should also include a &#226;&#128;&#156;timeout&#226;&#128;&#157; to indicate when this PR is intended to be committed, minus any expressed concerns. This would allow people to become aware of a proposed change that could impact them.
<br>
<p>(b) send a note to the devel mailing list indicating you are about to start working on a significant change to the code base. We generally do this on our weekly telecon, but not everyone can attend those. So rather than surprising folks with a PR out of the blue, it would be good to let the community know of your intentions so people can chime in with suggestions and contact you off-list about possibly contributing to the change. Besides, it might help to avoid having others committing conflicting changes during the effort.
<br>
<p>I figured we could discuss this on the next telecon (Dec 1st), but wanted to throw it out there for comment and advanced consideration.
<br>
<p>Ralph
