Hi folks
<br>
<p>After talking with Mike Dubman/Mellanox, we have clarified that the true
<br>
urgency with respect to 1.10.2 is in getting PRs into that branch. Release
<br>
of the official 1.10.2 tarball can occur a reasonable time later.
<br>
<p>Accordingly, we are going to accept PRs into 1.10.2 at a little more rapid
<br>
pace. So I'll be looking to commit PRs on a daily basis, gated by the
<br>
cleanliness of the prior night's MTT results.
<br>
<p>NOTE: IF you make a change to the MTT test suite, please send a note to
<br>
devel alerting us to the change and pointing to the corresponding PR
<br>
required to pass the test!!
<br>
<p>We are still going to maintain 1.10.2 as a *bug fix* release - so please
<br>
don't schedule new features for that milestone.
<br>
<p>We think that official release of 1.10.2 will likely take place in Jan.
<br>
<p>Thanks
<br>
Ralph
