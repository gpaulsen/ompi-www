<p>It's that time of year again: it's our annual review of those with write access to our Git repos.
<br>
<p>The purpose is simply to trim those who are no longer active, or otherwise no longer need write access to Open MPI repositories.  We don't want people moving to new jobs and still having write access to our code bases; this is our annual reminder to remove such accounts.
<br>
<p>*** I've created a spreadsheet that every organization will need to examine by COB Fri, 4 Sep, 2015 ***
<br>
<p>What you need to do:
<br>
<p>1. Visit this Google spreadsheet
<br>
<p>2. Find your organization.
<br>
<p>3. Examine each person in your organization:
<br>
&nbsp;&nbsp;&nbsp;- I've marked each Git repo that each person currently has WRITE access to
<br>
&nbsp;&nbsp;&nbsp;*** If that person can be dropped from all Open MPI repos, color that person's row ORANGE
<br>
&nbsp;&nbsp;&nbsp;*** If that person still needs WRITE git repo access, place an X in the corresponding &quot;Need commit?&quot; column
<br>
&nbsp;&nbsp;&nbsp;*** Once you are done checking each person, color that person's row GREEN so that we know this row is done
<br>
<p>### Please only mark off members who actually need write access in the foreseeable future.
<br>
<p>If someone in your org *might* need access at some point in the future, we can add them in the future -- it's a quick/easy process to do so.  Meaning: please don't mark someone as needing access *now* if they only *might* need access in the future.
