<p>
The major agenda item for next Tuesday's call will be to review the results of this week's face-to-face developer meeting.
<br>
<p>We've only had 1 day so far, but it's fairly obvious that we need to share the results, topics, and decisions that have been made at the meeting with everyone who was not here.  
<br>
<p>We're putting bullet points on the wiki for the major decisions, but it's no substitute for a) being here, or b) being able to talk through some of the details to give insight into how some of the decisions were made, and c) getting input from those who were not here.  Here's the wiki (scroll down to see the &quot;Resolved&quot; section):
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://github.com/open-mpi/ompi/wiki/Meeting-2015-01">https://github.com/open-mpi/ompi/wiki/Meeting-2015-01</a>
<br>
<p>Big item from yesterday, for example, is a plan to change how OMPI handles procs in each MPI process (for scalability reasons), and its implications on PML/BTL interactions (and potentially for PML/MTL interactions, but probably need some feedback from MTL maintainers before making changes to cm).
