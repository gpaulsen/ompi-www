Hi folks
<br>
<p>Given how close we are to the holiday break that many companies and people take at the end of the year, it seems unlikely that we will get an official 1.10.2 release out before the end of the month. So the proposed plan is to generate a release candidate once the final couple of PR&#226;&#128;&#153;s are merged, deal with any immediate issues that surface from it, and let the candidates &#226;&#128;&#156;cook&#226;&#128;&#157; until after the holiday.
<br>
<p>Based on that proposal, release of v1.10.2 would probably occur in Jan 2016. This means that 1.10.3 would come out something like April so we provide some spacing.
<br>
<p>Comments are welcome - this is a proposal, not a cast-in-concrete plan, at this time.
