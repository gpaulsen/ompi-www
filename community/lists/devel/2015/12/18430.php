<p>
Just in time for the holidays, the Open MPI Team, representing a consortium of research, academic, and industry partners, is pleased to announce the first release candidate of the upcoming v2.0.0 series: v2.0.0rc1.
<br>
<p>The v2.x series is a new series which includes many new features and under-the-hood improvements.  Many of these features and improvements were discussed in the Open MPI SC'15 State of the Union BOF; the slides are available here:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://blogs.cisco.com/performance/slides-from-open-mpi-sc15-sotu-bof">http://blogs.cisco.com/performance/slides-from-open-mpi-sc15-sotu-bof</a>
<br>
<p>Additionally, as the change in the first digit indicates, the v2.x series is not (binary) backwards compatible with the v1.10 series.  Specifically: MPI and OpenSHMEM applications will need to be recompiled against v2.0.0, and will need to use the v2.x &quot;mpirun&quot; and support libraries.  Slides 9-12 in the BOF slides (linked above) explain Open MPI's (relatively new) version numbering scheme.
<br>
<p>We do not expect to release v2.0.0 until January at the earliest; we wanted to make some early release candidates available to the Open MPI user community to get some real-world testing, and to get feedback before the final release becomes available.
<br>
<p>The v2.0.0 release candidate tarballs are available at the same location where the final release tarballs will be available:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.open-mpi.org/software/ompi/v2.x/">http://www.open-mpi.org/software/ompi/v2.x/</a>
<br>
<p>Happy holidays!
