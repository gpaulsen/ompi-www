<p>
I forgot to mention that you must change your Github flag to publicly state that you are part of the Open MPI organization to be able to use the ompi-release bot.  Go to this page:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://github.com/orgs/open-mpi/people">https://github.com/orgs/open-mpi/people</a>
<br>
<p>Find yourself, and change your membership from &quot;Private&quot; (the default) to &quot;Public&quot;.
<br>
<p>Then you can issue bot:* commands on ompi-release PRs.
<br>
<p>(I also added this info to <a href="https://github.com/open-mpi/ompi/wiki/OmpiReleaseBotCommands">https://github.com/open-mpi/ompi/wiki/OmpiReleaseBotCommands</a>)
