<p>
OMPI devs --
<br>
<p>Per lots of previous discussions, you all know that you can't assign labels, milestones, or users to issues/pull requests on the ompi-release repo.
<br>
<p>Gilles has written a Github bot that will allow you to do these things by inserting special tokens in the text of issues/pull requests/comments.  Here's an example:
<br>
<p>&nbsp;&nbsp;&nbsp;This PR fixes problem XYZ.
<br>
<p>&nbsp;&nbsp;&nbsp;label:bug
<br>
&nbsp;&nbsp;&nbsp;label:enhancement
<br>
&nbsp;&nbsp;&nbsp;milestone:v1.8.5
<br>
&nbsp;&nbsp;&nbsp;assign:@jsquyres
<br>
<p>*** PLEASE GO TRY IT on the sandbox ompi-release-bot repo.  
<br>
<p>Here's a fuller explanation of what OMPIBot does, and links to where you can try it out:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://github.com/open-mpi/ompi-release-bot/wiki">https://github.com/open-mpi/ompi-release-bot/wiki</a>
<br>
<p>Once we get enough people to try it/fix any bugs/etc., we'll deploy it on the ompi-release repo.
