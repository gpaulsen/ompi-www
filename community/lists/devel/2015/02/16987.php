Jeff,
<br>
<p>It might be helpful to define &quot;PR&quot; before first use on this page and others.
<br>
<p>While PR = &quot;Pull Request&quot; is common usage among folks familiar with Git, it
<br>
stands for &quot;Problem Report&quot; in other s/w development contexts (e.g. the
<br>
gcc-bugs mailing list and associated bugzilla).  So, the phrase &quot;file a PR&quot;
<br>
in the new wiki page could be misunderstood (being aware of the ambiguity I
<br>
had to read it twice to see which you meant).
<br>
<p>-Paul
