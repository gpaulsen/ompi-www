Hi folks
<br>
<p>I mentioned this on the OMPI devel telecon recently, but wanted to pass it along to a wider audience. There will be a BoF at the July ISC&#226;&#128;&#153;15 to discuss possible formation of a new open source community focused on providing a more streamlined, automated way of validating HPC software configurations. Basically, the notion is that the community would develop/provide fools for automating the validation process, a procedure for submitting packages for validation, and a database that people can use to check for version compatibility between packages.
<br>
<p>The BoF description and time is given here:
<br>
<p><a href="http://www.isc-events.com/isc15_ap/sessiondetails.htm?t=session&amp;o=207&amp;a=select">http://www.isc-events.com/isc15_ap/sessiondetails.htm?t=session&amp;o=207&amp;a=select</a> &lt;<a href="http://www.isc-events.com/isc15_ap/sessiondetails.htm?t=session&amp;o=207&amp;a=select">http://www.isc-events.com/isc15_ap/sessiondetails.htm?t=session&amp;o=207&amp;a=select</a>&gt;
<br>
<p>More discussion on this proposal will take place at SC&#226;&#128;&#153;15 as well, for interested parties who cannot attend at ISC.
