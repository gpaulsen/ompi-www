<br>
<p>Another option is the MPI Testing Tool 
<br>
<a href="http://www.open-mpi.org/projects/mtt/">http://www.open-mpi.org/projects/mtt/</a> but it might be more trouble than 
<br>
it's worth.
<br>
<p>(INCIDENTALLY, THERE ARE TRAC TROUBLES WITH THE THREE LINKS AT THE 
<br>
BOTTOM OF THAT PAGE!  COULD SOMEONE TAKE A LOOK?)
<br>
<p>If you do decide to explore MTT, 
<br>
<a href="http://www.open-mpi.org/projects/mtt/svn.php">http://www.open-mpi.org/projects/mtt/svn.php</a> tells you how to do a 
<br>
Subversion checkout.  It's a test harness.  For the tests themselves, 
<br>
look in mtt/trunk/samples/*-template.ini for examples of what tests to 
<br>
run.  Whether you want to pursue this route depends on whether you're 
<br>
serious about doing lots of testing.
