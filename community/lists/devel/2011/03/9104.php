<p>
To whom it may concern 
<br>
<p>&nbsp;&nbsp;
<br>
<p>Can open source development be sustainable? And if so, how? 
<br>
<p>This question raises interest in practitioners, affects ideologically
<br>
motivated developers, as well as researchers from several disciplines. Open
<br>
source management challenges traditional business models and views of
<br>
competition and collaboration. Therefore each stakeholder should take an
<br>
interest in the way open source management works. Hence we investigate the
<br>
antecedents of business model choice in the OSS industry, aiming to
<br>
identify, which factors are or were influential for companies&#146; initial
<br>
business decision regarding Open Source. We identified your company as an
<br>
active member of open source collaboration and development and therefore
<br>
would like to ask you to participate in this investigation.
<br>
<p>&nbsp;
<br>
<p>I am a research assistant at the Berlin Institute of Technology, Germany,
<br>
and my research project discusses the implications of open source
<br>
management. The main objective is analyzing key factors that drive the
<br>
development of open source business models and how companies and communities
<br>
can mutually benefit from collaborative development. The project is
<br>
supervised by Prof. Dr. zu Knyphausen-Aufse&#223;.
<br>
<p>&nbsp;
<br>
<p>I want to kindly ask you take part in our survey on open source business
<br>
models. If you were involved in decisions on the business strategy of your
<br>
company, please complete our survey. It will not take more than 10 &#150; 15
<br>
minutes to complete. Otherwise, please forward this enquiry to your
<br>
colleague(s) who are or have been in charge for your business model
<br>
decision.  
<br>
<p>&nbsp;
<br>
<p>The survey can be accessed online at: 
<br>
<p>&nbsp;
<br>
&lt;<a href="http://strategie.service.tu-berlin.de/limesurvey/index.php?sid=96753&amp;lang=e">http://strategie.service.tu-berlin.de/limesurvey/index.php?sid=96753&amp;lang=e</a>
<br>
n&gt;
<br>
<a href="http://strategie.service.tu-berlin.de/limesurvey/index.php?sid=96753&amp;lang=en">http://strategie.service.tu-berlin.de/limesurvey/index.php?sid=96753&amp;lang=en</a>
<br>
<p>&nbsp;
<br>
<p>The anonymity of your responses is guaranteed. All data will be analyzed and
<br>
published only in an aggregated form.
<br>
<p>As compensation for your efforts, I would like to offer you a more detailed
<br>
report on our findings. If you relinquish your anonymity, I can also provide
<br>
you with a benchmark report that compares your individual answers to the
<br>
overall study results.
<br>
<p>&nbsp;
<br>
<p>Further information about our research can be found at
<br>
&lt;<a href="http://www.oss-research.info/">http://www.oss-research.info/</a>&gt; <a href="http://www.oss-research.info">http://www.oss-research.info</a>
