<p>Speaking as the owner of this mercurial repo, I can confidently state that it's not yet ready for prime-time.  :-)
<br>
<p>Sidenote: We OMPI developers frequently make temporary branches -- sometimes in the form of mercurial repositories on bitbucket.org so that we can collaborate with others -- to develop new pieces of functionality.  Usually, these temporary branches are eventually folded back into the Open MPI mainline SVN trunk when the work is done.  Indeed, if you look under my ID at bitbucket, you'll see a truckload of mercurial repos in various states of completion.  None of them are ready for the SVN mainline yet.  :-)
<br>
<p>This particular repo is exactly what you said: Josh Hursey and I are working with the MTCP developers to add MTCP support to Open MPI.  The work has been ongoing for a while; it's not ready yet.
<br>
<p>Due to a subversion+mercurial snafu a few days ago, we actually just re-started this repo and ditched all the prior history (hence the name &quot;dmtcp2&quot;; I deleted the original &quot;dmtcp&quot; repo).  
<br>
<p>My point: if you want to follow the MTCP development, you're welcome to (that's why it's out in the open, after all!), but with the very strong disclaimer that it may break at any time -- it's a developer's unstable branch, after all.  It is unlikely that we will be able to provide much support for this stuff until it hits the SVN mainline.
