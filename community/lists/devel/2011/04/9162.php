<p>
Reminder:
<br>
&nbsp;&nbsp;If you are interested in attending the May 3-5 Open MPI Developers Meeting at ORNL let let Rich (rlgraham -at- ornl -dot- gov) and I know as soon as possible so we can start the paperwork. This is of particular importance for non-US citizens since the paperwork takes considerably more time.
<br>
<p><p>The meeting will be three full days (May 3-5) on the ORNL campus. I intend to setup a teleconf for some/most of the sessions for those that cannot attend in person. Once we have the agenda topics on the table we can start negotiating time allotments.
<br>
<p>Below are the agenda items that I have gathered so far (in no particular order):
<br>
&nbsp;- MPI 2.2 implementation tickets
<br>
&nbsp;- MPI 3.0 implementation planning
<br>
&nbsp;- ORNL: Hierarchical Collectives discussion
<br>
&nbsp;- Runtime integration discussion
<br>
&nbsp;- New Process Affinity functionality
<br>
&nbsp;- Update on ORTE development
<br>
&nbsp;- Fault tolerance feature development and integration
<br>
&nbsp;&nbsp;&nbsp;(C/R, logging, replication, FT-MPI, MPI 3.0, message reliability, ...)
<br>
<p>Other topics that I thought of which folks might want to discuss - Is there anyone that wants to include these and lead their discussions?:
<br>
&nbsp;- Threading design
<br>
&nbsp;- Performance tuning (Point-to-point and/or Collective)
<br>
&nbsp;- Testing infrastructure (MTT)
<br>
<p><p>Keep sending agenda items to the list (or me directly if you would rather). I hope to have the agenda sketched out by the teleconf on 4/12 so we can fine tune it on the call.
<br>
<p>Thanks,
<br>
