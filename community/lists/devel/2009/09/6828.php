<p>
Open MPI now has a new sub-project: Portable Hardware Locality  
<br>
(&quot;hwloc&quot;).
<br>
<p>hwloc represents the merger of libtopology and Portable Linux  
<br>
Processor Affinity (PLPA).  I described the project in a blog post here:
<br>
<p><a href="http://blogs.cisco.com/ciscotalk/performance/comments/announcing_hwloc_portable_hardware_locality_open_source_software/">http://blogs.cisco.com/ciscotalk/performance/comments/announcing_hwloc_portable_hardware_locality_open_source_software/</a>
<br>
<p>The main web site for hwloc is:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.open-mpi.org/projects/hwloc/">http://www.open-mpi.org/projects/hwloc/</a>
