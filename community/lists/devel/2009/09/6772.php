<p>
Hi,
<br>
<p>Ralph Castain wrote:
<br>
<span class="quotelev1">&gt; Let me point out the obvious since this has plagued us at LANL with 
</span><br>
<span class="quotelev1">&gt; regard to this concept. If a user wants to do something different, all 
</span><br>
<span class="quotelev1">&gt; they have to do is download and build their own copy of OMPI.
</span><br>
We are well aware of that. It is relatively easy for a user to 
<br>
circumvent the limitation...
<br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; My point here: this concept can help, but it should in no way be 
</span><br>
<span class="quotelev1">&gt; viewed as a solution to the problem you are trying to solve. It is at 
</span><br>
<span class="quotelev1">&gt; best a minor obstacle as we made it very simple for a user to 
</span><br>
<span class="quotelev1">&gt; circumvent such measures.
</span><br>
... however the problem we are trying to solve is that some users may 
<br>
inadvertantly modify parameters that they should not, for example when 
<br>
playing around with MCA parameters in order to &quot;tune&quot; their application.
<br>
System wide parameters make it possible to give a message to the user 
<br>
saying he is not allowed to do what he tried to do, and this is a nice 
<br>
to have feature.
<br>
<p>We are defending the system against users who are not ill-intentioned, 
<br>
but simply do not always know what they are doing when they change 
<br>
parameters. System wide parameters do help in this area.
<br>
