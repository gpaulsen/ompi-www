<p>
I think I have to agree with Terry.
<br>
<p>I love to inspire and see new, original, and unintended uses for Open  
<br>
MPI.  But our primary focus must remain to create, maintain, and  
<br>
continue to deliver a high performance MPI implementation.
<br>
<p>We have a long history of adding &quot;small&quot; things to Open MPI that are  
<br>
useful to 3rd parties because it helps them, helps further Open MPI  
<br>
adoption/usefulness, and wasn't difficult for us to do (&quot;small&quot; can  
<br>
have varying definitions).  I'm in favor of such things, as long as we  
<br>
maintain a policy of &quot;in cases of conflict, OMPI/high performance MPI  
<br>
wins&quot;.
<br>
<p><p>On Mar 12, 2009, at 9:01 AM, Terry Dontje wrote:
<br>
<p><span class="quotelev1">&gt; Sun's participation in this community was to obtain a stable and
</span><br>
<span class="quotelev1">&gt; performant MPI implementation that had some research work done on the
</span><br>
<span class="quotelev1">&gt; side to improve those goals and the introduction of new features.   We
</span><br>
<span class="quotelev1">&gt; don't have problems with others using and improving on the OMPI code
</span><br>
<span class="quotelev1">&gt; base but we need to make sure such usage doesn't detract from our
</span><br>
<span class="quotelev1">&gt; primary goal of performant MPI implementation.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; However, changes to the OMPI code base to allow it to morph or even
</span><br>
<span class="quotelev1">&gt; support a distributed OS does cause for some concern.  That is are we
</span><br>
<span class="quotelev1">&gt; opening the door to having more interfaces to support?  If so is this
</span><br>
<span class="quotelev1">&gt; wise in the fact that it seems to me we have a hard enough time trying
</span><br>
<span class="quotelev1">&gt; to focus on the MPI items?  Not to mention this definitely starts
</span><br>
<span class="quotelev1">&gt; detracting from the original goals.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; --td
</span><br>
