<p>
OpenFabrics is having its annual Sonoma meeting next week (see <a href="http://www.openfabrics.org/Sonoma.pdf">http://www.openfabrics.org/Sonoma.pdf</a> 
<br>
&nbsp;&nbsp;for more details).
<br>
<p>The event will be &quot;simulcast&quot; on Cisco WebEx *for free* for those who  
<br>
cannot attend in person.  WebEx attendees will see the slides and hear  
<br>
the in-room presenters.  I'm told that they're going to setup an audio  
<br>
link in the conference room such that WebEx attendees will be able to  
<br>
be heard over the in-room audio system, too.
<br>
<p>You may be interested to browse through the current agenda to see if  
<br>
there are any sessions that you might want to attend via WebEx(<a href="http://www.openfabrics.org/Sonoma2009preliminaryagenda.pdf">http://www.openfabrics.org/Sonoma2009preliminaryagenda.pdf</a> 
<br>
&nbsp;&nbsp;-- go to <a href="http://www.openfabrics.org/">http://www.openfabrics.org/</a> if that preliminary agenda link  
<br>
expires).  In particular, you may care about this session:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;MPI Panel: Tuesday, March 24, 2009 (I'll be presenting along with  
<br>
several other MPI implementors)
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* 1:30pm San Francisco (US Pacific time)
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* 4:30pm New York (US Eastern time)
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* 9:30pm Paris
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* 2:00am Bangalore
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;* REMEMBER THAT THE US HAS ALREADY CHANGED TIME THIS SPRING!
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;See this URL for time translation to your local time zone:
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.timeanddate.com/worldclock/meetingtime.html?month=3&amp;day=23&amp;year=2009&amp;p1=224&amp;p2=179&amp;p3=195&amp;p4=438&amp;iv=0">http://www.timeanddate.com/worldclock/meetingtime.html?month=3&amp;day=23&amp;year=2009&amp;p1=224&amp;p2=179&amp;p3=195&amp;p4=438&amp;iv=0</a>
