<p>
Hello all
<br>
<p>As Jeff indicated in an earlier note, Open MPI is revising its release  
<br>
procedure to embrace a feature and a stable series structure. Jeff and  
<br>
I have been designated co-release manager of the next feature/stable  
<br>
pair (to follow the current 1.3 feature, and upcoming 1.4 stable).
<br>
<p>Our current thinking is to branch the 1.5.0 feature series from the  
<br>
trunk on Tues, March 24, in the afternoon (US time). All features in  
<br>
the trunk at that time will be included in the 1.5.0 initial release.  
<br>
As the trunk is passing tests pretty reliably right now, we don't  
<br>
anticipate a great deal of time will be required to stabilize this  
<br>
release, so we are tentatively planning for 1.5.0 public release in May.
<br>
<p>Jeff and I will be constructing a list of the new features that are  
<br>
likely to be included in that release so we can publicly announce  
<br>
them. We will circulate a draft for everyone to comment on as we will  
<br>
undoubtedly miss things. Note that we (i.e., all of us) may subtract  
<br>
some of these as we go should it prove that a particular feature (a)  
<br>
isn't ready, and (b) will take some time to become ready. In those  
<br>
cases, we will push that feature off to a later 1.5 sub-release.
<br>
<p>We (i.e., Jeff and I) would also like to start assembling a list of  
<br>
proposed/upcoming features that might be included in future 1.5  
<br>
releases. In that regard, we would appreciate it if you sent us (off- 
<br>
list) anything you would like to see included, and when you think it  
<br>
might be ready. We will assemble the input, and then publish a  
<br>
proposed schedule for bringing new features to the 1.5 branch. This  
<br>
will just be a starting point for a community discussion.
<br>
<p>Thanks
<br>
Ralph
