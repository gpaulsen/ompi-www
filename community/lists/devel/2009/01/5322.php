<p>
Someone just asked me, so I pulled together a few fun web site stats:
<br>
<p>- We released Open MPI v1.3 on Jan 19, 2009
<br>
- I enabled Google Analytics tracking of v1.3 downloads on Jan 20
<br>
- From Jan 1, 2008 to Jan 20, 2009, there were 9,741 downloads of Open  
<br>
MPI v1.2 (including all sub-releases).  This is about ~812/month, or  
<br>
~187/week, or ~25/day
<br>
- After Jan 20, 2009, the number of v1.2 downloads drops off sharply
<br>
&nbsp;&nbsp;&nbsp;(we changed the default download links on the web site to point to  
<br>
v1.3)
<br>
- From Jan 20-Jan 26, there have been 235 downloads of Open MPI v1.3,  
<br>
or ~39/day
<br>
<p>These numbers *should* include downloads from our mirrors, but I've  
<br>
never verified that.
<br>
<p>These numbers do not include nightly tarball downloads; they only  
<br>
include official release and release candidate tarballs and srpms.
<br>
<p>Browsers
<br>
- 64% of our web site visitors use Firefox
<br>
- 17% use IE
<br>
- 7% use Safari
<br>
<p>Traffic sources
<br>
- 68% of our traffic comes from Google searches
<br>
- 13% of our traffic comes from people typing in &quot;www.open-mpi.org&quot;  
<br>
directly into their browser
<br>
- 3% comes from www.lam-mpi.org
<br>
- 2% comes from wikipedia
<br>
<p>Keywords
<br>
- 22% search for &quot;openmpi&quot; (sigh)
<br>
- 7% search for &quot;open mpi&quot; (I still have more work to do!)
<br>
<p>Visitors
<br>
- 32% of visitors are from the USA
<br>
- 7% are from Germany
<br>
- 6% are from France
<br>
- 5% are from Japan
<br>
