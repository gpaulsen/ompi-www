<p>
Per the call today, it seems pretty obvious that we need to get moving  
<br>
on the planning process for Open MPI v1.4.  The next step is electing  
<br>
Release Managers (RMs) and Gatekeepers (GKs), which needs to be done  
<br>
by the admin group according to <a href="https://svn.open-mpi.org/trac/ompi/wiki/Admistrative%20rules">https://svn.open-mpi.org/trac/ompi/wiki/Admistrative%20rules</a> 
<br>
.
<br>
<p>*** So that we can move forward with v1.4 planning, I'd like to  
<br>
propose that we have this election next week (i.e., email voting Feb  
<br>
2-5 2009). ***
<br>
<p>I nominate myself and Ralph Castain for Open MPI v1.4 Release  
<br>
Managers.  Although the OMPI admin rules don't specify, since v1.2 we  
<br>
have observed having one RM from research/academia and one RM from  
<br>
industry.  Ralph and I satisfy this informal criteria.
<br>
<p>I don't know who to nominate for Gatekeeper -- is anyone interested?   
<br>
This isn't an issue for a little while yet, but we do need a name (or  
<br>
better yet, a primary and a secondary) to nominate...  I think we've  
<br>
beat up Tim enough (1.2 and 1.3!), but perhaps we could convince him  
<br>
to be the secondary GK...?
