<p><p>To clarify...
<br>
<p>1.3 *is* stable, meaning &quot;ok for production use.&quot;  We test all 1.3  
<br>
releases before they go out, it undergoes regression testing, etc.
<br>
<p>We have two different version series:
<br>
<p>1. Odd minor numbers (e.g., 1.3.x): Feature release series.  They're  
<br>
stable and usable, but features may come and go during successive  
<br>
releases.  To be clear: feature release series does not mean &quot;beta&quot; or  
<br>
&quot;we haven't tested this much&quot;.
<br>
<p>2. Even minor number (e.g., 1.4.x) : Super stable series.  Only bug  
<br>
fixes will be applied; no feature additions or subtractions will occur.
<br>
<p>Both series have the same level of testing before they are released.   
<br>
The difference is mainly a classification indicating whether new  
<br>
features can be added / subtracted or not.
