<p>George Bosilca wrote:
<br>
<span class="quotelev1">&gt; The Open MPI Team, representing a consortium of bailed-out banks, car
</span><br>
<span class="quotelev1">&gt; manufacturers, and insurance companies, is pleased to announce the
</span><br>
<span class="quotelev1">&gt; release of the &quot;unbreakable&quot; / bug-free version Open MPI 2009,
</span><br>
<span class="quotelev1">&gt; (expected to be available by mid-2011).  This release is essentially a
</span><br>
<span class="quotelev1">&gt; complete rewrite of Open MPI based on new technologies such as C#,
</span><br>
<span class="quotelev1">&gt; Java, and object-oriented Cobol (so say we all!).  Buffer overflows
</span><br>
<span class="quotelev1">&gt; and memory leaks are now things of the past.  We strongly recommend
</span><br>
<span class="quotelev1">&gt; that all users upgrade to Windows 7 to fully take advantage of the new
</span><br>
<span class="quotelev1">&gt; powers embedded in Open MPI.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; This version can be downloaded from the The Onion web site or from
</span><br>
<span class="quotelev1">&gt; many BitTorrent networks (seeding now; the Open MPI ISO is
</span><br>
<span class="quotelev1">&gt; approximately 3.97GB -- please wait for the full upload).
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Here is an abbreviated list of changes in Open MPI 2009 as compared to
</span><br>
<span class="quotelev1">&gt; the previous version:
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; - Dropped support for MPI 2 in favor of the newly enhanced MPI 11.7
</span><br>
<span class="quotelev1">&gt;  standard.  MPI_COOK_DINNER support is only available with additional
</span><br>
<span class="quotelev1">&gt;  equipment (some assembly may be required).  An experimental PVM-like
</span><br>
<span class="quotelev1">&gt;  API has been introduced to deal with the current limitations of the
</span><br>
<span class="quotelev1">&gt;  MPI 11.7 API.
</span><br>
<span class="quotelev1">&gt; - Added a Twitter network transport capable of achieving peta-scale
</span><br>
<span class="quotelev1">&gt;  per second bandwidth (but only on useless data).
</span><br>
<span class="quotelev1">&gt; - Dropped support for the barely-used x86 and x86_64 architectures in
</span><br>
<span class="quotelev1">&gt;  favor of the most recent ARM6 architecture.  As a direct result,
</span><br>
<span class="quotelev1">&gt;  several Top500 sites are planning to convert from their now obsolete
</span><br>
<span class="quotelev1">&gt;  peta-scale machines to high-reliability iPhone clusters using the
</span><br>
<span class="quotelev1">&gt;  low-latency AT&amp;T 3G network.
</span><br>
<span class="quotelev1">&gt; - The iPhone iMPI app (powered by iOpen MPI) is now downloadable from
</span><br>
<span class="quotelev1">&gt;  the iTunes Store.  Blackberry support will be included in a future
</span><br>
<span class="quotelev1">&gt;  release.
</span><br>
<span class="quotelev1">&gt; - Fix all compiler errors related to the PGI 8.0 compiler by
</span><br>
<span class="quotelev1">&gt;  completely dropping support.
</span><br>
<span class="quotelev1">&gt; - Add some &quot;green&quot; features for energy savings.  The new &quot;--bike&quot;
</span><br>
<span class="quotelev1">&gt;  mpirun option will only run your parallel jobs only during the
</span><br>
<span class="quotelev1">&gt;  operation hours of the official Open MPI biking team.  The
</span><br>
<span class="quotelev1">&gt;  &quot;--preload-result&quot; option will directly embed the final result in
</span><br>
<span class="quotelev1">&gt;  the parallel execution, leading to more scalable and reliable runs
</span><br>
<span class="quotelev1">&gt;  and decreasing the execution time of any parallel application under
</span><br>
<span class="quotelev1">&gt;  the real-time limit of 1 second.  Open MPI is therefore EnergyStar
</span><br>
<span class="quotelev1">&gt;  compliant when used with these options.
</span><br>
<span class="quotelev1">&gt; - In addition to moving Open MPI's lowest point-to-point transports to
</span><br>
<span class="quotelev1">&gt;  be an external project, limited support will be offered for
</span><br>
<span class="quotelev1">&gt;  industry-standard platforms.  Our focus will now be to develop
</span><br>
<span class="quotelev1">&gt;  highly scalable transports based on widely distributed technologies
</span><br>
<span class="quotelev1">&gt;  such as SMTP, High Performance Gopher (v3.8 and later), OLE COMM,
</span><br>
<span class="quotelev1">&gt;  RSS/Atom, DNS, and Bonjour.
</span><br>
<span class="quotelev1">&gt; - Opportunistic integration with Conflicker in order to utilize free
</span><br>
<span class="quotelev1">&gt;  resources distributed world-wide.
</span><br>
<span class="quotelev1">&gt; - Support for all Fortran versions prior to Fortran 2020 has been
</span><br>
<span class="quotelev1">&gt;  dropped.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Make today an Open MPI day!
</span><br>
