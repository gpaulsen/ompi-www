<p>
Hello Ralph,
<br>
<p>* Ralph Castain wrote on Sat, Apr 25, 2009 at 05:09:01PM CEST:
<br>
<span class="quotelev1">&gt; Just to be clear, Ralf - I'm not advocating that we change build  
</span><br>
<span class="quotelev1">&gt; systems. I agree it has been a good relationship, and your participation 
</span><br>
<span class="quotelev1">&gt; has been welcome and extremely helpful.
</span><br>
<p>Understood; and thanks!
<br>
<p><span class="quotelev1">&gt; My point was only that the GPL continues to evolve and seems to be  
</span><br>
<span class="quotelev1">&gt; growing more aggressive in its &quot;viral&quot; clauses, which makes it harder to 
</span><br>
<span class="quotelev1">&gt; work with those packages without getting &quot;assimilated&quot;, as the Borg  
</span><br>
<span class="quotelev1">&gt; would say.
</span><br>
<p>(The following is of course my sole opinion only.)
<br>
<p>Well, of course I cannot tell you how to view or judge the evolution of
<br>
the GPL either; and I understand that the anti-tivoization clause in
<br>
GPLv3 is one of the developments you seem to be hinting at.  I don't
<br>
claim that to be different from how you see it.  Of course, the FSF
<br>
would formulate that differently: they saw a &quot;subversion&quot; of the GPLv2
<br>
in intent if not in the letter of the license.  So they closed that
<br>
loophole.
<br>
<p>However, with respect to the exception clauses that have been written
<br>
for GCC, and are being written for Autoconf, at least the *intent* has
<br>
not been to become any more &quot;viral&quot; than before at all.  For example,
<br>
the GCC Exception, as far as I understand it, has opened up the way to
<br>
write plugins for GCC; something, which the FSF did not want to allow
<br>
to happen at all with the GPLv2 Exception.
<br>
<p>With the current Autoconf draft Exception, there are two goals: one, to
<br>
merely reformulate the current GPLv2 Exception in terms of GPLv3 Section
<br>
&quot;7. Additional Terms&quot;, which has formalized the whole exception business
<br>
a bit.  Second, to address in some way the apparent loophole that one
<br>
could take the whole of Autoconf and clone it by the construction that
<br>
I have mentioned before.  Note that this &quot;loophole&quot; is something the
<br>
lawyers considered possible from reading the text of the old GPLv2
<br>
Exception, but it would pretty clearly already have been against the
<br>
intent of that old Exception (i.e., the loophole would be considered
<br>
a hack by any means).
<br>
<p>To be honest, I don't see the tendency to get &quot;more viral&quot;, rather, the
<br>
desire to clarify things where intention and letter of licenses differ.
<br>
I must admit though, that the newer license texts are longer, more
<br>
complicated, and may be intimidating on that basis along, which should
<br>
be taken into account, too.
<br>
<p>Cheers,
<br>
Ralf
