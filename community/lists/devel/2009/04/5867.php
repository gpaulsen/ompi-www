</span><br>
<span class="quotelev1">&gt; I think I'll need to ask Cisco's lawyers about this.  Can others ask
</span><br>
<span class="quotelev1">&gt; their corporate overlords as well?
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Begin forwarded message:
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev2">&gt; &gt; From: &quot;Ralf Wildenhues&quot; &lt;Ralf.Wildenhues_at_[hidden]&gt;
</span><br>
<span class="quotelev2">&gt; &gt; Date: April 21, 2009 2:46:37 PM EDT
</span><br>
<span class="quotelev2">&gt; &gt; To: &lt;autoconf_at_[hidden]&gt;, &lt;automake_at_[hidden]&gt;, &lt;libtool_at_[hidden]&gt;, &lt;autotools-announce_at_[hidden]
</span><br>
<span class="quotelev3">&gt; &gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; Subject: RFC: proposed GPLv3 license exception draft
</span><br>
<span class="quotelev2">&gt; &gt; Reply-To: &lt;autoconf_at_[hidden]&gt;
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; [ cross-posted to several groups; please followup on the autoconf
</span><br>
<span class="quotelev2">&gt; &gt; list ]
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; In order to complete the GNU Project's migration to GPLv3, every GNU
</span><br>
<span class="quotelev2">&gt; &gt; program that has exceptions to its license needs to have those
</span><br>
<span class="quotelev2">&gt; &gt; exceptions updated for GPLv3.  We've prepared draft text for an
</span><br>
<span class="quotelev2">&gt; &gt; updated
</span><br>
<span class="quotelev2">&gt; &gt; version of the Autoconf exception, and we're interested in hearing
</span><br>
<span class="quotelev2">&gt; &gt; your
</span><br>
<span class="quotelev2">&gt; &gt; feedback on it.
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; The primary purpose of this update is to have the exception build on
</span><br>
<span class="quotelev2">&gt; &gt; top
</span><br>
<span class="quotelev2">&gt; &gt; of the framework for Additional Permissions that GPLv3 provides in
</span><br>
<span class="quotelev2">&gt; &gt; section 7.  We are not trying to change the exception's underlying
</span><br>
<span class="quotelev2">&gt; &gt; policy: we still want to allow people to distribute the normal
</span><br>
<span class="quotelev2">&gt; &gt; output of
</span><br>
<span class="quotelev2">&gt; &gt; Autoconf under any license they want.  However, the new draft does  
</span><br>
<span class="quotelev1">&gt; try
</span><br>
<span class="quotelev2">&gt; &gt; to address new issues that have come up since the current GPLv2- 
</span><br>
<span class="quotelev1">&gt; based
</span><br>
<span class="quotelev2">&gt; &gt; exception was written.
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; The new exception grants people permission to distribute Eligible
</span><br>
<span class="quotelev2">&gt; &gt; Output
</span><br>
<span class="quotelev2">&gt; &gt; Material under terms of their choice.  In short, Eligible Output
</span><br>
<span class="quotelev2">&gt; &gt; Material is a more formally defined way of talking about the
</span><br>
<span class="quotelev2">&gt; &gt; &quot;configure
</span><br>
<span class="quotelev2">&gt; &gt; scripts&quot; that are the subject of the old GPLv2-based exception.  One
</span><br>
<span class="quotelev2">&gt; &gt; change of note is that it is defined to explicitly exclude tracing
</span><br>
<span class="quotelev2">&gt; &gt; output, to prevent people from using that mechanism to be able to
</span><br>
<span class="quotelev2">&gt; &gt; distribute more source under the terms of the exception than they  
</span><br>
<span class="quotelev1">&gt; are
</span><br>
<span class="quotelev2">&gt; &gt; supposed to be able to.  If anyone causes Autoconf to output stuff
</span><br>
<span class="quotelev2">&gt; &gt; that
</span><br>
<span class="quotelev2">&gt; &gt; is not Eligible Output Material, then they will not be able to take
</span><br>
<span class="quotelev2">&gt; &gt; advantage of the exception: they would have to distribute the
</span><br>
<span class="quotelev2">&gt; &gt; configure
</span><br>
<span class="quotelev2">&gt; &gt; scripts under the terms of GPLv3 alone.
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; Another benefit of the new exception is that it is not FSF- 
</span><br>
<span class="quotelev1">&gt; specific in
</span><br>
<span class="quotelev2">&gt; &gt; any way.  When other copyright holders make modified versions of
</span><br>
<span class="quotelev2">&gt; &gt; Autoconf, they can apply the exact same permission text to their
</span><br>
<span class="quotelev2">&gt; &gt; changes, and it will work the way people want.
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; We hope that this new exception will help make Autoconf's  
</span><br>
<span class="quotelev1">&gt; licensing a
</span><br>
<span class="quotelev2">&gt; &gt; little more clear and robust -- if also a little more verbose -- in
</span><br>
<span class="quotelev2">&gt; &gt; the
</span><br>
<span class="quotelev2">&gt; &gt; same way that GPLv3 has done for the entire free software
</span><br>
<span class="quotelev2">&gt; &gt; community.  We
</span><br>
<span class="quotelev2">&gt; &gt; are interested in hearing feedback from Autoconf developers about
</span><br>
<span class="quotelev2">&gt; &gt; whether there might be intended good uses of the software that are  
</span><br>
<span class="quotelev1">&gt; not
</span><br>
<span class="quotelev2">&gt; &gt; covered by this exception -- or conversely, known bad uses of the
</span><br>
<span class="quotelev2">&gt; &gt; software that might be covered.  We're also interested in hearing if
</span><br>
<span class="quotelev2">&gt; &gt; there are particular parts of the text that you think might be
</span><br>
<span class="quotelev2">&gt; &gt; misunderstood by developers: it may not always be possible, but we'd
</span><br>
<span class="quotelev2">&gt; &gt; like for this exception to be as clear as possible to as many people
</span><br>
<span class="quotelev2">&gt; &gt; as
</span><br>
<span class="quotelev2">&gt; &gt; possible.  If you're interested, please review the text and let us
</span><br>
<span class="quotelev2">&gt; &gt; know
</span><br>
<span class="quotelev2">&gt; &gt; what you think.
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; Below is the text of the proposed exception.
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; Thanks to Brett Smith for help in preparing this message.
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; --- cut ---
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; This Exception is an additional permission under section 7 of the  
</span><br>
<span class="quotelev1">&gt; GNU
</span><br>
<span class="quotelev2">&gt; &gt; General Public License, version 3 (&quot;GPLv3&quot;).
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; The purpose of this Exception is to allow distribution of Autoconf's
</span><br>
<span class="quotelev2">&gt; &gt; typical output under terms of the recipient's choice (including
</span><br>
<span class="quotelev2">&gt; &gt; proprietary).
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; 0. Definitions
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; &quot;Covered Code&quot; is any source code and/or object code of Autoconf
</span><br>
<span class="quotelev2">&gt; &gt; that is a
</span><br>
<span class="quotelev2">&gt; &gt; covered work under this License.
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; &quot;Eligible Output Material&quot; is Covered Code that is included in the
</span><br>
<span class="quotelev2">&gt; &gt; standard, minimally verbose, non-debugging and non-tracing output of
</span><br>
<span class="quotelev2">&gt; &gt; the
</span><br>
<span class="quotelev2">&gt; &gt; version of Autoconf distributed to you under this License.   
</span><br>
<span class="quotelev1">&gt; Moreover,
</span><br>
<span class="quotelev2">&gt; &gt; &quot;Eligible Output Material&quot; may be comprised only of Covered Code
</span><br>
<span class="quotelev2">&gt; &gt; that (a)
</span><br>
<span class="quotelev2">&gt; &gt; must necessarily appear in Autoconf-generated configure scripts and
</span><br>
<span class="quotelev2">&gt; &gt; (b) is
</span><br>
<span class="quotelev2">&gt; &gt; required for those configure scripts to function.
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; &quot;Ineligible Output Material&quot; is Covered Code that is not Eligible
</span><br>
<span class="quotelev2">&gt; &gt; Output
</span><br>
<span class="quotelev2">&gt; &gt; Material.
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; 1. Grant of Additional Permission.
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; You have permission to propagate output of Autoconf, even if such
</span><br>
<span class="quotelev2">&gt; &gt; propagation would otherwise violate the terms of GPLv3.  However, if
</span><br>
<span class="quotelev2">&gt; &gt; you
</span><br>
<span class="quotelev2">&gt; &gt; cause Autoconf to output any Ineligible Output Material, you do not
</span><br>
<span class="quotelev2">&gt; &gt; have
</span><br>
<span class="quotelev2">&gt; &gt; permission to convey the resulting covered work under this Exception
</span><br>
<span class="quotelev2">&gt; &gt; and
</span><br>
<span class="quotelev2">&gt; &gt; you must remove this Exception in accordance with the second
</span><br>
<span class="quotelev2">&gt; &gt; paragraph of
</span><br>
<span class="quotelev2">&gt; &gt; GPLv3's Section 7.
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; 2. No Weakening of Autoconf Copyleft.
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
<span class="quotelev2">&gt; &gt; The availability of this Exception does not imply any general
</span><br>
<span class="quotelev2">&gt; &gt; presumption
</span><br>
<span class="quotelev2">&gt; &gt; that third-party software is unaffected by the copyleft requirements
</span><br>
<span class="quotelev2">&gt; &gt; of
</span><br>
<span class="quotelev2">&gt; &gt; the license of Autoconf.
</span><br>
<span class="quotelev2">&gt; &gt;
</span><br>
