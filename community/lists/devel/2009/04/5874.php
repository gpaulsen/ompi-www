<p>
OMPI testing organizations:
<br>
<p>*** This is an end-of-life announcement for the &quot;ompi-core-testers&quot;  
<br>
MTT SVN branch.  This branch will be deleted on 25 May, 2009.
<br>
<p>*** Also note that new development will soon be occurring on the MTT  
<br>
SVN trunk such that it may become unstable at times.  We encourage all  
<br>
OMPI testing organizations using the MTT trunk to move to the  
<br>
official /v3.0/ branch (see below).
<br>
<p>All organizations are strongly encouraged to do the following:
<br>
<p>1. Migrate to use the new <a href="https://svn.open-mpi.org/svn/mtt/branches/v3.0/">https://svn.open-mpi.org/svn/mtt/branches/v3.0/</a> 
<br>
&nbsp;&nbsp;branch.  This branch is a copy of MTT's trunk as of 24 April 2009.   
<br>
This branch is therefore the newest version of MTT, but has been in  
<br>
production use by Cisco, Sun, and Indiana U. (and others) for many  
<br>
months.  It is quite stable and is the preferred version to use.  Its  
<br>
INI file syntax is slightly different than the old &quot;ompi-core-testers&quot;  
<br>
branch; you'll likely need to update your syntax.
<br>
<p>2. If you are currently using the ompi-core-testers branch and cannot  
<br>
upgrade your INI file syntax, please move to the <a href="https://svn.open-mpi.org/svn/mtt/branches/v2.0/">https://svn.open-mpi.org/svn/mtt/branches/v2.0/</a> 
<br>
&nbsp;&nbsp;branch.  It is a copy of the ompi-core-testers branch as of 24 April  
<br>
2009, so nothing has changed.  The name is simply more consistent and  
<br>
allows a clear versioning path forward.
<br>
<p>Thanks.
