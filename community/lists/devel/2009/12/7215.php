<p>
WHAT: Branch (again) for v1.5
<br>
<p>WHY: Heavy divergence of trunk vs. v1.5 with few associated CMR's
<br>
<p>WHERE: v1.5
<br>
<p>WHEN: Tuesday, Dec. 15th 2009 after the teleconference
<br>
<p>TIMEOUT: Dec. 15th, 2009
<br>
<p><p>More Details
<br>
------------
<br>
<p>With v1.4 finally being out in the wild, we'd like to advance on the feature 
<br>
release v1.5.
<br>
<p>There have been some quite radical changes on the trunk recently that make the 
<br>
tracking of divergence vs. v1.5 needlessly hard (please see the overview of 
<br>
action items patches if we do not follow this RFC).  Paired with this, most 
<br>
developers have not been filing v1.5 CMR's; someone would need to chase down 
<br>
everything that has happened on the trunk and &quot;encourage&quot; developers to then 
<br>
file relevant CMRs.
<br>
<p>Re-branching will likely cause some &quot;I told you so&quot;'s.  We tip our hats to 
<br>
acknowledge that you were right.  :-)
<br>
<p>We hope that re-branching, paired with the new automatically-create-CMRs-via-
<br>
SVN-commit-messages feature will help keep a new v1.5 branch in much better 
<br>
shape.  
<br>
<p>The most relevant question for the discussion would seem to be: is there 
<br>
anything on the trunk that is not intended for v1.5?  Let's discuss next 
<br>
Tuesday.
<br>
<p>Regardless of whether we re-branch or not, we'd like to ask all to ramp up 
<br>
v1.5 MTT-testing, possibly even by adding other test applications.
<br>
<p>With best regards,
<br>
Jeff and Rainer
