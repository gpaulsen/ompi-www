<p>
The MPI Forum announced at its SC09 BOF that they are soliciting  
<br>
community feedback to help guide the MPI-3 standards process.  A  
<br>
survey is available online at the following URL:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://mpi-forum.questionpro.com/">http://mpi-forum.questionpro.com/</a>
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Password: mpi3
<br>
<p>In this survey, the MPI Forum is asking as many people as possible for  
<br>
feedback on the MPI-3 process -- what features to include, what  
<br>
features to not include, etc.
<br>
<p>We encourage you to forward this survey on to as many interested and  
<br>
relevant parties as possible.
<br>
<p>It will take approximately 10 minutes to complete the questionnaire.
<br>
<p>No question in the survey is mandatory; feel free to only answer the  
<br>
questions which are relevant to you and your applications. Your  
<br>
answers will help the MPI Forum guide its process to create a  
<br>
genuinely useful MPI-3 standard.
<br>
<p>This survey closes December 31, 2009.
<br>
<p>Your survey responses will be strictly confidential and data from this  
<br>
research will be reported only in the aggregate. Your information will  
<br>
be coded and will remain confidential. If you have questions at any  
<br>
time about the survey or the procedures, you may contact the MPI Forum  
<br>
via email to mpi-comments_at_mpi-forum.org.
<br>
<p>Thank you very much for your time and support.
