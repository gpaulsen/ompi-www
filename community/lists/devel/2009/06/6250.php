<p>
All:
<br>
We are close to releasing 1.3.3.  This is the current plan:
<br>
&nbsp;&nbsp;- Evening of 6/16: collect MTT runs on the current branch w/the current
<br>
1.3.3 features &amp; fixes
<br>
&nbsp;&nbsp;- If all goes well with the overnight MTT runs, roll a release candidate
<br>
on 6/17
<br>
&nbsp;&nbsp;- Put 1.3.3rc1 through its paces over the next couple of days
<br>
&nbsp;&nbsp;- If all goes well with rc1, release 1.3.3 on Friday, June 19
<br>
<p>1.3.3 will include support for Windows as its major new feature, as well as
<br>
a number of defect fixes.
<br>
<p>1.3.3 will be the final feature release in the 1.3 series.  As such, with
<br>
the new feature/stable numbering
<br>
scheme, the next release in the series will contain defect fixes only and
<br>
will transition to 1.4.  This
<br>
will be the stable/maintenance branch.  The plan is for it to follow the
<br>
1.3.3 release by a fairly short time
<br>
(4-6 weeks), and subsequent releases in the series will take place as need
<br>
be depending on the bug
<br>
fix volume &amp; criticality.
<br>
<p>Thanks,
<br>
--brad
<br>
1.3/1.4 co-release mgr
