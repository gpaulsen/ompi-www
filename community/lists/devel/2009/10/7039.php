<p>
The IU sysadmin has added some special sauce to the OMPI Trac that  
<br>
allows arbitrary users to get Trac accounts.  Here's what this means:
<br>
<p>&nbsp;&nbsp;1. The login for Trac is now a web page rather than an HTTP Basic  
<br>
Auth.  You may notice that the login page is now slightly different.
<br>
<p>&nbsp;&nbsp;2. You may have just gotten an email asking you to verify your Trac  
<br>
email address.  Please click through and verify it.  If you didn't get  
<br>
this, please go check your email address in Trac.
<br>
<p>&nbsp;&nbsp;3. Anyone who signs up for a Trac account (whether they're a core  
<br>
developer or not) can create tickets, comment on tickets, etc.
<br>
<p>&nbsp;&nbsp;4. Signing up for a Trac account does not give you SVN write  
<br>
access.  That's still a separate process that is gated by submitting a  
<br>
signed Open MPI 3rd Party Contribution form.
<br>
<p>Please let me know if you have any questions or problems.
