<p>
After a round of internal reviews, Rainer and I will shortly post the  
<br>
full v1.5 series plan on the wiki.
<br>
<p>The first item up for community feedback is the proposal we made on  
<br>
the weekly engineering call today: branch for v1.5 COB Friday, Oct 9.   
<br>
We'll start making nightly v1.5 tarballs which will be suitable for  
<br>
MTT.  The intent is to stabilize what is currently on the trunk and  
<br>
release it as v1.5.0.  A target release date has not yet been picked  
<br>
-- the only thing we want to do at this point is branch and start  
<br>
stablizing.
<br>
<p>1.5.1 will be open for new features.  Please see <a href="https://svn.open-mpi.org/trac/ompi/milestone/Open%20MPI%201.5">https://svn.open-mpi.org/trac/ompi/milestone/Open%20MPI%201.5</a> 
<br>
&nbsp;&nbsp;for more details on the v1.5 plan (more will be showing up there  
<br>
over the next week, after the internal reviews are done).
<br>
<p>Does anyone have any objections to branching for v1.5 this Friday?
