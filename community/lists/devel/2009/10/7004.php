<p>
Thanks to Sun who have integrated padb with MTT there are now padb
<br>
traces in the automated testing logs of OpenMPI for cases where the
<br>
tests have timed out.  These traces show the contents of MPI message
<br>
queues, stack traces and where available local variables and parameters
<br>
to functions.
<br>
<p>An example can be found at the link below, I hope people find it useful.
<br>
<p><a href="http://www.open-mpi.org/mtt/index.php?do_redir=1161">http://www.open-mpi.org/mtt/index.php?do_redir=1161</a>
<br>
<p>I am actively working on improvements for this if anybody has anything
<br>
they would like to see added.
<br>
