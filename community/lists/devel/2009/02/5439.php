OMPI Developers --
<br>
<p>Ralph and I are continuing our Mercurial investigations.  The dominos  
<br>
are starting to fall in order nicely and it's looking pretty good that  
<br>
the Open MPI project could switch away from Subversion to Mercurial if  
<br>
we choose to.  You can see our current list of open questions / items  
<br>
on this wiki page:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://svn.open-mpi.org/trac/ompi/wiki/Mercurial">https://svn.open-mpi.org/trac/ompi/wiki/Mercurial</a>
<br>
<p>One question we have for OMPI developers: are you using any Subversion- 
<br>
specific tools?  E.g., are you using the SVN Eclipse plugin?  ...or  
<br>
any other SVN-bundled or third-party tool that was specifically  
<br>
designed for SVN?  If so, are there Mercurial equivalents, and/or can  
<br>
you live without this tool if we switch?
