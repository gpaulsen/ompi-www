<p>
Indiana U. has added another service to the Open MPI web site: a fully  
<br>
indexed and searchable database of Open MPI source code trees.   
<br>
There's a link under &quot;Source Code Access&quot; entitled &quot;Searchable source  
<br>
tree&quot; on the OMPI web site that takes you to <a href="https://svn.open-mpi.org/source/">https://svn.open-mpi.org/source/</a> 
<br>
&nbsp;&nbsp;(get the CA cert from <a href="http://www.cs.indiana.edu/Facilities/FAQ/Mail/csci.crt">http://www.cs.indiana.edu/Facilities/FAQ/Mail/csci.crt</a> 
<br>
&nbsp;&nbsp;to avoid SSL certificate warnings from your browser).
<br>
<p>This site, powered by OpenGrok, is a &quot;wicked fast&quot; searchable database  
<br>
of both the Open MPI SVN trunk and v1.3 release branch.
<br>
<p>Enjoy!
