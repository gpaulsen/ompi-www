<p>On Feb 3, 2010, at 8:23 AM, Jeff Squyres wrote:
<br>
<p><span class="quotelev1">&gt; A little while ago, IU added the feature of automatically creating CMRs from SVN commits when you add tokens like this in your commit message:
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    svn ci -m &quot;This fixes ...foo...  cmr:v1.4.2&quot;
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; IU just extended this feature by allowing you to specify a reviewer, thusly:
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    svn ci -m &quot;This fixes ...foo...  cmr:v1.4.2:reviewer=jsquyres&quot;
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; You must specify a valid Trac ID.  If you do this, the ticket will be assigned to that ID, meaning that they'll get an email with the ticket and a request to review it.
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; More description is here:
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    <a href="https://svn.open-mpi.org/trac/ompi/wiki/TracSVNCommitMessages">https://svn.open-mpi.org/trac/ompi/wiki/TracSVNCommitMessages</a>
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; Enjoy!
