<br>
<p>I took a *brief* look at the TIPC web site -- it is sorta analogous to Open-MX?  (i.e., sending raw ethernet frames as opposed to TCP)
<br>
<p>We'd love to see a BTL module for TIPC in Open MPI -- are you interested in writing one?  I'd be happy to explain how to do so.
