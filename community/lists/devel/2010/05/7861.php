On Apr 30 2010, Ralph Castain wrote:
<br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Guess this has been too upsetting a question - I'll work off-list with 
</span><br>
<span class="quotelev1">&gt; the other developers to determine an appropriate OMPI behavior.
</span><br>
<p>I have responded to Ralph Castain by Email, but I need to correct the
<br>
implication in the above.
<br>
<p>What OpenMPI chooses to do, and to document, is a matter for OpenMPI alone.
<br>
The MPI standard makes it very clear that implementations are encouraged to
<br>
produce sane specifications for the behaviour that MPI has left undefined,
<br>
and to document them.
<br>
<p>What OpenMPI proposes for inclusion in the forthcoming MPI 3.0 standard has
<br>
to be acceptable to all of the other MPI implementations, present and
<br>
future.  Regrettably, Ralph Castain's desired specification is not feasible
<br>
in general, and is therefore unsuitable for MPI 3.0 (unless the portability
<br>
of MPI is sacrificed).
<br>
<p>So there are two issues, which shouldn't be confused.
<br>
