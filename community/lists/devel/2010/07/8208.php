<p><span class="quotelev1">&gt; We used to have the branches &quot;locked&quot; to all but the gatekeeper to prevent this kind of mistake. Did this change? Or did you forget to lock the 1.5 branch?
</span><br>
<p>We changed it for v1.5 -- they're *not* locked because Ralph (back when Ralph was the RM from LANL with me) and I anticipated letting developers commit their &quot;larger&quot; features over to the release branch themselves and spare some GK pain.
<br>
<p>E.g., an ORTE refresh may take some pain and iteration to get right.  So let Ralph do it himself rather than do it via George/GK proxy.
<br>
<p>Similar for hwloc issues.  Similar for ROMIO refresh.  ...etc.
<br>
