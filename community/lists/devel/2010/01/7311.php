We mentioned today on the call a potentially aggressive schedule to get v1.5 out the door:
<br>
<p>- re-branch from SVN trunk this Friday, 13 Jan, 2010
<br>
- target release for Tuesday, 16 Feb, 2010
<br>
<p>Yes, this means releasing in about 5 weeks.  It's an aggressive schedule, but given that the trunk is pretty stable, it's quite do-able (there was consensus about this on the call today).  I've updated the 1.5 trac milestone and feature lists:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://svn.open-mpi.org/trac/ompi/milestone/Open%20MPI%201.5">https://svn.open-mpi.org/trac/ompi/milestone/Open%20MPI%201.5</a>
