Be sure to target the correct milestone when you use the &quot;cmr&quot; tokens in SVN commit messages.  For example, when you intend stuff to go to the next v1.4 release, you should use:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;cmr:v1.4.2
<br>
<p>If you just put &quot;cmr:v1.4&quot;, it'll target 1.4(.0), which we've already released.
