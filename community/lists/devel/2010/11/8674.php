Per some discussion on the teleconf yesterday, it was felt that some users don't necessarily understand that 1.4.x is the current &quot;stable&quot; series, and that that is the series they should be using if they want time-tested stability.
<br>
<p>I have made 2 changes to the web site:
<br>
<p>1. When you click on &quot;Download&quot; on the left hand navigation, you are now brought to the 1.4.x download page, not the 1.5.x download page (the fact that &quot;download&quot; took you to the 1.5.x download page was probably quite misleading).
<br>
<p>--&gt; Of course, you can get to any release series by clicking on the respective link on the left; I just changed the default for the general &quot;Download&quot; link.
<br>
<p>2. I changed the 1.5 entry in the left hand navigation from &quot;(feature)&quot; to &quot;(beta)&quot;.  The rationale is that users might understand that terminology better.  &quot;Beta&quot; is probably a bit too strong of a word -- we *have* done a lot of testing, but we want to set the expectation right that production sites should probably still be using 1.4.x and 1.5 should be for early adopters or those who need new features that are included in the 1.5.x series.
