<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; I'd vote for !B..  It goes against the whole branch and stabilize approach.  The fact that we've been bad about pushing changes to v1.5 is no reason to whole-sale throw out our release philosophy.  Plus, I know there's stuff in the trunk that shouldn't be in 1.5 (like my recent portals work that's nowhere near ready for prime time).
</span><br>
<p>FWIW, I outlined the reasons on the call why I thought B was appropriate.  Here's the (very) short version:
<br>
<p>- We state that changes may be large in feature release series.  The goal of a feature series is features.  Here's specifically what we state:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;o Even minor release numbers are part of &quot;super-stable&quot;
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;release series (e.g., v1.4.0). Releases in super stable series
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;are well-tested, time-tested, and mature. Such releases are
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;recomended for production sites. Changes between subsequent
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;releases in super stable series are expected to be fairly small.
<br>
&nbsp;&nbsp;&nbsp;&nbsp;o Odd minor release numbers are part of &quot;feature&quot; release
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;series (e.g., 1.3.7). Releases in feature releases are
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;well-tested, but they are not necessarily time-tested or as
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;mature as super stable releases. Changes between subsequent
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;releases in feature series may be large.
<br>
<p>- I'd prefer not to end the 1.5 series prematurely.  I.e., let's give it time to get mature before going v1.6.
<br>
- There are a lot of new features on the trunk that are worth bringing over / releasing to the world in the not-distant future.
<br>
<p>You're right that there may be some stuff that isn't ready for prime time on the trunk.  Your portals stuff is the first thing to be called out specifically, but we can handle that (e.g., not update the portals directories on the v1.5 branch).
