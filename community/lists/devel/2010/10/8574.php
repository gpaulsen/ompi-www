<p>
Developers -- I'm updating the 1.5 README file.  Are these sections still accurate?  Please send me corrections ASAP:
<br>
<p>- The run-time systems that are currently supported are:
<br>
&nbsp;&nbsp;- rsh / ssh
<br>
&nbsp;&nbsp;- LoadLeveler
<br>
&nbsp;&nbsp;- PBS Pro, Open PBS, Torque
<br>
&nbsp;&nbsp;- Platform LSF (v7.0.2 and later)
<br>
&nbsp;&nbsp;- SLURM
<br>
&nbsp;&nbsp;- Cray XT-3 and XT-4
<br>
&nbsp;&nbsp;- Sun Grid Engine (SGE) 6.1, 6.2 and open source Grid Engine
<br>
&nbsp;&nbsp;- Microsoft Windows CCP (Microsoft Windows server 2003 and 2008)
<br>
<p>- Systems that have been tested are:
<br>
&nbsp;&nbsp;- Linux (various flavors/distros), 32 bit, with gcc, and Sun Studio 12
<br>
&nbsp;&nbsp;- Linux (various flavors/distros), 64 bit (x86), with gcc, Absoft,
<br>
&nbsp;&nbsp;&nbsp;&nbsp;Intel, Portland, and Sun Studio 12 compilers (*)
<br>
&nbsp;&nbsp;- OS X (10.5, 10.6), 32 and 64 bit (x86_64), with gcc and Absoft
<br>
&nbsp;&nbsp;&nbsp;&nbsp;compilers (*)
<br>
&nbsp;&nbsp;- Solaris 10 update 2, 3 and 4, 32 and 64 bit (SPARC, i386, x86_64),
<br>
&nbsp;&nbsp;&nbsp;&nbsp;with Sun Studio 10, 11 and 12
<br>
<p>&nbsp;&nbsp;(*) Be sure to read the Compiler Notes, below.
<br>
<p>- Other systems have been lightly (but not fully tested):
<br>
&nbsp;&nbsp;- Other 64 bit platforms (e.g., Linux on PPC64)
<br>
&nbsp;&nbsp;- Microsoft Windows CCP (Microsoft Windows server 2003 and 2008);
<br>
&nbsp;&nbsp;&nbsp;&nbsp;see the README.WINDOWS file.
<br>
