<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; FWIW, I outlined the reasons on the call why I thought B was appropriate.  Here's the (very) short version:
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; - We state that changes may be large in feature release series.  The goal of a feature series is features.  Here's specifically what we state:
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    o Even minor release numbers are part of &quot;super-stable&quot;
</span><br>
<span class="quotelev1">&gt;      release series (e.g., v1.4.0). Releases in super stable series
</span><br>
<span class="quotelev1">&gt;      are well-tested, time-tested, and mature. Such releases are
</span><br>
<span class="quotelev1">&gt;      recomended for production sites. Changes between subsequent
</span><br>
<span class="quotelev1">&gt;      releases in super stable series are expected to be fairly small.
</span><br>
<span class="quotelev1">&gt;    o Odd minor release numbers are part of &quot;feature&quot; release
</span><br>
<span class="quotelev1">&gt;      series (e.g., 1.3.7). Releases in feature releases are
</span><br>
<span class="quotelev1">&gt;      well-tested, but they are not necessarily time-tested or as
</span><br>
<span class="quotelev1">&gt;      mature as super stable releases. Changes between subsequent
</span><br>
<span class="quotelev1">&gt;      releases in feature series may be large.
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; - I'd prefer not to end the 1.5 series prematurely.  I.e., let's give it time to get mature before going v1.6.
</span><br>
<span class="quotelev1">&gt; - There are a lot of new features on the trunk that are worth bringing over / releasing to the world in the not-distant future.
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; You're right that there may be some stuff that isn't ready for prime time on the trunk.  Your portals stuff is the first thing to be called out specifically, but we can handle that (e.g., not update the portals directories on the v1.5 branch).
</span><br>
<p><p>I understand what you're saying.  But the fact still remains that our release process is supposed to be features move as needed/requested/ready from the trunk to the development release.  This is not that at all - this is &quot;we're lazy, so we're going to do a wholesale sync whether features are ready or not&quot;.  That's a bad plan.  If you want developers to list the features that should move, that's fine.  But just saying &quot;we've decided everything is ready because it's easier&quot; seems like a bad precedent.
