As we discussed on the call last week, since there is already a bit of a divergence between the trunk and the v1.5 branch, how's this for a wild idea: 
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;What if we re-sync the entire trunk to the v1.5 branch, stabilize that, 
<br>
&nbsp;&nbsp;&nbsp;&nbsp;and call it v1.5.1?
<br>
<p>The assumption here is that it will be [far] easier to just re-sync the trunk to the v1.5 branch than to try to bring over stuff in a piecemeal fashion.
<br>
<p>There's a *bunch* of new stuff on the trunk that is not on the v1.5 branch -- there's more than enough &quot;meat&quot; to call it a new release.  
<br>
<p>*** Put differently: is there anything on the trunk that is *not* ready to go to the v1.5 series?
