Hi,
<br>
<p>Does the IMPI protocol solve cross-platform endian/size issues? I'm not sure if this is implied by what's stated in the OpenMPI support-systems FAQ:
<br>
<p><a href="http://www.open-mpi.org/faq/?category=supported-systems">http://www.open-mpi.org/faq/?category=supported-systems</a>
<br>
<p>&quot;8. Does Open MPI support execution in heterogeneous environments?
<br>
<p>Open MPI currently requires that the representation of C, C++, and Fortran datatypes be the same on all platforms within a single parallel application. Endian and size difference between nodes in a parallel job will cause undefined behavior in Open MPI.
<br>
<p>Support for heterogeneous run-time environments is on the to-do list, but has not yet been implemented.&quot;
