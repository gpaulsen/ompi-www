<p>
The Open MPI project has finally resumed formal bug tracking.  We are
<br>
using the Trac system (<a href="http://trac.edgewall.com/">http://trac.edgewall.com/</a>) that nicely integrates
<br>
bug tracking with Subversion through revision and ticket cross linking,
<br>
wiki-like features, etc.  The Open MPI bug tracker is available here:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://svn.open-mpi.org/trac/ompi/">http://svn.open-mpi.org/trac/ompi/</a>
<br>
<p>There is also a link in the left-hand navigation on the Open MPI web
<br>
sites for &quot;Bug tracking&quot; (mirrors will get it at their next update).
<br>
Submission of bugs is limited to developers; reporting bugs on the
<br>
mailing lists is still the preferred method to submit issues to us.
<br>
<p>For those who want to follow all bug activity, you can sign up on the
<br>
&quot;bugs&quot; mailing list:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.open-mpi.org/mailman/listinfo.cgi/bugs">http://www.open-mpi.org/mailman/listinfo.cgi/bugs</a>
