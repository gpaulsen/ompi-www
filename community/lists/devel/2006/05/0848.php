<p>Thanks for your interest.
<br>
<p><span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Is the &quot;process fault tolerance&quot; aka FT-MPI semantics being worked  
</span><br>
<span class="quotelev1">&gt; on? Or what is the roadmap for this feature? Will it be one of the  
</span><br>
<span class="quotelev1">&gt; frameworks in the MCA?
</span><br>
<p>Process fault tolerance is currently under active development. At the  
<br>
moment, we are working on integrating checkpoint/restart (LAM/MPI- 
<br>
like) style of fault tolerance into Open MPI. The integration of that  
<br>
effort into the trunk is still a few months off, at best.
<br>
<p>FT-MPI style is on the roadmap, but with no clear date for release  
<br>
unfortunately. We have not laid out exactly how FT-MPI style will be  
<br>
implemented, but it will likely involve one or more MCA frameworks.
<br>
<p><span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Thanks for your time. If there's a better place to ask these  
