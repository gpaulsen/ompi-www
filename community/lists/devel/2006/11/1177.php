Hi everyone,
<br>
Just a heads up that we shouldn't schedule any OMPI things on Dec. 19th.
<br>
The Open MPI mailing lists, SVN, trac, the website, pretty much everything
<br>
will be down that day.
<br>
See Indiana University's power outage announcement e-mail below:
<br>
<p>---------- Forwarded message ----------
<br>
Subject: Lindley Hall Power Outage 12/19/2006
<br>
<p>There will be a power outage in Lindley Hall from 8-12am Tuesday December
<br>
19th. This outage will only affect the 480V feed to the building which
<br>
includes the Computer Science Department computer machine room, the elevator,
<br>
and the air handling system. All CS servers located in the machine room
<br>
(LH416) will be down during this time. This will take down the network as
<br>
well as all the file/mail/web/etc servers for the department effectively
<br>
rendering just about every computer in the building useless.
<br>
<p>Please note that the building lighting and 110V outlets will not be affected
<br>
so most workstations will have power through the outage. However, with the
<br>
network and servers being down the workstations will be mostly unusable
<br>
and we expect to have to reboot many workstations at the conclusion of
<br>
the outage.
<br>
<p>This outage is related to construction of the new science building (MSB-1).
<br>
---------- End Forwarded message ----------
