<p>
The Open MPI SVN server will be going down for maintenance from  
<br>
noon-4pm US Eastern on Wednesday, 15 March, 2006 (i.e., next Wednesday).
<br>
<p>All Open MPI-related repositories will be offline during this time,  
<br>
including:
<br>
<p>ompi
<br>
mtt
<br>
plpa
<br>
<p>The purpose of the downtime is to migrate the repositories to new  
<br>
server hardware.  The IP name, IP address, and SSL certificate will  
<br>
be staying the same, so all previous checkouts, both authenticated  
<br>
and anonymous, should continue to work once the repositories are  
<br>
brought back on-line.  Hence, this downtime is simply a few hours  
<br>
when you will not be able to update or commit.
