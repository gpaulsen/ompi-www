<p>
Yo all
<br>
<p>As part of the ORTE discussions that took place today, it was mentioned that
<br>
we would be modifying the hostfile behavior to support several user requests
<br>
and to resolve problems when mixing resource manager-provided allocations
<br>
and hostfiles. This has been an on-going discussion for some time, most
<br>
recently resulting a couple of months ago in a specification for hostfile
<br>
syntax and behavior.
<br>
<p>Tim Prins had volunteered to modify the hostfile system to support the
<br>
revised behavior. I'm hoping he will still find time to do so once he
<br>
returns from his impending vacation. The modified hostfile component will be
<br>
moved to the RAS framework as part of this work (and then the RDS framework
<br>
will be removed).
<br>
<p>For anyone interested, you will find a complete description of the
<br>
use-cases, desired behavior, and planned modifications in Ticket #1018 on
<br>
our tracking system:
<br>
<p><a href="https://svn.open-mpi.org/trac/ompi/ticket/1018">https://svn.open-mpi.org/trac/ompi/ticket/1018</a>
<br>
<p>Please feel free to comment there regarding any concerns or suggestions you
<br>
might have.
<br>
<p>Thanks
<br>
Ralph
