<p>
I didn't really put this in RFC format with a timeout, but no one  
<br>
objected, so I have created:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://svn.open-mpi.org/svn/ompi/public">http://svn.open-mpi.org/svn/ompi/public</a>
<br>
<p>Developers should feel free to use this tree for public temporary  
<br>
branches.  Specifically:
<br>
<p>- use /tmp if your branch is intended to be private
<br>
- use /public if your branch is intended to be public
<br>
<p>Enjoy.
<br>
