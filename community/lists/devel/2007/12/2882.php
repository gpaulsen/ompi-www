<p><span class="quotelev1">&gt; EuroPVM/MPI 2008 CALL FOR PAPERS
</span><br>
<span class="quotelev1">&gt;  	
</span><br>
<span class="quotelev1">&gt; 15th European PVMMPI Users' Group Meeting
</span><br>
<span class="quotelev1">&gt; Dublin, Ireland, September 7 - 10, 2008
</span><br>
<span class="quotelev1">&gt; web: <a href="http://pvmmpi08.ucd.ie">http://pvmmpi08.ucd.ie</a>
</span><br>
<span class="quotelev1">&gt; organized by UCD School of Computer Science and Informatics
</span><br>
<span class="quotelev1">&gt;  	
</span><br>
<span class="quotelev1">&gt; BACKGROUND AND TOPICS
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; PVM (Parallel Virtual Machine) and MPI (Message Passing Interface)
</span><br>
<span class="quotelev1">&gt; have evolved into the standard interfaces for high-performance  
</span><br>
<span class="quotelev1">&gt; parallel
</span><br>
<span class="quotelev1">&gt; programming in the message-passing paradigm. EuroPVM/MPI is the most
</span><br>
<span class="quotelev1">&gt; prominent meeting dedicated to the latest developments of PVM and MPI,
</span><br>
<span class="quotelev1">&gt; their use, including support tools, and implementation, and to
</span><br>
<span class="quotelev1">&gt; applications using these interfaces. The EuroPVM/MPI meeting naturally
</span><br>
<span class="quotelev1">&gt; encourages discussions of new message-passing and other parallel and
</span><br>
<span class="quotelev1">&gt; distributed programming paradigms beyond MPI and PVM.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; The 15th European PVM/MPI Users' Group Meeting will be a forum for  
</span><br>
<span class="quotelev1">&gt; users
</span><br>
<span class="quotelev1">&gt; and developers of PVM, MPI, and other message-passing programming
</span><br>
<span class="quotelev1">&gt; environments. Through the presentation of contributed papers, vendor
</span><br>
<span class="quotelev1">&gt; presentations, poster presentations and invited talks, attendees will
</span><br>
<span class="quotelev1">&gt; have the opportunity to share ideas and experiences to contribute to  
</span><br>
<span class="quotelev1">&gt; the
</span><br>
<span class="quotelev1">&gt; improvement and furthering of message-passing and related parallel
</span><br>
<span class="quotelev1">&gt; programming paradigms.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Topics of interest for the meeting include, but are not limited to:
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt;    * PVM and MPI implementation issues and improvements
</span><br>
<span class="quotelev1">&gt;    * Latest extensions to PVM and MPI
</span><br>
<span class="quotelev1">&gt;    * PVM and MPI for high-performance computing, clusters and grid
</span><br>
<span class="quotelev1">&gt;      environments
</span><br>
<span class="quotelev1">&gt;    * New message-passing and hybrid parallel programming paradigms
</span><br>
<span class="quotelev1">&gt;    * Interaction between message-passing software and hardware
</span><br>
<span class="quotelev1">&gt;    * Fault tolerance in message-passing programs
</span><br>
<span class="quotelev1">&gt;    * Performance evaluation of PVM and MPI applications
</span><br>
<span class="quotelev1">&gt;    * Tools and environments for PVM and MPI
</span><br>
<span class="quotelev1">&gt;    * Algorithms using the message-passing paradigm
</span><br>
<span class="quotelev1">&gt;    * Applications in science and engineering based on message-passing
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Submissions on applications demonstrating both the potential and
</span><br>
<span class="quotelev1">&gt; shortcomings of MPI and PVM are particularly welcome.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; As in the previous years, the special session 'ParSim' will focus on
</span><br>
<span class="quotelev1">&gt; numerical simulation for parallel engineering environments. EuroPVM/ 
</span><br>
<span class="quotelev1">&gt; MPI
</span><br>
<span class="quotelev1">&gt; 2008
</span><br>
<span class="quotelev1">&gt; will also hold the 'Outstanding Papers' session, where the best papers
</span><br>
<span class="quotelev1">&gt; selected by the program committee will be presented.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; SUBMISSION INFORMATION
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Contributors are invited to submit a full paper as a PDF document not
</span><br>
<span class="quotelev1">&gt; exceeding 8 pages in English (2 pages for poster abstracts). The title
</span><br>
<span class="quotelev1">&gt; page should contain an abstract of at most 100 words and five specific
</span><br>
<span class="quotelev1">&gt; keywords.
</span><br>
<span class="quotelev1">&gt; The paper needs to be formatted according to the Springer LNCS
</span><br>
<span class="quotelev1">&gt; guidelines
</span><br>
<span class="quotelev1">&gt; (<a href="http://www.springer.de/comp/lncs/authors.html">http://www.springer.de/comp/lncs/authors.html</a>). The usage of LaTeX  
</span><br>
<span class="quotelev1">&gt; for
</span><br>
<span class="quotelev1">&gt; preparation of the contribution as well as the submission in camera
</span><br>
<span class="quotelev1">&gt; ready
</span><br>
<span class="quotelev1">&gt; format is strongly recommended. Style files can be found at
</span><br>
<span class="quotelev1">&gt; <a href="http://www.springer.de/comp/lncs/authors.html">http://www.springer.de/comp/lncs/authors.html</a>. New work that is not  
</span><br>
<span class="quotelev1">&gt; yet
</span><br>
<span class="quotelev1">&gt; mature for a full paper, short observations, and similar brief
</span><br>
<span class="quotelev1">&gt; announcements are invited for the poster session. Contributions to the
</span><br>
<span class="quotelev1">&gt; poster session should be submitted in the form of a two page abstract.
</span><br>
<span class="quotelev1">&gt; All these contributions will be fully peer reviewed by the program
</span><br>
<span class="quotelev1">&gt; committee.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Submissions to the special session 'Current Trends in Numerical
</span><br>
<span class="quotelev1">&gt; Simulation for Parallel Engineering Environments' (ParSim 2008) are
</span><br>
<span class="quotelev1">&gt; handled and reviewed by the respective session chairs. For more
</span><br>
<span class="quotelev1">&gt; information please refer to the ParSim website
</span><br>
<span class="quotelev1">&gt; (<a href="http://www.lrr.in.tum.de/Par/arch/events/parsim08/">http://www.lrr.in.tum.de/Par/arch/events/parsim08/</a>).
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; All accepted submissions are expected to be presented at the  
</span><br>
<span class="quotelev1">&gt; conference
</span><br>
<span class="quotelev1">&gt; by one of the authors, which requires registration for the conference.
</span><br>
<span class="quotelev1">&gt;
</span><br>
