<p>
Okay, I have heard back from several people. The consensus seems to be that:
<br>
<p>1. a regular telecon is probably not worth doing at this time. We will look
<br>
at this again as more people start regularly contributing to the code
<br>
<p>2. some kind of web-based system that keeps people apprised of what people
<br>
are doing - and are planning on doing - would be very helpful
<br>
<p>3. more frequent &quot;announcements&quot; of changes would help, especially when they
<br>
are significant
<br>
<p>Based on the feedback, I am going to setup an OpenRTE wiki to track current
<br>
areas of activity and planned &quot;next-steps&quot;. In addition, I will try to keep
<br>
some kind of &quot;blog-like&quot; record of my activities on that site so people can
<br>
get a view of what I'm up to (since I am the primary - though not exclusive
<br>
- person modifying the code). For instance, I frequently find and fix minor
<br>
&quot;bugs&quot; as I'm working on adding functionality to the code - I will note
<br>
those on the site as I am fixing them.
<br>
<p>Again, I would like to note that people are always welcome to drop me a note
<br>
or call me on the phone if they have a question about what I'm doing or
<br>
planning to do.
<br>
<p>Thanks
<br>
Ralph
