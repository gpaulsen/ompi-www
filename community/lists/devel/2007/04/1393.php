On Apr 3, 2007, at 9:07 AM, pooja_at_[hidden] wrote:
<br>
<p><span class="quotelev1">&gt; Actually I am working on the course project in which I am running a  
</span><br>
<span class="quotelev1">&gt; huge
</span><br>
<span class="quotelev1">&gt; computational intensive code.
</span><br>
<span class="quotelev1">&gt; I am running this code on cluster.
</span><br>
<span class="quotelev1">&gt; Now my work is to find out when does the process send control messages
</span><br>
<span class="quotelev1">&gt; (e.g. compute process to I/O  process indicating I/O data is ready)
</span><br>
<p>By &quot;I/O&quot;, do you mean stdin/stdout/stderr, or other file I/O?
<br>
<p>If you mean stdin/stdout/stderr, this is handled by the IOF (I/O  
<br>
Forwarding) framework/components in Open MPI.  It's somewhat  
<br>
complicated, system-level code involving logically multiplexing data  
<br>
sent across pipes to sockets (i.e., local process(es) to remote  
<br>
process(es)).
<br>
<p>If you mean MPI-2 file I/O, you want to look at the ROMIO package; it  
<br>
handles all the MPI-2 API for I/O.
<br>
<p>Or do you mean &quot;I/O&quot; such as normal MPI messages (such as those  
<br>
generated by MPI_SEND and MPI_RECV)?  FWIW, we normally refer to  
<br>
these as MPI messages, not really &quot;I/O&quot; (we typically reserve the  
<br>
term &quot;I/O&quot; for file IO and/or stdin/stdout/stderr).
<br>
<p>Which do you mean?
<br>
<p><span class="quotelev1">&gt; and when does they send actual data (e.g I/O nodes fetching actual  
</span><br>
<span class="quotelev1">&gt; data
</span><br>
<span class="quotelev1">&gt; that is to be transfered.)
</span><br>
<p>This seems to imply that you're talking about parallel/network  
<br>
filesystems.  I have to admit that I'm now quite confused about what  
<br>
you're asking for.  :-)
<br>
<p><span class="quotelev1">&gt; And I have to log the timing and duration in other file.
</span><br>
<p>If you need to log the timing and duration of MPI calls, this is  
<br>
pretty easy to do with the PMPI interface -- you can intercept all  
<br>
MPI calls, log whatever information you want to log, invoke the  
<br>
underlying MPI function to do the real work, and then log the duration.
<br>
<p><span class="quotelev1">&gt; For this I need to know the States of Open MPi (Control messges)
</span><br>
<span class="quotelev1">&gt; So that I can simply put print statements in Open MPi code and find  
</span><br>
<span class="quotelev1">&gt; out
</span><br>
<span class="quotelev1">&gt; how it works.
</span><br>
<p>I would [strongly] advise using a debugger.  Printf statements will  
<br>
only take you so far, and can be quite confusing in a parallel  
<br>
scenario -- especially when they can alter the timing of the system  
<br>
(i.e., Heisenburg kinds of effects).
<br>
<p><span class="quotelev1">&gt; For this reason I was asking to know the state changes or atleast  
</span><br>
<span class="quotelev1">&gt; the way
</span><br>
<span class="quotelev1">&gt; to find it out.
</span><br>
<p>I'm still not clear on what state changes you're asking about.
<br>
<p>&nbsp;From this e-mail and your prior e-mails, it *seems* like you're  
<br>
asking about how data gets from MPI_SEND in one process to MPI_RECV  
<br>
in another process.  Is that right?
<br>
<p>If so, I would not characterize the code that does this as a state  
<br>
machine in the traditional sense.  Sure, as a computer program, it  
<br>
technically *is* a state machine that changes states according to  
<br>
assembly instructions, registers, etc., but we did not use generic  
<br>
state machine abstractions throughout the code base.  In many places,  
<br>
there's simply a linear sequence of events -- not a re-entrant state  
<br>
machine.
<br>
<p>So if you're asking how a user message gets from MPI_SEND in one  
<br>
process to MPI_RECV in another, we can describe that (it's a very  
<br>
complicated answer that depends on many factors, actually -- it is  
<br>
*not* a straightforward answer, not only because OMPI deals with many  
<br>
device/network types, but also because there can be many variables  
<br>
decided at run time that determine how a message is sent from a  
<br>
process to a peer).
<br>
<p>So before we go any further -- can you, as precisely as possible,  
<br>
describe exactly what information you're looking for?
<br>
