<p>
All MPI users / developers:
<br>
<p>At both the Euro PVM/MPI'07 and the SC'07 conferences, we had  
<br>
productive meetings about how to progress the MPI standard: what to do  
<br>
about pending bugs in the MPI-1 and MPI-2 specs, new ideas that are  
<br>
emerging for possible successors to MPI-2, etc.  It has been  
<br>
determined that the MPI Forum is formally going to re-convene and  
<br>
start having both electronic and physical meetings to resolve these  
<br>
issues.
<br>
<p>The first meeting is going to be in Chicago, IL, USA in early January  
<br>
2008.
<br>
<p>I strongly encourage all interested parties to participate in the  
<br>
process.  Please see the mpi-21 mailing list archives (particularly  
<br>
over the past week) and join the list yourself:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.mpi-forum.org/mpi2_1/index.htm">http://www.mpi-forum.org/mpi2_1/index.htm</a>
<br>
<p>Please spread the word.  Thanks.
<br>
