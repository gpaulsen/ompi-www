<p>
I literally just discovered that the trac &quot;milestone&quot; pages can be  
<br>
edited.
<br>
<p>This seems like a much better place to put the 1.1, 1.2, and 1.3  
<br>
release series wiki pages.  So I moved all the content and updated the  
<br>
links on the front wiki page.  Each &quot;1.x&quot; milestone is now a top-level  
<br>
view of the entire series with links to individual milestone pages for  
<br>
details about that specific release.  For example:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://svn.open-mpi.org/trac/ompi/milestone/Open%20MPI%201.2">https://svn.open-mpi.org/trac/ompi/milestone/Open%20MPI%201.2</a>
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://svn.open-mpi.org/trac/ompi/milestone/Open%20MPI%201.3">https://svn.open-mpi.org/trac/ompi/milestone/Open%20MPI%201.3</a>
<br>
<p>Release Managers / Gatekeepers:
<br>
- the pages are editable just like other wiki pages, so nothing  
<br>
changes there
<br>
- when you edit the milestone page, I note that there's a handy  
<br>
&quot;Retarget associated open tickets to milestone [X]&quot; option for moving  
<br>
all leftover tickets to the next milestone
