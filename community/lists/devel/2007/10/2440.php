<p>As previously announced, we will be holding a meeting on Wed. Nov. 14th,
<br>
from 5:30 pm to 7:00 pm in room D6 at the Sparks convention center in Reno -
<br>
site of SC'07.  The intent of the meeting is to organize for an active
<br>
period of the MPI forum.  The proposed agenda is:
<br>
<p>&nbsp;* Introduction: why restart the forum discussions
<br>
&nbsp;* Scope of effort - discussion.  There is a wide range of opinions on how
<br>
to proceed, with a clear need for clarifications and simple corrections to
<br>
the 2.0 standard (such as changing the size of the length parameter used in
<br>
MPI send functions), as well as a desire for larger changes to the standard,
<br>
such as adding functionality to support process fault tolerance.  We need to
<br>
decide if to consider both types of of changes in a single standard change,
<br>
as parallel but distinct set of changes to the standard, or some other
<br>
approach.
<br>
&nbsp;* Form of effort - all electronic, mix of electronic and periodic
<br>
face-to-face meetings, or some other manner.
<br>
&nbsp;* Voting procedures - who can vote, how are votes conducted, etc.
<br>
&nbsp;* Organizational structure - Identify those who will handle forum
<br>
logistics.
<br>
<p>If time permits:
<br>
&nbsp;* List of initial topics of interest. I will compile a list of initial
<br>
items, so please send topics of interest to mpi-21_at_[hidden] .
<br>
<p>Please feel free to comment on the agenda, and more important, please plan
<br>
to attend.
<br>
