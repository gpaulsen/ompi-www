<p>
Many of you are using the &quot;ompimtttester&quot; SVN account for read-only access the ompi-tests SVN repository for MTT testing.
<br>
<p>Short version
<br>
=============
<br>
<p>There was an accidental disclosure of this SVN account's password in a public forum today, so we had to reset the password.  If you represent a core Open MPI development organization that uses this account, please contact Mike Dubman &lt;miked_at_[hidden]&gt; for the new password.
<br>
<p>More detail
<br>
===========
<br>
<p>We password protect the ompi-tests SVN repo because it contains copies of publicly-available software for which we do not have redistribution rights.  As such, this password cannot be public knowledge -- it is restricted to core OMPI development organizations.  We therefore must take the responsible action and change the password immediately.  
<br>
<p>Sorry folks; your MTT runs may not be able to SVN update from the ompi-tests repo until you update your local copy of the password.
