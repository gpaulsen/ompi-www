Hi folks
<br>
<p>I believe I have previously mentioned the PMIx project - an effort to extend the PMI interface to support exascale operations. The project has been on the OMPI web site for a little while now, but I finally got around to populating it with a description of the planned features. Artem has agreed to take the lead in development, and I'll be working with him as my time permits. Other interested parties are, of course, welcome!
<br>
<p>The PMIx web page is here:
<br>
