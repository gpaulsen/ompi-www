<p>
Many thanks guys, this thread was most helpful in finding the fix.
<br>
<p>Paul H. nailed 80% of it on the head in the post where he identified the Makefile.am change.  That Makefile.am change was due to three things:
<br>
<p>1. Fixing a real bug (elsewhere in that commit)
<br>
2. My misunderstanding of how module files work in Fortran
<br>
3. The fact that gfortran, Absoft, and ifort *don't* require you to link in the .o files generated by modules, but apparently pgfortran *does*
<br>
<p>Blarg.
<br>
<p>That led to the duplicate symbol issue which Paul also encountered when he tried to fix the original problem, so I fixed that, too (which was a direct consequence of the first fix).
<br>
<p>Should be fixed in the trunk now; we tested with pgfortran on Craig Rasmussen's cluster (many thanks, Craig!).
<br>
<p>CMR is <a href="https://svn.open-mpi.org/trac/ompi/ticket/4519">https://svn.open-mpi.org/trac/ompi/ticket/4519</a>.
