<p>
It is that time again -- it's time to clean house of SVN write access accounts.
<br>
<p>SHORT VERSION
<br>
=============
<br>
<p>Edit the wiki to preserve your organization's SVN accounts by COB, Thursday, July 31, 2014:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://svn.open-mpi.org/trac/ompi/wiki/2014-SVN-summer-cleaning">https://svn.open-mpi.org/trac/ompi/wiki/2014-SVN-summer-cleaning</a>
<br>
<p>If you don't indicate which accounts are still in use by the deadline, THEY WILL BE DELETED.
<br>
<p>MORE DETAIL
<br>
===========
<br>
<p>Once a year, we prune non-active SVN accounts. For example:
<br>
<p>* People who are no longer involved in Open MPI
<br>
* People who no longer need write access to Open MPI's Subversion
<br>
* People who have moved organizations and no longer have that organization's permission to commit to Open MPI
<br>
<p>Each organization is responsible for editing this wiki page and moving each of their IDs to the &quot;keep SVN write access&quot; or &quot;remove SVN write access&quot; columns. There's also some unknown IDs (meaning: Jeff didn't recognize those IDs and therefore didn't know with which organization to group them). Please claim your organization's IDs.
<br>
<p>ALL ORGANIZATIONS MUST CLASSIFY THEIR COMMITTERS!
