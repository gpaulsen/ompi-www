Now that Open has migrated its repositories to GitHub, there is no point in maintaining the read-only Bitbucket and GitHub mirrors of OMPI's SVN repository any further.  They are, respectively:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://bitbucket.org/ompiteam/ompi-svn-mirror">https://bitbucket.org/ompiteam/ompi-svn-mirror</a>
