<p>
So it's again that time in a month or two that we receive an email
<br>
regarding the Java bindings for OpenMPI.
<br>
<p>I am currently working on message passing systems for Java and would like
<br>
to know if there is any published work since the resurrection of the
<br>
mpiJava and its integration into the OpenMPI stack or any open source
<br>
projects going on using the Java bindings for OpenMPI.
<br>
<p>So far I have found these works:
<br>
<p><p>*The JUNIPER Project &lt;<a href="http://www.juniper-project.org/">http://www.juniper-project.org/</a>&gt;*
<br>
Alexey Cheptsov and Bastian Koller (HLRS) entitled &quot;Message-Passing
<br>
Interface for Java Applications: Practical Aspects of Leveraging High
<br>
Performance Computing to Speed and Scale Up the Semantic Web&quot;.
<br>
<p>*Towards the availability of Java bindings in open MPI*
<br>
<p>Oscar Vega-Gisbert, Jose E. Roman, Siegmar Gro&#223;, and Jeffrey M. Squyres.
<br>
2013. Towards the availability of Java bindings in open MPI. In *Proceedings
<br>
of the 20th European MPI Users' Group Meeting* (EuroMPI '13). ACM, New
<br>
York, NY, USA, 141-142. DOI=10.1145/2488551.2488599
<br>
<a href="http://doi.acm.org/10.1145/2488551.2488599">http://doi.acm.org/10.1145/2488551.2488599</a>
<br>
<p><p>It would be great to know if I am missing any major work happening out
<br>
there in this realm.
