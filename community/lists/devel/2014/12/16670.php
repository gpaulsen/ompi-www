Hi Folks,
<br>
<p>Jeff and I have been considering changing the ABI compatibility store fory
<br>
Open MPI for the 1.9/2.0 release stream.  Basically no promises for the
<br>
odd/feature release series, but keep the current ABI promise for the
<br>
even release series.
<br>
<p>I've updated the 1.9/2.0 release page
<br>
&lt;<a href="https://github.com/open-mpi/ompi/wiki/Releasev19">https://github.com/open-mpi/ompi/wiki/Releasev19</a>&gt; on the wiki to include
<br>
this proposal.
<br>
<p>Please let us know if you think that it might be problematic to relax
<br>
the ABI compatibility promise in the features release series.
<br>
<p>This will be on the agenda for the developers' workshop next month.
<br>
<p>Thanks,
