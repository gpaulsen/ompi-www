<p>
Ralph Castain &lt;rhc_at_[hidden]&gt; writes:
<br>
<p><span class="quotelev1">&gt; I'm not a lawyer, but that agreement was formulated by the lawyers of
</span><br>
<span class="quotelev1">&gt; several national labs, universities, and corporations back at the very
</span><br>
<span class="quotelev1">&gt; beginning of the project, and so that's what we have to use.
</span><br>
<p>MPICH does the same thing, but the CLA grants permission to redistribute
<br>
under the terms of the Apache-2.0 license, yet the product is being
<br>
distributed under a license that is not compatible with Apache-2.0.
<br>
<p>Even more strongly, lots of BSD-style permissive software is
<br>
incorporated into GPLv2 and LGPLv2.1 distributions (this direction is
<br>
well-tested).  Meanwhile, Apache-2.0 is famously incompatible with GPLv2
<br>
and LGPLv2.1.
<br>
<p>&nbsp;&nbsp;&quot;Please note that this license is not compatible with GPL version 2,
<br>
&nbsp;&nbsp;because it has some requirements that are not in that GPL
<br>
&nbsp;&nbsp;version. These include certain patent termination and indemnification
<br>
&nbsp;&nbsp;provisions.&quot; -- <a href="https://www.gnu.org/licenses/license-list.html#apache2">https://www.gnu.org/licenses/license-list.html#apache2</a>
<br>
<p>License compatibility is a transitive property, so you certainly can't
<br>
distribute Apache-2.0 software under a BSD-style license (as shown in
<br>
the compatibility chart I posted).
<br>
<p><span class="quotelev1">&gt; According to the lawyers, it is indeed compatible. I'll let them argue
</span><br>
<span class="quotelev1">&gt; it :-)
</span><br>
<p>I'd be interested in hearing the legal argument for why the apparent
<br>
incompatibility is legally acceptable.  I'm not familiar with other
<br>
projects using this particular combination.
<br>
