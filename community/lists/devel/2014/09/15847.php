<p>
The ompi-tests SVN repo has been moved to Github.  The SVN repo is now in read-only mode.
<br>
<p>Just like the SVN ompi-tests repo, the Github ompi-tests repo is private.  You need to be an active organization in Open MPI to be able to access it.  You can use your developer Github ID or you can request the read-only account password from me (e.g., if you were having MTT use the &quot;ompi-tests&quot; account for SVN access).
<br>
<p>You are strongly encouraged to update your MTT to fetch from the new Github repo:
