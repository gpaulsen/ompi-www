<br>
<p><span class="quotelev1">&gt; Paul Hargrove &lt;phhargrove_at_[hidden]&gt; writes:
</span><br>
<span class="quotelev2">&gt; &gt; The GUIs for things like browsing commits, viewing diffs, etc are pretty
</span><br>
<span class="quotelev2">&gt; &gt; similar in capability and each is sufficiently intuitive (after a brief
</span><br>
<span class="quotelev2">&gt; &gt; learning curve) that I don't find I need any conscious effort to &quot;mode
</span><br>
<span class="quotelev2">&gt; &gt; switch&quot; between their use.  The ability to comment on commits in lieu of
</span><br>
<span class="quotelev2">&gt; &gt; the ticket system is a good feature, for instance, that &quot;just works&quot; in
</span><br>
<span class="quotelev2">&gt; &gt; both systems.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; One concern is that those commits appearing in the release repository
</span><br>
<span class="quotelev1">&gt; won't have the comments from the development repository.  So if you use
</span><br>
<span class="quotelev1">&gt; the comments and pull requests for debugging and discussion, you would
</span><br>
<span class="quotelev1">&gt; be better off with one repository so that you don't have to keep
</span><br>
<span class="quotelev1">&gt; switching repositories (in your browser) to access that metadata.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<p>Jed as a good point there (about comments and pull-request discussion
<br>
threads not appearing in the repo itself and therefore not moving with them
<br>
across repos).  On that point I agree there is a reason to be concerned.
<br>
So, I only consider use of those comments as a good alternative to use of
<br>
an email thread (which is not connected to either/any repo).  And, of
<br>
course, at some point neither commit/pull-request comments nor email are a
<br>
proper alternative to use of an issue tracker.
<br>
<p>I think it is worth noting that while the comment threads are not part of
<br>
the git repo, the commits (and thus their comments) do have persistent URLs
<br>
that can be useful in commit messages (especially CMR/pull-request merge
<br>
commits), the issue tracker and even in devel-list emails.
<br>
<p>Those points might be worthy of mentioning in the git-usage docs for OMPI.
