<p>
Question for the developer community:
<br>
<p>SHORT: Should I convert the private ompi-docs SVN to a private repo on Github?
<br>
<p>MORE DETAIL:
<br>
<p>The ompi-docs SVN is a private repo that was used heavily in the first several years of the project for collaborating to write papers.  In the past few years, I think it's mainly been used by George and me for the OMPI SC BOF, and that's about it.
<br>
<p>The Open MPI community on Github does have private repos (since ompi-test is now there).  Should I convert ompi-docs to Git and move it to a private repo on Github?
<br>
<p>My scripts are good enough that the conversion process is now fairly automated; it's not much work to convert and move ompi-docs to Git/Github.
<br>
<p>As such, I don't think I have a much of an opinion either way.  
<br>
<p>Thoughts?
