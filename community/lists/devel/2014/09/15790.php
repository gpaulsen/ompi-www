Developers --
<br>
<p>While a final timeline has not been set, please note that the Mercurial and Github mirrors of Open MPI's SVN repository are not long for this world.
<br>
<p>Once we switch over to Git/Github:
<br>
<p>1. It will not be possible to maintain the Bitbuck Mercurial mirror any more.  It will be retired shortly thereafter.
<br>
<p>2. Those who are using the current Github mirror (<a href="https://github.com/open-mpi/ompi-svn-mirror">https://github.com/open-mpi/ompi-svn-mirror</a>), note that you will likely have to start over with the new/official OMPI git repo: the history of the new ompi repo is likely to be completely different than that of the ompi-mirror.  Meaning: if you're a Git Sorcerer, you might be able to work magic to get your work from ompi-mirror to the new ompi repo.  But you should likely just be prepared to extract your non-upstream work from your ompi-mirror clones and rebase them on to the new ompi repo.
