<p>
Paul Hargrove &lt;phhargrove_at_[hidden]&gt; writes:
<br>
<span class="quotelev1">&gt; The GUIs for things like browsing commits, viewing diffs, etc are pretty
</span><br>
<span class="quotelev1">&gt; similar in capability and each is sufficiently intuitive (after a brief
</span><br>
<span class="quotelev1">&gt; learning curve) that I don't find I need any conscious effort to &quot;mode
</span><br>
<span class="quotelev1">&gt; switch&quot; between their use.  The ability to comment on commits in lieu of
</span><br>
<span class="quotelev1">&gt; the ticket system is a good feature, for instance, that &quot;just works&quot; in
</span><br>
<span class="quotelev1">&gt; both systems.  
</span><br>
<p>One concern is that those commits appearing in the release repository
<br>
won't have the comments from the development repository.  So if you use
<br>
the comments and pull requests for debugging and discussion, you would
<br>
be better off with one repository so that you don't have to keep
<br>
switching repositories (in your browser) to access that metadata.
<br>
<p><p>PETSc uses bitbucket mostly because it lets us unify our ACLs.  All our
<br>
source code is open, but we use private repositories for proposals and
<br>
(sometimes) papers.  We have *.edu addresses, so there is no charge.
<br>
<p>I'm a happy user of both github and bitbucket and think either will work
<br>
for your purposes.
