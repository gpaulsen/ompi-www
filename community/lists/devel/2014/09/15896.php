<br>
<p>More seriously, I'd suggest to use Semantic Versioning[1], which is
<br>
actually straight forward for both, developers and users. I'm quoting
<br>
the linked website:
<br>
<p><span class="quotelev1">&gt; Given a version number MAJOR.MINOR.PATCH, increment the:
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; 1. MAJOR version when you make incompatible API changes,
</span><br>
<span class="quotelev1">&gt; 2. MINOR version when you add functionality in a backwards-compatible
</span><br>
<span class="quotelev1">&gt;    manner, and
</span><br>
<span class="quotelev1">&gt; 3. PATCH version when you make backwards-compatible bug fixes.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Additional labels for pre-release and build metadata are available as
</span><br>
<span class="quotelev1">&gt; extensions to the MAJOR.MINOR.PATCH format.
</span><br>
<p>[1] <a href="http://semver.org/">http://semver.org/</a>
