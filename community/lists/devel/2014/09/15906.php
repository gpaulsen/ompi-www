<p>
At just about at the last minute, a new contender showed up: GerritHub.io.
<br>
<p>GerritHub claims to allow us to effectively have ACLs on branches.  I.e., everyone could commit on master, but only release managers can commit on release branches.  This would be nice, and would allow us to avoid having the 2 repos, like we're currently planning to do at Github (i.e., &quot;ompi&quot; and &quot;ompi-release&quot;).
<br>
<p>We need a little time to investigate this, and it seems prudent to postpone the transition tomorrow.
<br>
<p>We'll tentatively aim for *next* Wednesday, October 1, 2014.
