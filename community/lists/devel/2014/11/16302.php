Hi folks
<br>
<p>Those of you following the mailing lists probably know that we had hoped to release 1.8.4 last Friday, but were unable to do so. We currently have a couple of issues pending resolution, and our developers are badly &#226;&#128;&#156;crunched&#226;&#128;&#157; by final prep for Supercomputing. We then will hit the US Thanksgiving holiday, when most of our developers are out.
<br>
<p>I&#226;&#128;&#153;m afraid getting a release out at this time of year is always problematic for those reasons.
<br>
<p>So release of 1.8.4 has been delayed until sometime in Dec. For those of you needing something sooner, please feel free to download the latest 1.8.4 nightly tarball. It actually is running well as the remaining issues mostly pertain to configuration problems that are not frequently encountered, and it contains bug fixes that have been discussed on the mailing list over the last few months. You can find it here:
<br>
<p><a href="http://www.open-mpi.org/nightly/v1.8/">http://www.open-mpi.org/nightly/v1.8/</a>
<br>
<p>Our apologies for the delay
