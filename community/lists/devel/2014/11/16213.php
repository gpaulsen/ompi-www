SHORT VERSION
<br>
=============
<br>
<p>Will anyone be attending from Europe?
<br>
<p>This may influence the location of the meeting.
<br>
<p>MORE DETAIL
<br>
===========
<br>
<p>We're tentatively thinking that Dallas, TX would be a good location for the meeting (at the Cisco facility).  The rationale was as follows:
<br>
<p>1. Chicago is nice and central, but the weather in Jan/Feb can lead to travel problems
<br>
2. The San Francisco Bay area is also nice, but is not very central
<br>
3. Dallas was seen as a compromise:
<br>
&nbsp;&nbsp;&nbsp;- it's central
<br>
&nbsp;&nbsp;&nbsp;- it's likely a 1-flight for most US attendees
<br>
&nbsp;&nbsp;&nbsp;- it's less likely to have weather-related travel problems in Jan/Feb
<br>
<p>However, it was brought to my attention today that San Francisco (or Atlanta or New York?) may be more attractive to European attendees.  I.e., there's few direct flights from Europe to Dallas (if any?).
