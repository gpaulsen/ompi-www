<p>
Ralph and I were talking about the upcoming OMPI dev meeting yesterday.
<br>
<p>SHORT VERSION
<br>
=============
<br>
<p>Let's have a proper 2-3 day standalone face-to-face OMPI dev meeting, not coupled with the MPI Forum meeting.  Use the following doodle to pick what weeks are good for you:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://doodle.com/yremrx62e2dvddkf">http://doodle.com/yremrx62e2dvddkf</a>
<br>
<p>***NOTE*** The week of May 5 is a possibility, so please respond by COB tomorrow, Thursday, 10 Apr 2014.
<br>
<p>MORE DETAIL
<br>
===========
<br>
<p>Over the last few OMPI dev meetings, we've been trying to consolidate travel by piggybacking the OMPI dev meetings with MPI Forum meetings.  This usually means that we start the OMPI dev meeting at 2pm on a Thursday, and finish around noon the next day.  
<br>
<p>A few notes about this scheme:
<br>
<p>- The MPI Forum now only meets 4x a year (instead 6x a year)
<br>
- The MPI Forum now meets for 4 days (instead of 3)
<br>
- This allows for a 24-hour OMPI dev meeting (Thurs ~noon - Fri ~noon)
<br>
- As a result, we try to compress a zillion topics into 24 hours... with varying success
<br>
- By Thursday, we're all pretty brain-fried by Thursday from all the Forum discussions
<br>
<p>We therefore propose having a standalone, multi-day OMPI dev meeting.  This will let us focus on OMPI stuff, give each topic its proper amount of time, and allow us to not be brain-fried from Forum arguments all week.  It also makes it more worthwhile for European attendees (e.g., from Germany and Israel).
<br>
<p>The Big Topic for the upcoming meeting in thread safety.  I suggest having a 3 day meeting, and including some coding time (to work towards thread safety) in the agenda so that we can actually *do some work* to prototype/prove out some of the discussions we have in person.
<br>
<p>Let's pick a week (see the doodle above), and then iron our the specifics in that week (e.g., start Monday or Tuesday, go for 2 days or 3, etc.).
<br>
<p>*** The week of May 5 is a possibility, which is only a month away -- so please answer the Doodle by COB tomorrow, Thursday, April 10th, 2014.
