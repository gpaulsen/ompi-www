Effective immediately, the Open MPI web site repository (i.e., all the PHP that runs www.open-mpi.org) is now at github (it used to be in svn.open-mpi.org):
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://github.com/open-mpi/ompi-www">https://github.com/open-mpi/ompi-www</a>
<br>
<p>Read access to the old SVN repo is still available, but write access has been disabled.  The old SVN repo will disappear at some point (once we find/fix everything that breaks by moving to git/github).
<br>
<p>If you want/need write access to the web site, please let me know, and I can add you to the github repo access list.  Current users with write access are:
<br>
<p>- me
<br>
- Ralph Castain
<br>
- Brice Goglin
<br>
<p>If you push changes up to github, they'll become live on the production site within 15 minutes.
