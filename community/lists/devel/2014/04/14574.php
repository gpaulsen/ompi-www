<p>
Per <a href="http://www.open-mpi.org/community/lists/devel/2014/04/14572.php">http://www.open-mpi.org/community/lists/devel/2014/04/14572.php</a> and per discussion on the call this morning, 1.8.1rc1 has been posted:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.open-mpi.org/software/ompi/v1.8/">http://www.open-mpi.org/software/ompi/v1.8/</a>
<br>
<p>The only code change is to fix the critical fix regarding session directory behavior, and to disallow (by default) root from executing mpirun.
<br>
<p>This is a minimal-distance change from v1.8[.0].
<br>
<p>If people could do some sanity testing of this tarball, that would be great.
<br>
<p>If there are no problems (and I don't expect there to be), I plan to release 1.8.1 tomorrow.
