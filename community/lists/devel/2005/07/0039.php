FYI --
<br>
<p>Normally, you put in a .ompi_unignore with some user ID's in it so that 
<br>
a component is selectively unignored for people whose $USER matches any 
<br>
of the strings in the .ompi_unignore file (one per line -- a simple 
<br>
grep kind of match).
<br>
<p>If you want the whole world to not ignore a component, then just remove 
<br>
the .ompi_ignore file.
