</span><br>
<p>We have released v0.9 of the Portable Linux Processor Affinity (PLPA  
<br>
-- pronounced &quot;pli-pa&quot;) project, a standalone library that hides all  
<br>
the muckety-muck of processor affinity that we have been discussing  
<br>
on this list for the past few weeks.  This version does not pretend  
<br>
to be a stable release yet -- although it's quite small and we've  
<br>
done a bunch of testing with it, it's now time to open this project  
<br>
up to the community for more wide-spread testing and real-world  
<br>
feedback.
<br>
<p>The PLPA has its own web pages and mailing lists -- let's move all  
<br>
discussion over to those lists.  See the PLPA home page for more  
<br>
details:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.open-mpi.org/software/plpa/">http://www.open-mpi.org/software/plpa/</a>
