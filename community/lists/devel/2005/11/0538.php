<p>
I just added a NEWS file in both v1.0 and the trunk.  It's meant to be 
<br>
a list of user-noticable changes between versions.  We found in LAM 
<br>
that users look at it to see what's new between successive release 
<br>
versions.  It was also helpful for users searching for specific bug 
<br>
fixes (e.g., they found a problem and want to know if version x.y.z 
<br>
fixes it) or specific feature requests.
<br>
<p>So please add bullet points into NEWS as appropriate.
<br>
<p>For example, if you add a major new feature, add a bullet about it in 
<br>
the NEWS file.  If you fix a user-reported bug, add a bullet about it 
<br>
in the NEWS file (list the user who reported it too -- it's a nice way 
<br>
to say &quot;thanks!&quot;).  And so on.
