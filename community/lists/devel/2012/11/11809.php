<p>
Dear Open MPI Team,
<br>
<p>A day-long session (&quot;devroom&quot;) on Free/Libre and Open Source Software 
<br>
(FLOSS) for scientists will be held during the next FOSDEM conference, 
<br>
Brussels, 2-3 February 2013 (<a href="http://fosdem.org/2013">http://fosdem.org/2013</a>).
<br>
<p>We aim at having a dozen or two short talks introducing projects, 
<br>
advertising brand new features of established tools, discussing issues 
<br>
relevant to the development of software for scientific computing, and 
<br>
touching on the interdependence of FLOSS and open science.
<br>
<p>You can find more info on the call for talks at:
<br>
<a href="http://slayoo.github.com/fosdem2013/">http://slayoo.github.com/fosdem2013/</a>
<br>
<p>The deadline for sending talk proposals is December 16th 2012.
<br>
<p>Please send your submissions or comments to:
<br>
foss4scientists-devroom_at_[hidden]
<br>
<p>Please do forward this message to anyone potentially interested.  Please 
<br>
also let us know if you have any suggestions for what would you like to 
<br>
hear about in the devroom.
<br>
<p>Looking forward to meeting you in Brussels.
<br>
Thanks in advance.
<br>
<p>The conveners,
<br>
Sylwester Arabas, Juan Antonio A&#241;el, Christos Siopis
<br>
<p>P.S. There are open calls for main-track talks, lightning talks, and 
<br>
stands at FOSDEM as well, see: <a href="https://www.fosdem.org/2013/">https://www.fosdem.org/2013/</a>
<br>
