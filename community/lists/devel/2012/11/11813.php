On Nov 20 2012, Jeff Squyres wrote:
<br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; While at SC, Brian, Ralph, Nathan and I had long conversations about C99. 
</span><br>
<span class="quotelev1">&gt; We decided:
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; - all compilers that we care about seem to support C99 - so let's move 
</span><br>
<span class="quotelev1">&gt; the trunk and v1.7 to *require* C99 and see if anyone screams
</span><br>
<span class="quotelev1">&gt;  --&gt; we're NOT doing this in v1.6 - mind you: we're not (yet) adding any 
</span><br>
<span class="quotelev1">&gt; C99 code to OMPI yet; we're just requiring a C99 compiler via configure
</span><br>
<p>I very, VERY strongly advise you to decide what you mean by that.  Very
<br>
few compilers that 'support' C99 do so for the whole language, and most
<br>
use the more system-dependent features in very different ways.  In theory,
<br>
just enabling C99 could break code, and I know of a few ways that actually
<br>
occurs.  I doubt that they will affect Open MPI.
