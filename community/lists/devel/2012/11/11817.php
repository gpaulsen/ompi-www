On Nov 20 2012, Jeff Squyres wrote:
<br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev2">&gt;&gt; I very, VERY strongly advise you to decide what you mean by that. Very 
</span><br>
<span class="quotelev2">&gt;&gt; few compilers that 'support' C99 do so for the whole language, and most 
</span><br>
<span class="quotelev2">&gt;&gt; use the more system-dependent features in very different ways. In 
</span><br>
<span class="quotelev2">&gt;&gt; theory, just enabling C99 could break code, and I know of a few ways 
</span><br>
<span class="quotelev2">&gt;&gt; that actually occurs. I doubt that they will affect Open MPI.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Some Open MPI developers just want to use some of the C99 syntax, such as 
</span><br>
<span class="quotelev1">&gt; the struct field initialization syntax.
</span><br>
<p>Which is one of the safer features, provided that you use it cleanly.
<br>
My point is that it is important to decide WHICH features are allowed,
<br>
not least because WG14 have made some C99 features optional in C11,
<br>
and C++11 has adopted only some C99 features.  Nowadays, almost all
<br>
C compilers are actually tweaked C++ ones.
<br>
