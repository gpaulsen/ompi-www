<p>
Please verify this list of supported systems for the v1.5.5 release:
<br>
<p>- The run-time systems that are currently supported are:
<br>
&nbsp;&nbsp;- rsh / ssh
<br>
&nbsp;&nbsp;- LoadLeveler
<br>
&nbsp;&nbsp;- PBS Pro, Open PBS, Torque
<br>
&nbsp;&nbsp;- Platform LSF (v7.0.2 and later)
<br>
&nbsp;&nbsp;- SLURM
<br>
&nbsp;&nbsp;- Cray XT-3, XT-4, and XT-5
<br>
&nbsp;&nbsp;- Oracle Grid Engine (OGE) 6.1, 6.2 and open source Grid Engine
<br>
&nbsp;&nbsp;- Microsoft Windows CCP (Microsoft Windows server 2003 and 2008)
<br>
<p>- Systems that have been tested are:
<br>
&nbsp;&nbsp;- Linux (various flavors/distros), 32 bit, with gcc, and Oracle
<br>
&nbsp;&nbsp;&nbsp;&nbsp;Solaris Studio 12
<br>
&nbsp;&nbsp;- Linux (various flavors/distros), 64 bit (x86), with gcc, Absoft,
<br>
&nbsp;&nbsp;&nbsp;&nbsp;Intel, Portland, and Oracle Solaris Studio 12 compilers (*)
<br>
&nbsp;&nbsp;- OS X (10.5, 10.6, 10.7), 32 and 64 bit (x86_64), with gcc and
<br>
&nbsp;&nbsp;&nbsp;&nbsp;Absoft compilers (*)
<br>
&nbsp;&nbsp;- Oracle Solaris 10, 32 and 64 bit (SPARC, i386, x86_64), with
<br>
&nbsp;&nbsp;&nbsp;&nbsp;Oracle Solaris Studio 12
<br>
<p>&nbsp;&nbsp;(*) Be sure to read the Compiler Notes, below.
<br>
<p>- Other systems have been lightly (but not fully tested):
<br>
&nbsp;&nbsp;- Other 64 bit platforms (e.g., Linux on PPC64)
<br>
&nbsp;&nbsp;- Microsoft Windows CCP (Microsoft Windows server 2003 and 2008);
<br>
&nbsp;&nbsp;&nbsp;&nbsp;see the README.WINDOWS file.
