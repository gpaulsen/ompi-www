[Our apologies if you receive multiple copies or if you are not related to MPI]
<br>
<p>Dear MPI user,
<br>
<p>The MPI Forum is about to ratify the MPI-3.0, a new version of the MPI standard.
<br>
As part of this process, we are soliciting feedback on the current draft standard.
<br>
The draft document can be found here:
<br>
<p><a href="http://meetings.mpi-forum.org/mpi3.0_draft_2.pdf">http://meetings.mpi-forum.org/mpi3.0_draft_2.pdf</a> 
<br>
<p>We are seeking the following feedback in particular:
<br>
<p>- Small requests/corrections that can be resolved before finishing 3.0.
<br>
- Requests for clarification of unclear text or undefined behavior.
<br>
- Undetected severe inconsistencies or bugs that may delay the
<br>
&nbsp;&nbsp;standard publication.
<br>
- Wishes for additional enhancements or new functionality
<br>
&nbsp;&nbsp;to be taken into account after 3.0 is published.
<br>
<p>Please comment before Sep. 6, 2012 as follows:
<br>
<p>1. Subscribe to: <a href="http://lists.mpi-forum.org/mailman/listinfo.cgi/mpi-comments">http://lists.mpi-forum.org/mailman/listinfo.cgi/mpi-comments</a>
<br>
2. Then send your comments to: mpi-comments_at_[hidden] , 
<br>
&nbsp;&nbsp;&nbsp;together with the URL of the version of the MPI standard and 
<br>
&nbsp;&nbsp;&nbsp;the page and line numbers on which you are commenting. 
<br>
<p>Messages sent from an unsubscribed e-mail address will not be considered.
<br>
<p>Thank you in advance for your help.
<br>
<p>Best regards,
<br>
<p>The MPI Forum 
