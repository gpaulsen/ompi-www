<p>
It's that time of year again: it's time to trim down all the non-active SVN accounts.  We try to do this every July, although we might have accidentally skipped last July...?
<br>
<p>*** DEADLINE: COB, Thursday, July 26, 2012.
<br>
<p>The goal is to trim down the list of writeable SVN accounts to just those who are actually actively working in Open MPI.  Remember: it is trivial to add (or re-add) a writable SVN account.  Hence, it is better to err on the side of &quot;remove&quot;.
<br>
<p>All organizations are responsible for editing the wiki page below.  Moving all the the SVN IDs listed for your organization into the &quot;Keep&quot; or &quot;Remove&quot; columns.  Specific instructions are on the wiki page:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://svn.open-mpi.org/trac/ompi/wiki/2012-SVN-summer-cleaning">https://svn.open-mpi.org/trac/ompi/wiki/2012-SVN-summer-cleaning</a>
<br>
<p>There's a link right on the front wiki page, too.
<br>
<p>*** DEADLINE: All accounts that are not classified by COB, Thursday, July 26, 2012 will have their SVN write permissions removed.
