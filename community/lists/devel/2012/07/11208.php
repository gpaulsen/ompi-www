<p>
Hello everyone -
<br>
<p>This morning the branch for the 1.7 release series was created (and all
<br>
the other ancillary stuff that goes along with a branch also occurred).
<br>
The nightly tarballs are available at the expected location
<br>
(<a href="http://www.open-mpi.org/nightly/v1.7/">http://www.open-mpi.org/nightly/v1.7/</a>), with the first generated this
<br>
morning.
<br>
<p>If you run MTT, please consider adding the 1.7 series to your tests.  As a
<br>
reminder, with the 1.6 series released, the 1.4 and 1.5 series no longer
<br>
need to be tested on a regular basis.
<br>
<p>If you make a change that should go into 1.7, please remember to file a
<br>
CMR.  This is particularly true of changes which add MPI-2.2 or MPI-3.0
<br>
features to OMPI.
<br>
<p>Finally, a reminder that a number of &quot;unsupported&quot; components will be
<br>
removed from the 1.7 branch on Friday.  If you're concerned about a
<br>
component being removed, the full status list is available here:
<br>
<a href="https://svn.open-mpi.org/trac/ompi/wiki/Releasev17#Stalecodecheck">https://svn.open-mpi.org/trac/ompi/wiki/Releasev17#Stalecodecheck</a>
<br>
