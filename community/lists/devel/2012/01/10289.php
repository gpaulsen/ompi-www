Hot on the heels of rc3, rc4 is out:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.open-mpi.org/software/ompi/v1.4/">http://www.open-mpi.org/software/ompi/v1.4/</a>
<br>
<p>The main differences are the 8 issues that Paul Hargrove mentioned:
<br>
<p>Make v1.4 configure fail on OS X 10.3 and earlier
<br>
<p>Fix opal/util/path.c for Linux with old compilers
<br>
<p>README updates for the Sun compilers
<br>
<p>README updates noting systems that are no longer supported
<br>
<p>Fix assembly generation code on BSD in v1.4
