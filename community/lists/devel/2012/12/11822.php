<span class="quotelev1">&gt; BACKGROUND AND TOPICS 
</span><br>
<span class="quotelev1">&gt; ----------------------- 
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; EuroMPI is the preeminent meeting for users, developers and researchers to interact and discuss new developments and applications of message-passing parallel computing, in particular in and related to the Message Passing Interface (MPI). The annual meeting has a long, rich tradition, and the 20th European MPI Users' Group Meeting will again be a lively forum for discussion of everything related to usage and implementation of MPI and other parallel programming interfaces. Traditionally, the meeting has focused on the efficient implementation of aspects of MPI, typically on high-performance computing platforms, benchmarking and tools for MPI, short-comings and extensions of MPI, parallel I/O and fault tolerance, as well as parallel applications using MPI. The meeting is open towards other topics, in particular application experience and alternative interfaces for high-performance heterogeneous, hybrid, distributed memory systems. 
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; Topics of interest include, but are not limited to: 
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; - MPI implementation issues and improvements 
</span><br>
<span class="quotelev1">&gt; - Extensions to and shortcomings of MPI 
</span><br>
<span class="quotelev1">&gt; - Tools and environments for MPI 
</span><br>
<span class="quotelev1">&gt; - Hybrid and heterogeneous programming with MPI and other interfaces 
</span><br>
<span class="quotelev1">&gt; - Relation of MPI to alternative interfaces for hybrid/heterogeneous distributed memory systems 
</span><br>
<span class="quotelev1">&gt; - Interaction between message-passing software and hardware, in particular new high performance architectures 
</span><br>
<span class="quotelev1">&gt; - Fault tolerance in message-passing implementations and systems 
</span><br>
<span class="quotelev1">&gt; - Performance evaluation for MPI and MPI based applications- 
</span><br>
<span class="quotelev1">&gt; - Automatic performance tuning of MPI applications and implementations 
</span><br>
<span class="quotelev1">&gt; - Verification of message passing applications and protocols 
</span><br>
<span class="quotelev1">&gt; - Applications using message-passing, in particular in Computational Science and Scientific Computing 
</span><br>
<span class="quotelev1">&gt; - Non-standard message-passing applications 
</span><br>
<span class="quotelev1">&gt; - Parallel algorithms in the message-passing paradigm 
</span><br>
<span class="quotelev1">&gt; - Algorithms using the message-passing paradigm 
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; The meeting will feature contributed talks on the selected, peer-reviewed papers, invited expert talks covering upcoming and future issues, a vendor session where selected vendors will present their new developments in hybrid and heterogeneous cluster and high-performance architectures, a poster session, and a tutorial day. 
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; The scientific part of the conference is organized in cooperation with ACM SIGHPC. Conference proceedings will be published in the ACM Digital Library, which includes short and long papers, workshop papers, and posters. Selected high quality papers will be published in an international journals. There will also be a reward for the overall best paper from the academic conference. 
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; SUBMISSION INFORMATION 
</span><br>
<span class="quotelev1">&gt; ----------------------- 
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; Contributors are invited to submit a full paper as a PDF document not exceeding 6 pages in English. The title page should contain an abstract of at most 100 words and five specific, topical keywords. The paper must be formatted according to double-column ACM ICPS proceedings style. The usage of LaTeX for preparation of the contribution as well as the submission in camera ready format is strongly recommended. Style files can be found at <a href="http://www.acm.org/publications/icps-instructions/">http://www.acm.org/publications/icps-instructions/</a>. 
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; New work that is not yet mature for a full paper, short observations, and similar brief announcements are invited for the poster session. Contributions to the poster session should be submitted in the form of a two page abstract. 
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt; All contributions will be fully peer reviewed by the program committee. Papers shall be submitted electronically via Easychair, see <a href="http://www.easychair.org/conferences/?conf=eurompi2013">http://www.easychair.org/conferences/?conf=eurompi2013</a>. 
</span><br>
