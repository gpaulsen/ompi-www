<p>
A few times recently, we've forgotten about long-running RFC's (myself included!).
<br>
<p>Someone pointed out to me that it would be trivial to track RFC's in trac.  So I added the &quot;RFC&quot; ticket type (just like &quot;defect&quot;, &quot;CMR&quot;, &quot;enhancement&quot;).  This will make it easy to correlate outstanding RFCs, the code branch they're intended for, the timeout dates, etc.
<br>
<p>So when you send your next RFC, please enter it in trac, too.  Or perhaps just enter it in trac, and in the &quot;More information&quot; section of what you sent to devel, put a link to the RFC ticket.
