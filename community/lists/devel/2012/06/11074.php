On Jun 1, 2012, at 5:17 PM, George Bosilca wrote:
<br>
<p><span class="quotelev1">&gt; I'm not sure I follow what was &quot;get it right&quot;, but Tim's comment is still valid (look at the from field on the last set of 1.6 commits).
</span><br>
<p>Right.  Prior to this, commits were from &lt;svn_id&gt;@osl.iu.edu -- which was a fictitious set of email addresses.  There was no particular reason for it, so we changed it to be from svn-commit-mailer_at_osl.iu.edu.  
<br>
<p>I.e., if the mail is going to be from a fictitious address, let's have 1 fictitious address -- not N.
<br>
<p><span class="quotelev1">&gt; Moreover, these commits contains our real addresses in clear in the text. We all work hard to obfuscate them, I definitively prefer to keep them that way.
</span><br>
<p>I'm not sure I understand this point.  The mails are not archived on the web, and your email address is given out every time you send an email to the list.
<br>
<p>Is this any different?  
<br>
<p>If so, we can take it out -- it's no problem.  But it was intended as a mechanism so that you -- the email reviewer -- can see who the person is and see their email address, which sometimes helps identify someone's institution.  I thought this would be useful because sometimes I don't always recognize the SVN ID that comes by.
<br>
<p>If people hate this, we can always take it back out.  I thought it would be harmless from a spam perspective, and (potentially) useful from an email reviewer perspective.
