<p>
Hi all -
<br>
<p>As part of an effort to get 1.7 out the door quickly, we'd like to figure
<br>
out which components are essentially &quot;unmaintained&quot;.  With that in mind,
<br>
I've made a list of all components in the trunk.  Ralph and I would
<br>
appreciate it if everyone would &quot;claim&quot; the components their organization
<br>
will continue to maintain through the 1.7 series.  Keep in mind that if
<br>
you add your name to a component, you will be hounded to fix any major
<br>
bugs in the component which are delaying a release.  Components without
<br>
maintainers may be removed as part of the 1.7 release.
<br>
<p>The wiki page is here:
<br>
<p>&nbsp;&nbsp;<a href="https://svn.open-mpi.org/trac/ompi/wiki/Releasev17#Stalecodecheck">https://svn.open-mpi.org/trac/ompi/wiki/Releasev17#Stalecodecheck</a>
<br>
<p>Please update before the telecon next Tuesday.
