On Jun 26, 2012, at 9:28 AM, Josh Hursey wrote:
<br>
<p><span class="quotelev1">&gt; - ORCA: Open Runtime Collaborative Abstraction
</span><br>
<span class="quotelev1">&gt; - OCRA: Open Collaborative Runtime Abstraction
</span><br>
<span class="quotelev1">&gt; - OREI: Open Runtime Environment Interface
</span><br>
<span class="quotelev1">&gt; - ORSL: Open Runtime Services Layer
</span><br>
<span class="quotelev1">&gt; - ORRA: Open Runtime Resource Abstraction
</span><br>
<span class="quotelev1">&gt; - ORTA: Open Runtime API
</span><br>
<span class="quotelev1">&gt; - ORTI: Open Runtime Interface
</span><br>
<p><p>I don't think I have much of an opinion here, other than perhaps &quot;Open MPI&quot; instead of &quot;Open&quot; -- just to imply that this is tied to Open MPI, not an open-100%-free-for-all interface (I'm not saying that the M from MPI has to be in the acronym, though -- similar to how Ralph re-branded ORTE a while ago to be the &quot;Open MPI Run-Time Environment&quot; vs. the &quot;Open Run Time Environment&quot;).
<br>
<p>My only $0.02: ORTA and ORTI actually work kinda well, because they're only 1 letter different than ORTE, implying a heavy similarity.
