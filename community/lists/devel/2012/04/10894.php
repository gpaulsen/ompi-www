Hi folks
<br>
<p>On the weekly telecon, we settled on the dates for a developer's meeting to discuss the OMPI 1.7 release series. Because some of us will be at the MPI Forum meeting in Japan in June, and we are hitting the typical vacation season, the choice of dates was severely limited. Accordingly, we have decided to meet June 6-8 at Cisco in San Jose, with the meeting starting at 1pm on the 6th and ending at noon on the 8th to accommodate travel.
<br>
<p>I have setup a wiki page for the meeting:
<br>
<p><a href="https://svn.open-mpi.org/trac/ompi/wiki/June12Meeting">https://svn.open-mpi.org/trac/ompi/wiki/June12Meeting</a>
<br>
