<p>
Hi folks
<br>
<p>Just letting people know that we have decided to host another Open MPI Developer's meeting this year, tentatively scheduled to start at 9am (Pacific) on Thursday 12/6, ending around noon on Fri 12/7, in San Jose, CA. Topics to be discussed should be entered on the OMPI wiki here:
<br>
<p><a href="https://svn.open-mpi.org/trac/ompi/wiki/Dec12Meeting">https://svn.open-mpi.org/trac/ompi/wiki/Dec12Meeting</a>
<br>
<p>In addition, this year we will be giving a rolling &quot;workshop&quot; on the OMPI code design at Greenplum (just down the road in San Mateo) during that week. This will include:
<br>
<p>* Jeff Squyres - an overview of the OMPI code architecture
<br>
<p>* Brian Barrett - the MPI messaging stack
<br>
<p>* Edgar Gabriel - MPI I/O
<br>
<p>* Ralph Castain - ORTE state machine principles of operation, revised ORTE messaging stack
<br>
<p>Other talks may be added as the schedule evolves (Sam Guitterrez may give one on &quot;overlay networks&quot; - still TBA). The meeting wiki referenced above will include the full schedule of these talks, and anyone is welcome to attend (please sign-up on the wiki). Additionally, Greenplum has volunteered to record the talks and will post them on the Web for interested parties that are unable to attend.
<br>
<p>Ralph
