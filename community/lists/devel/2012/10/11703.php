<p>
*** IF YOU RUN MTT, YOU NEED TO READ THIS.
<br>
<p>Due to some server re-organization at Indiana University (read: our gracious hosting provider), we are moving the Open MPI community MTT database to a new server.  Instead of being found under www.open-mpi.org/mtt/, the OMPI MTT results will soon be located under mtt.open-mpi.org.
<br>
<p>Josh and I have been running tests on the new server and we think it is ready; it's now time to move the rest of the community to it.
<br>
<p>1. In order to make this change, we need some &quot;quiet time&quot; where no one is submitting new MTT results.  As such, we will be shutting down MTT/disallowing new MTT submissions over this upcoming weekend: from COB Friday, 2 Nov 2012 through Monday morning, 5 Nov 2012 (all times US Central).
<br>
<p>** Translation: don't expect submissions or queries to work after about 5pm on Friday through about 8am Monday (US Central).
<br>
<p>**** Super obvious translation: turn off your MTT runs this weekend.
<br>
<p>2. After this weekend, you will need to update your MTT submission URL from:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://www.open-mpi.org/mtt/submit/">https://www.open-mpi.org/mtt/submit/</a>
<br>
<p>to
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://mtt.open-mpi.org/submit/">https://mtt.open-mpi.org/submit/</a>
<br>
<p>Thanks!
