Hi Josh, hi everybody,
<br>
<p>we'd like to have VampirTrace enabled by default -- well, of course we do ;)
<br>
<p>Our main reason is the potential users: As soon as they want to use our tool 
<br>
they want to use it with the same configuration as the default Open MPI 
<br>
installation. Making the administrator re-install it at this point would be 
<br>
most inconvenient.
<br>
<p>If you're _not_ using our tools, you will _not_ see any difference at all 
<br>
while using the Open MPI installation or running your MPI program. 
<br>
<p>And regarding configuration and compilation of Open MPI itself, we try hard to 
<br>
make everything robust and smooth. 
<br>
<p>As there is no one insisting in disabled by default, we'd like to keep it 
<br>
enabled by default. I hope this is ok with everybody.
<br>
