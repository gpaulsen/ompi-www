<p>
All:
<br>
<p>We are now at the point where Ralph is ready to merge the changes he has
<br>
been making to ORTE into the trunk.  This is a major item for the
<br>
1.3release and its inclusion into the trunk will enable it to be
<br>
tested and
<br>
stabilized on a wider variety of environments.  To make this merge as smooth
<br>
as possible, I propose the following procedure and timing:
<br>
<p>&nbsp;&nbsp;Between now and the morning (EST) of the 28th (Thursday):
<br>
&nbsp;&nbsp;&nbsp;&nbsp;- hold off on major changes to the trunk, particularly those that would
<br>
change the existing directory structure.
<br>
&nbsp;&nbsp;&nbsp;&nbsp;- the trunk will not be locked, nor under gatekeeper control, so
<br>
modifications can still be committed.  But, please use judgement as to what
<br>
gets checked in between now and Thursday.
<br>
<p>&nbsp;&nbsp;Morning (EST) of the 28th (Thursday):
<br>
&nbsp;&nbsp;&nbsp;&nbsp;- Ralph will send out a note to the devel list to let people know when
<br>
he is starting his merge.  At this point, the trunk will become unstable
<br>
until his merge is finished and initial tests are run.
<br>
&nbsp;&nbsp;&nbsp;&nbsp;- During this time, please hold off on any modifications to the trunk.
<br>
&nbsp;&nbsp;&nbsp;&nbsp;- Ralph will send out a subsequent note when his merge is done, and the
<br>
trunk is once again available.  Hopefully this will be within 48 hours of
<br>
the start of the merge.
<br>
<p>I hope that this sequence will be acceptable to the development community.
<br>
If not, please surface the concerns here so that we can discuss them and
<br>
decide how best to proceed.
<br>
<p>Thanks,
<br>
