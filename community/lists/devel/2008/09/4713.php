The official HG OMPI mirror is back.
<br>
<p>We tried some pull's from the the new mirror with a tree that was  
<br>
cloned before the rebuild and had some limited success.  It *seemed*  
<br>
to pull down ok, but there was a complicated merge at the end that, to  
<br>
be blunt, I didn't have time to figure out.  :-\
<br>
<p>So I think that if you have a clone of the old HG OMPI tree, you might  
<br>
be able to continue if you can endure a slightly complicated merge.   
<br>
If not, your best bet is likely to create an &quot;hg diff&quot; of your tree  
<br>
and apply it to a new clone of the HG OMPI tree.
