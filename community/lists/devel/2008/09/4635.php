Hello Jeff, all,
<br>
<p>if you allow me my two cents,
<br>
<p>* Jeff Squyres wrote on Mon, Sep 08, 2008 at 08:44:28PM CEST:
<br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; At the MPI Forum meeting in Dublin, the MPI ABI meeting was... er...  
</span><br>
<span class="quotelev1">&gt; shall we say, &quot;spirited.&quot;  :-)  Both the benefits and drawbacks of an  
</span><br>
<span class="quotelev1">&gt; MPI ABI are widely contended (it's a surprisingly complex topic).
</span><br>
<p>it sounds quite daunting.
<br>
<p><span class="quotelev1">&gt; - If it is ever completed, MPI ABI compliance will be a separate entity 
</span><br>
<span class="quotelev1">&gt; from the MPI 2.x and 3.x standards.  ABI compliance will be a checkmark 
</span><br>
<span class="quotelev1">&gt; for an MPI implementation, but will be unrelated to an implementation's 
</span><br>
<span class="quotelev1">&gt; 2.1, 2.2, 3.0, ...etc. compliance.
</span><br>
<p>How can that be possible?   An MPI ABI will have to be versioned in
<br>
the same way that the API is versioned.  You can have an ABI version
<br>
for each API version though, I guess.
<br>
<p>And of course the MPI C++ ABI will require specifying a C++ ABI
<br>
(which, for Windows, means specifying the compiler and possibly its
<br>
major release number used), but this is venturing off into details.
