On May 15, 2008, at 2:37 PM, Caciano Machado wrote:
<br>
<p><span class="quotelev1">&gt; What are the meanings of the PML names OB1, DR and CM? The paper &quot;An  
</span><br>
<span class="quotelev1">&gt; evaluation of Open MPI's Matching Transport Layer on the Cray XT&quot;  
</span><br>
<span class="quotelev1">&gt; says that they don't have any special meaning, but here (<a href="http://www.open-mpi.org/papers/workshop-2006/wed_01_pt2pt.pdf">http://www.open-mpi.org/papers/workshop-2006/wed_01_pt2pt.pdf</a> 
</span><br>
<span class="quotelev1">&gt; ) I saw that DR stands for &quot;Data Reliability&quot;. It's correct?
</span><br>
<p><p>The names mostly reflect our horrid ability to name things.  :-)
<br>
<p>You are correct about &quot;DR&quot; -- but note that it was never finished.   
<br>
It's probably only pseudo-operable; it doesn't do everything that it  
<br>
was designed to do because the DR developers got other priorities and  
<br>
never finished it.
<br>
<p>OB1 = Obi-Wan Kenobi from Star Wars (we couldn't think of any good  
<br>
name at the time and really needed a name so that we could start  
<br>
coding...)
<br>
&nbsp;&nbsp;--&gt; similarly, the &quot;r2&quot; BML is R2-D2 from Star Wars
<br>
<p>CM = &quot;Connor MacLeod&quot; from The Highlander movie, a reference to the  
<br>
fact that &quot;there can only be 1&quot; ...MTL module in an MPI process (vs.  
<br>
OB1, which can have as many BTL modules as are relevant)
<br>
<p>There you go -- a little insight into the warped minds of those of us  
<br>
who bring you Open MPI.  Ok, I'm a little embarrassed now.  ;-)
