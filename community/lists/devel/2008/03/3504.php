<p>
LWN.net has an interesting article about how Emacs chose a new version
<br>
control system: &lt;<a href="http://lwn.net/Articles/272011/">http://lwn.net/Articles/272011/</a>&gt;
<br>
<p>They were back in the CVS stone ages, but their main contenders were
<br>
the same big three of distributed VCSs: git, hg and bzr.  The article
<br>
pulls out a couple of very good quotes from their discussion.  The one
<br>
that caught my eye was from Richard Stallman:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;We already know the most important thing about what we will find from
<br>
&nbsp;&nbsp;&nbsp;&nbsp;a careful study of git, mercurial and Bzr. We will find that each has
<br>
&nbsp;&nbsp;&nbsp;&nbsp;its advantages and disadvantages -- but none of them conclusive. Each
<br>
&nbsp;&nbsp;&nbsp;&nbsp;will be preferred by some people, but any one of them would work out
<br>
&nbsp;&nbsp;&nbsp;&nbsp;well enough. 
<br>
<p>&nbsp;- R.
