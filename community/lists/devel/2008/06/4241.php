On Jun 27, 2008, at 3:25 AM, Gnanasekar Loganathan wrote:
<br>
<p><span class="quotelev1">&gt; I want to run a MPI application on 4 Linux pc's.
</span><br>
<span class="quotelev1">&gt; I have the configuration details also.
</span><br>
<span class="quotelev1">&gt; My questions is, i need to install MPI on 4 linux pc's?
</span><br>
<span class="quotelev1">&gt; Whether i need to run the same MPI application on 4 linux pc's?
</span><br>
<p><p>Yes, you need to have MPI installed on all 4 machines, or have it  
<br>
available via a networked filesystem (such as NFS).  These FAQ  
<br>
categories may help some:
<br>
<p><a href="http://www.open-mpi.org/faq/?category=sysadmin">http://www.open-mpi.org/faq/?category=sysadmin</a>
<br>
<a href="http://www.open-mpi.org/faq/?category=building">http://www.open-mpi.org/faq/?category=building</a>
<br>
<p>It is certainly easiest if you run the same executable on all 4  
<br>
machines.
<br>
<p>If you're very new to MPI, you might want to take a tutorial to learn  
<br>
some of the basics.  If you scroll down in <a href="http://ci-tutor.ncsa.uiuc.edu/browse.php">http://ci-tutor.ncsa.uiuc.edu/browse.php</a> 
<br>
, you'll see an &quot;Introduction to MPI&quot; tutorial on that site  
<br>
(registration is free).
