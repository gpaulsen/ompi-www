<span class="quotelev1">&gt; The Hardware Locality (hwloc) team is pleased to announce the first
</span><br>
<span class="quotelev1">&gt; release candidate for v1.4.2:
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt;    <a href="http://www.open-mpi.org/projects/hwloc/">http://www.open-mpi.org/projects/hwloc/</a>
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; v1.4.2rc1 is the first milestone of a bug fix release. It brings
</span><br>
<span class="quotelev1">&gt; several important fixes.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; The following is a summary of the changes since v1.4.1:
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; * Fix build on Solaris 9 and earlier when fabsf() is not a compiler
</span><br>
<span class="quotelev1">&gt;   built-in. Thanks to Igor Gali&#196;&#135; for reporting the problem.
</span><br>
<span class="quotelev1">&gt; * Fix support for more than 32 processors on Windows. Thanks to Hartmut
</span><br>
<span class="quotelev1">&gt;   Kaiser for reporting the problem.
</span><br>
<span class="quotelev1">&gt; * Fix process-wide binding and cpulocation routines on Linux when some
</span><br>
<span class="quotelev1">&gt;   threads disappear in the meantime. Thanks to Vlad Roubtsov for reporting
</span><br>
<span class="quotelev1">&gt;   the issue.
</span><br>
<span class="quotelev1">&gt; * Make installed scripts executable. Thanks to Jirka Hladky for reporting
</span><br>
<span class="quotelev1">&gt;   the problem.
</span><br>
<span class="quotelev1">&gt; * Fix libtool revision management when building for Windows. This fix was
</span><br>
<span class="quotelev1">&gt;   also released as hwloc v1.4.1.1 Windows builds. Thanks to Hartmut Kaiser
</span><br>
<span class="quotelev1">&gt;   for reporting the problem.
</span><br>
<span class="quotelev1">&gt; * Add Port info attribute to network OS devices inside OpenFabrics PCI
</span><br>
<span class="quotelev1">&gt;   devices so as to identify which interface corresponds to which port.
</span><br>
<span class="quotelev1">&gt; * Document requirements for interoperability helpers: I/O devices discovery
</span><br>
<span class="quotelev1">&gt;   is required for some of them; the topology must match the current host
</span><br>
<span class="quotelev1">&gt;   for most of them.
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; Note that I haven't been able to post Windows builds yet but it will
</span><br>
<span class="quotelev1">&gt; happen in the near future.
