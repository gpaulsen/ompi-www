<p>
The Hardware Locality (hwloc) team is pleased to announce the release of v1.3:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.open-mpi.org/projects/hwloc/">http://www.open-mpi.org/projects/hwloc/</a> 
<br>
<p>v1.3 is a major new release series for hwloc.  It includes many new features
<br>
and changes over the v1.2.x series.
<br>
The recently-released v1.2.2 is expected to be the last release of its series
<br>
(see <a href="http://www.open-mpi.org/community/lists/hwloc-announce/2011/10/0024.php">http://www.open-mpi.org/community/lists/hwloc-announce/2011/10/0024.php</a>)
<br>
<p>The following is a summary of the changes since the v1.2 series:
<br>
<p>* Major features
<br>
&nbsp;&nbsp;+ Add I/O devices and bridges to the topology using the pciutils
<br>
&nbsp;&nbsp;&nbsp;&nbsp;library. Only enabled after setting the relevant flag with
<br>
&nbsp;&nbsp;&nbsp;&nbsp;hwloc_topology_set_flags() before hwloc_topology_load(). See the
<br>
&nbsp;&nbsp;&nbsp;&nbsp;I/O Devices section in the documentation for details.
<br>
* Discovery improvements
<br>
&nbsp;&nbsp;+ Add associativity to the cache attributes.
<br>
&nbsp;&nbsp;+ Add support for s390/z11 &quot;books&quot; on Linux.
<br>
&nbsp;&nbsp;+ Add the HWLOC_GROUPING_ACCURACY environment variable to relax
<br>
&nbsp;&nbsp;&nbsp;&nbsp;distance-based grouping constraints. See the Environment Variables
<br>
&nbsp;&nbsp;&nbsp;&nbsp;section in the documentation for details about grouping behavior
<br>
&nbsp;&nbsp;&nbsp;&nbsp;and configuration.
<br>
&nbsp;&nbsp;+ Allow user-given distance matrices to remove or replace those
<br>
&nbsp;&nbsp;&nbsp;&nbsp;discovered by the OS backend.
<br>
* XML improvements
<br>
&nbsp;&nbsp;+ XML is now always supported: a minimalistic custom import/export
<br>
&nbsp;&nbsp;&nbsp;&nbsp;code is used when libxml2 is not available. It is only guaranteed
<br>
&nbsp;&nbsp;&nbsp;&nbsp;to read XML files generated by hwloc.
<br>
&nbsp;&nbsp;+ hwloc_topology_export_xml() and export_xmlbuffer() now return an
<br>
&nbsp;&nbsp;&nbsp;&nbsp;integer.
<br>
&nbsp;&nbsp;+ Add hwloc_free_xmlbuffer() to free the buffer allocated by
<br>
&nbsp;&nbsp;&nbsp;&nbsp;hwloc_topology_export_xmlbuffer().
<br>
&nbsp;&nbsp;+ Hide XML topology error messages unless HWLOC_XML_VERBOSE=1.
<br>
* Minor API updates
<br>
&nbsp;&nbsp;+ Add hwloc_obj_add_info to customize object info attributes.
<br>
* Tools
<br>
&nbsp;&nbsp;+ lstopo now displays I/O devices by default. Several options are
<br>
&nbsp;&nbsp;&nbsp;&nbsp;added to configure the I/O discovery.
<br>
&nbsp;&nbsp;+ hwloc-calc and hwloc-bind now accept I/O devices as input.
<br>
&nbsp;&nbsp;+ Add --restrict option to hwloc-calc and hwloc-distribute.
<br>
&nbsp;&nbsp;+ Add --sep option to change the output field separator in hwloc-calc.
<br>
&nbsp;&nbsp;+ Add --whole-system option to hwloc-ps.
<br>
<p>The next release series (v1.4.x) is expected to focus on making multi-node
<br>
topology support easier to use.
<br>
