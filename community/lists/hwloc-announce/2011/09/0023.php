<p>
The Hardware Locality (hwloc) team is pleased to announce the first
<br>
release candidate of version 1.2.2
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.open-mpi.org/projects/hwloc/">http://www.open-mpi.org/projects/hwloc/</a>
<br>
<p>v1.2.2 is a bug fix release which addresses all known bugs in the
<br>
v1.2 serie. In the meantime, we are preparing the second release
<br>
candidate of v1.3 which brings new features.
<br>
<p>The following is a summary of the changes since v1.2.1:
<br>
<p>* Fix build on AIX 5.2, thanks Utpal Kumar Ray for the report.
<br>
* Fix XML import of very large page sizes or counts on 32bits platform,
<br>
&nbsp;&nbsp;thanks to Karsten Hopp for the RedHat ticket.
<br>
* Fix crash when administrator limitations such as Linux cgroup require
<br>
&nbsp;&nbsp;to restrict distance matrices. Thanks to Ake Sandgren for reporting the
<br>
&nbsp;&nbsp;problem.
<br>
* Fix the removal of objects such as AMD Magny-Cours dual-node sockets
<br>
&nbsp;&nbsp;in case of administrator restrictions.
<br>
* Improve error reporting and messages in case of wrong synthetic topology
<br>
&nbsp;&nbsp;description.
<br>
* Several other minor internal fixes and documentation improvements.
<br>
