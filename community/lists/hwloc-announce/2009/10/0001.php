<p>
It is unlikely that we'll normally announce release candidates on the  
<br>
&quot;announce&quot; list, but since this is the first potential release after  
<br>
being re-branded from &quot;libtopology&quot; to &quot;hwloc&quot;, this announcement  
<br>
warrants a little extra-wide dispersal.
<br>
<p>The hwloc team is pleased to announce that v0.9.1rc1 is available:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.open-mpi.org/software/hwloc/v0.9/">http://www.open-mpi.org/software/hwloc/v0.9/</a>
<br>
<p>This is not a final release, but it's darn close.  Most work has been  
<br>
in re-branding from &quot;libtopology&quot; to &quot;hwloc&quot;, but there are a few new  
<br>
features as well.  The documentation on the main hwloc website has  
<br>
been updated to 0.9.1rc1 (both PDF and HTML).
<br>
<p>Please test on all available platforms and let us know on the users or  
<br>
devel list if you run into any problems.
<br>
<p>Thanks!
