<p>
OPAL = Open Portability Access Layer.  It is compiled into libopen- 
<br>
pal.so (or .a) in the OMPI code base.
<br>
<p>It's basically our lowest later of portability and &quot;glue&quot; code in Open  
<br>
MPI -- our commodity stuff for linked lists, etc.  But also OS  
<br>
portability stuff: processor affinity, assembly code, event handling,  
<br>
etc.
