<p>
Unfortunately, our user docs are somewhat lacking.  :-\
<br>
<p>Our best docs are in the README file in the Open MPI tarball and on  
<br>
the FAQ on the web site.
<br>
<p>If you looking to learn about MPI programming, there are a number of  
<br>
good tutorials on the web; the one I usually recommend is the 2-part  
<br>
tutorial at NCSA (you have to sign up for a free account):
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://ci-tutor.ncsa.uiuc.edu/login.php">http://ci-tutor.ncsa.uiuc.edu/login.php</a>
<br>
<p>That teaches you about the standardized MPI API.  Then compiling and  
<br>
running your MPI applications with Open MPI is relatively  
<br>
straightforward -- the FAQ should hopefully cover most of your  
<br>
questions.  If not, please feel free to post to the users list.
<br>
