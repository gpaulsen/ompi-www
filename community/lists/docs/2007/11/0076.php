<font face="Lucida">I'd like to recommend using OpenOffice.org (OOo) as
an authoring environment.<br>
<br>
For one thing, there are many output options, including open document
text format (.odt) as well as DocBook xml, html, and pdf.<br>
<br>
And, I believe there are already a number of usable book/article
templates available that we can use.<br>
<br>
My concern is that we come up with an agreeable authoring environment
as soon as possible so we can get started developing content.<br>
<br>
If we generate source in .xml, we can convert to PDF and html using OOo.<br>
<br>
Seems like a quick way to get started.<br>
<br>
How does that sound?<br>
<br>
You can download free distros of OOo from the openoffice.org site. I've
been experimenting with the download for Solaris x86 on my laptop and
it seems to work just fine. <br>
<br>
Comments? Opinions?<br>
