<font face="Lucida">Sorry for the lack of postings recently. <br>
<br>
But I have had the opportunity to look at the documentation support for
some of the other MPI distributions. In particular, the LAM/MPI
documentation seems the most interesting so far.<br>
<br>
<a class="moz-txt-link-freetext" href="http://www.lam-mpi.org/using/docs/">http://www.lam-mpi.org/using/docs/</a><br>
<br>
They provide a User Guide
<a class="moz-txt-link-freetext" href="http://www.lam-mpi.org/download/files/7.1.4-user.pdf">http://www.lam-mpi.org/download/files/7.1.4-user.pdf</a><br>
an Installation Guide&nbsp;
<a class="moz-txt-link-freetext" href="http://www.lam-mpi.org/download/files/7.1.4-install.pdf">http://www.lam-mpi.org/download/files/7.1.4-install.pdf</a><br>
<br>
and a number of internal API guides.<br>
<br>
The User Guide chapters include <br>
</font>
<ul>
  <li>Release Notes</li>
  <li>Getting Started</li>
  <li>Supported MPI Functionality</li>
  <li>System Services Interface Overview (is there an Open MPI
equivalent?)</li>
  <li>Command-line Quick Reference <br>
  </li>
  <li>Information about internal modules <br>
  </li>
  <li>Debugging with TotalView and other debuggers</li>
  <li>Troubleshooting</li>
  <li>Misc details</li>
</ul>
The whole thing, in pdf is about 120 pages. <br>
<br>
How close does this outline fit into Open MPI? How much internal
details would be useful to users?<br>
<br>
They also have some LAM/MPI specific tutorials at<br>
<a class="moz-txt-link-freetext" href="http://www.lam-mpi.org/tutorials/">http://www.lam-mpi.org/tutorials/</a><br>
<br>
All of this looks like an interesting model to follow. <br>
Comments?<br>
