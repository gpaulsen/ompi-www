<p>
The Indiana University IP lawyers have proposed that contributions to  
<br>
Open MPI documentation follow the same procedure as contributing  
<br>
code: sign and submit an Open MPI 3rd Party Contribution form, found  
<br>
here:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.open-mpi.org/community/contribute/">http://www.open-mpi.org/community/contribute/</a>
<br>
<p>It's technically a little overkill (from my not-legally-trained  
<br>
understanding), but they thought it might be easier than setting up a  
<br>
new/different procedure just for contributing documentation.   
<br>
Specifically, it keeps one agreement as the overall umbrella for  
<br>
contributing to *all* Open MPI efforts (Open MPI code, Open MPI  
<br>
documentation, the sub-projects, etc.).
<br>
<p>For example, Sun has already submitted this form so to enable Richard  
<br>
Friedman to contribute, they would simply need to amend their  
<br>
schedule A.  Other organizations / individuals who wish to contribute  
<br>
who have not already submitted the form will need to do so.
