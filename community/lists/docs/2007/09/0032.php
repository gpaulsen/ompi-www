<p>
Hello,
<br>
<p>I was wondering if the Open MPI docs could have a structure similar to
<br>
the NetBeans Community Docs
<br>
(<a href="http://wiki.netbeans.org/wiki/view/CommunityDocs">http://wiki.netbeans.org/wiki/view/CommunityDocs</a>)
<br>
<p>My reasons for feeling the same:
<br>
<p>1. Wiki style editing makes publishing docs easy
<br>
2. We could also make provision for easily printable formats like PDF
<br>
<p>If such a structure is not capable of being &quot;standalone&quot;, then may be
<br>
we can have this supplementing the actual Open MPI docs project.
<br>
<p>Also, as far as the managing is concerned, we could have a Community
<br>
Manager who will be in charge of making sure that we have quality docs
<br>
on the wiki. To assist the manager, we can have co-ordinators who will
<br>
play the role of spreading a word and getting more contributions for
<br>
the Open MPI docs.
<br>
<p>I would like to have some comments on the acceptability of my views.
<br>
<p>Thanks,
<br>
