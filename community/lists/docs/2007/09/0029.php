<font face="Lucida Grande">As I said in an earlier email today, I'm new
to this community and I'm not fully aware of its dynamics, how it
works, who the people are, and what to expect. But, I'm in the door.
This all started a few weeks ago when I told my manager that I needed
to find out more about our (Sun's) ClusterTools, which is based on Open
MPI, because it is an important part of Sun's HPC software stack. This
led me to open-mpi.org, looking for the documentation. The ClusterTools
documentation only describes how to use the ClusterTools package, not
how to program with Open MPI.&nbsp; Which is appropriate.<br>
<br>
But Sun is committed to working with open source communities, and so my
manager gave me the green light to go ahead and see if a docs project
could be started. <br>
<br>
I'm glad to see that a number of people are out there interested in
doing something in this area. The devil's in the details, but so's the
fun. <br>
<br>
We may be a bit slow in getting things to the point where we're writing
and putting back content, but we're going to get there. First, lets set
up a working framework so things go smoothly.&nbsp; <br>
<br>
But here's a question we might want to discuss among ourselves over
email in the meantime:<br>
I'm curious to find out how you (or programmers you know who are using
MPI in some form or another) learn about MPI. Where do you/they go to
develop these skills? What books/articles/courses do people use?&nbsp;
Programmers are not born with innate knowledge of MPI. So where to they
attain those skills.&nbsp; Thoughts?<br>
