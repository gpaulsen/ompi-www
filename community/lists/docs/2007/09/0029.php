  <ul>
    <li><font face="Lucida Grande">reference manual</font></li>
    <li><font face="Lucida Grande">user guide</font></li>
    <li><font face="Lucida Grande">man pages</font></li>
    <li><font face="Lucida Grande">tutorials</font></li>
    <li><font face="Lucida Grande">release notes</font></li>
    <li><font face="Lucida Grande">code examples</font></li>
  </ul>
  <li><font face="Lucida Grande">Where do we stand with regard to the
"official" MPI documents from mpi-forum.org? <br>
    </font></li>
  <ul>
    <li><font face="Lucida Grande">Can we cut/paste and edit content
from the 2.x reports into an Open MPI reference? Or do we have to worry
about copyrighted content?&nbsp; Who "owns" the content currently?<br>
      </font></li>
    <li><font face="Lucida Grande">How do we keep in sync with the
"official" MPI documents.</font></li>
    <li><font face="Lucida Grande">Is there a chance that Open MPI will
      <i>become</i> the official MPI? (Or am I being too naive?)</font></li>
  </ul>
  <li><font face="Lucida Grande">What are the deltas between "official"
MPI 2.x and Open MPI? And how do we document this?</font></li>
