<font face="Lucida Grande">Well, here's some status, just so we can
keep everyone involved.<br>
<br>
Jeff is working to create an infrastructure of a docs project within
the open-mpi.org. We should have a formal project established real soon.<br>
<br>
Meantime, I'm doing some research to find out what are the right tools
to consider for collaborative docs development. And tomorrow (Thurs)
I'm meeting with one of our experts on DITA (Darwin Information Typing
Architecture) at Sun. There are a number of free, open source, tools
for authoring, building, maintaining, and publishing various kinds of
documents collaboratively. There's DITA, docbook, and maybe more. (I'm
pretty sure we don't want to use TeX)<br>
<br>
Personally, I haven't kept up with recent developments so I'm giving
myself a crash course in all this, and it's quite a lot to take in. I
should have something to say about tools next week. <br>
<br>
The next steps might be to figure out rules of engagement, what needs
to be done, organize teams around tasks, and set up some kind of
milestones. The goal is to provide a complete (or, as complete as
possible) set of user documentation for Open MPI that is accurate,
manageable, extensible, yadda, yadda. <br>
<br>
Lots of questions rush immediately to mind, like:<br>
</font>
