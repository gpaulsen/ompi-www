<font face="Lucida">Here's something to look at over the weekend.<br>
<br>
I talked to one of the people here at Sun who coordinates the open
source documentation project at Open Solaris, and she pointed me at a
set of pages on opensolaris.org that outline the tools and procedures
they use for collaborative editing and authoring of documentation:<br>
<br>
<a class="moz-txt-link-freetext" href="http://opensolaris.org/os/community/documentation/doc_collab/tools/">http://opensolaris.org/os/community/documentation/doc_collab/tools/</a><br>
<br>
Keep in mind that the Open Solaris docs project is much bigger than our
project by many orders of magnitude and include thousands of pages of
original docs from Sun that are now open sourced, including man pages.<br>
<br>
As I currently understand it, the Open MPI docs project will be doing
mostly new docs that we author.<br>
<br>
Open Solaris is using a version of Sun's scaled down version of
Docbook, called Solbook. The pages pointed to above also give
comparisons between various tools that collaborators are free to use.<br>
<br>
There is a lot here. And not all of it would apply. But this gives a
good idea of how a very large collaborative effort is working. E.g. I
found out that a group in Poland is taking the ZFS documentation (in
XML) and translating it to Polish.<br>
<br>
Have a great weekend.<br>
