On Sep 20, 2007, at 1:08 AM, Amit Kumar Saha wrote:
<br>
<p><span class="quotelev1">&gt; I was wondering if the Open MPI docs could have a structure similar to
</span><br>
<span class="quotelev1">&gt; the NetBeans Community Docs
</span><br>
<span class="quotelev1">&gt; (<a href="http://wiki.netbeans.org/wiki/view/CommunityDocs">http://wiki.netbeans.org/wiki/view/CommunityDocs</a>)
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; My reasons for feeling the same:
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; 1. Wiki style editing makes publishing docs easy
</span><br>
<span class="quotelev1">&gt; 2. We could also make provision for easily printable formats like PDF
</span><br>
<span class="quotelev1">&gt;
</span><br>
<span class="quotelev1">&gt; If such a structure is not capable of being &quot;standalone&quot;, then may be
</span><br>
<span class="quotelev1">&gt; we can have this supplementing the actual Open MPI docs project.
</span><br>
<p>Richard has some excellent thoughts about document formatting -- as  
<br>
he mentioned in his e-mail, he leans towards DITA stuff (which I'm  
<br>
only barely familiar with).  He sent me some URL's for reference on  
<br>
this stuff before, and I've asked him to re-send them to the list.
<br>
<p>The Big Issue with DITA is that it allows us a lot of flexibility --  
<br>
more so than a wiki:
<br>
<p>- composition of the document -- mixing and matching sections/ 
<br>
chapters/etc.
<br>
- allowing 3rd party / sub-Open MPI distributors to insert / remove  
<br>
selected content while staying up to date with the main document  
<br>
(e.g., Cisco distributes Open MPI, but we have our own text that we  
<br>
add in for documentation)
<br>
- ...a bunch of other nice properties that Richard can expound upon :-)
<br>
<p><span class="quotelev1">&gt; Also, as far as the managing is concerned, we could have a Community
</span><br>
<span class="quotelev1">&gt; Manager who will be in charge of making sure that we have quality docs
</span><br>
<span class="quotelev1">&gt; on the wiki. To assist the manager, we can have co-ordinators who will
</span><br>
<span class="quotelev1">&gt; play the role of spreading a word and getting more contributions for
</span><br>
<span class="quotelev1">&gt; the Open MPI docs.
</span><br>
<p>This is probably also a good idea -- having some kind of coordination  
<br>
mesiter who ties it all together.  We have the same concept on the  
<br>
code side (release managers for a given release series).
