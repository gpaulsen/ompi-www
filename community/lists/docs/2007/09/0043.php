Wikis are good for most kinds of quick and simple docs. And the
documentation pages on netbeans.org is a good model to follow.<br>
<br>
But I think first we need to decide what kind of docs we're going to be
creating from within the docs project. We could also create a
"community wiki" along the lines of netbeans.org 's community docs
pages that permit easy publishing (moderated) from anywhere by anyone.<br>
<br>
At the moment, I'm not clear about what kinds of docs fall within the
scope of the Open MPI project. I&nbsp; (mistakenly) thought that a reference
to the MPI API would be central. (I'm still not clear about the
relationships between the MPI Forum and the Open MPI project. Maybe
Jeff and I need to have a phone call sometime next week.&nbsp; I'm really a
newbie here.)<br>
<br>
So before we can talk about how the docs will be published/distributed,
and the tools associated with producing them, we need to determine just
what kinds of user documentation this group will be producing, at least
for this initial start-up phase.<br>
<br>
Then we can talk about tools.<br>
<br>
(As I mentioned earlier, DITA might not be a good solution, at least
for now. What I'm hearing is that the features for reuse of content are
great, but the overhead to produce content is high, and that the
available free and open source tools are not of production quality,
currently. That may take a few years. So I'm going to talk to the Sun
people involved with OpenSolaris documentation. We also have people
involved with NetBeans docs, so I'll be talking to them as well. Stay
tuned.)<br>
<br>
So, can we start a discussion about what kinds of user docs would be
essential for a "Phase One" Open MPI project?<br>
<br>
