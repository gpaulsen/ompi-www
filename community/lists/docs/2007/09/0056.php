<p>
I've been working on getting &quot;similar but different&quot; logos for each  
<br>
of the OMPI sub-projects recently.  The staff artist at U. Tennessee  
<br>
(who made the original OMPI logo) has been quite helpful in making  
<br>
new logos for us.  Since user docs is turning into an official sub- 
<br>
project, I think we need a logo for it; this logo would be put in the  
<br>
top-left corner of <a href="http://www.open-mpi.org/projects/user-docs/">http://www.open-mpi.org/projects/user-docs/</a> and  
<br>
the trac/wiki pages.
<br>
<p>The UT artist made some logos for the PLPA sub-project but I didn't  
<br>
like them because (to me) they look Mac-like and PLPA is specific to  
<br>
Linux.  I think my reaction was based on the rounded corners of the  
<br>
squares, or perhaps the shade of green of the circle that looks like  
<br>
the &quot;maximize&quot; button on Mac windows.
<br>
<p>But what do you think of either of these for the user docs sub- 
<br>
project?  (ignore the &quot;PLPA&quot; at the bottom of them; that's easily  
<br>
changed)
