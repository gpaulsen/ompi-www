<p>
Hello,
<br>
<p>I have briefly spoken with Jeff Squyres and Joshua Hersey about the Open
<br>
MPI documentation project. Based on feedback from them a rough draft of an
<br>
outline has been created.  Open MPI is a great resource, and it can benefit
<br>
from a central resource that describes how to use it. There is a lot of
<br>
information out there, but it can be difficult to find.
<br>
<p>I will attach a copy of the current document here. It is quite rough, and
<br>
the important thing at this point is the outline that can be found at the
<br>
beginning of the document. In terms of the big picture the opening chapter
<br>
should contain a broad overview but should also offer a quick start guide
<br>
that gives a basic introduction on how to compile and run a program as well
<br>
as perform some very basic tasks.
<br>
<p>The thinking on the other chapters is that they will be more focused and
<br>
provide documentation that  ranges from basic, intermediate and advanced
<br>
usage of the Open MPI library.
<br>
<p>The files are written in LaTeX, and they can be found at
<br>
<a href="https://github.com/open-mpi/docs">https://github.com/open-mpi/docs</a> The document that is attached here was
<br>
made from the development branch. At this point there is not much in the
<br>
master branch.
<br>
<p>Again, the document is rough and needs more thought. Insights and ideas at
<br>
this point are welcome.
<br>
