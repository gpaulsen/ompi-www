<span class="quotelev3">&gt; &gt; &gt;
</span><br>
<span class="quotelev3">&gt; &gt; &gt; The developer community had a few suggestions:
</span><br>
<span class="quotelev3">&gt; &gt; &gt;
</span><br>
<span class="quotelev3">&gt; &gt; &gt; 1. Affirmation of focusing on user-level documentation of Open
</span><br>
<span class="quotelev1">&gt; MPI-specific issues.  E.g., here's how OMPI's wrapper compilers work;
</span><br>
<span class="quotelev1">&gt; here's how to use --showme, here's how to use OMPI's mpirun, here's how to
</span><br>
<span class="quotelev1">&gt; set OMPI MCA params, ...etc.  A short MPI programming tutorial would be ok,
</span><br>
<span class="quotelev1">&gt; but keep it short/high level, and use it as a gateway to explaining the
</span><br>
<span class="quotelev1">&gt; OMPI tools surrounding an MPI source code (because there's books and lots
</span><br>
<span class="quotelev1">&gt; of google material on writing MPI code).
</span><br>
<span class="quotelev3">&gt; &gt; &gt;
</span><br>
<span class="quotelev3">&gt; &gt; &gt; 2. Additionally, try to make docs that can be fairly stable over time:
</span><br>
<span class="quotelev1">&gt; stuff that won't need to be updated frequently, because documentation tends
</span><br>
<span class="quotelev1">&gt; to suffer from bit rot.  So if we can intentionally write fairly stable
</span><br>
<span class="quotelev1">&gt; docs to start with, they'll have a longer shelf life.
</span><br>
<span class="quotelev3">&gt; &gt; &gt;
</span><br>
<span class="quotelev3">&gt; &gt; &gt; Meaning: don't document every OMPI detail.  Rather, (at least to start
</span><br>
<span class="quotelev1">&gt; with) document all the high-level OMPI concepts that haven't changed in a
</span><br>
<span class="quotelev1">&gt; long time: ./configure &amp;&amp; make -j 32 install, wrapper compilers, mpirun,
</span><br>
<span class="quotelev1">&gt; MCA params, etc.
</span><br>
<span class="quotelev3">&gt; &gt; &gt;
</span><br>
<span class="quotelev3">&gt; &gt; &gt; 3. The community suggested that PDF might not be a good primary format
</span><br>
