<p>
I think you're using the wrong email list -- you probably want to send email to the Open MPI user's list, not the documentation list.
<br>
<p>You should also send more information about your cluster (e.g., processors, OS, RAM, interconnect, etc.).
<br>
<p>You should be able to do some back-of-the-envelope kinds of computations to see what kind of performance you *should* be getting on your cluster (given processor speed, RAM and problem size, etc.), and then compare that to what you *are* getting.  If they're wildly different, you probably need to tune.
