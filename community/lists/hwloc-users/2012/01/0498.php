<p>
Hello,
<br>
<p>I am attempting to compile hwloc into a static library so that it may be integrated into a larger project without having to ship the shared library with the release.  The project is cross-platform, and so needs to support Windows 32/64-bit and Linux.
<br>
<p>First of all, is Windows 64-bit supported? There is only a 32-bit release on the downloads page.
<br>
<p>I have looked at this page:  <a href="http://www.open-mpi.org/projects/hwloc/doc/v1.3.1/a00009.php">http://www.open-mpi.org/projects/hwloc/doc/v1.3.1/a00009.php</a>
<br>
However, when I specify the --enable-embedded-mode flag in configure in Linux, no libraries are built at all - the specified prefix directory contains only empty directories.
<br>
<p>I've managed to compile a working static library on Linux using the headers generated by configure, but am having a lot of difficulty doing the same on Windows - the Visual Studio compiler runs into a lot of issues. Is there a simple way to do this?
