<p>Hello
<br>
<p>The L3 bug only applies to 12-core Opteron 62xx/63xx, while you have
<br>
16-core Opterons. Your L3 locality is correct, but your NUMA locality is
<br>
wrong:
<br>
$ cat sys/devices/system/node/node*/cpumap             
<br>
00000000,00ffffff
<br>
0000ff00,ff000000
<br>
000000ff,00000000
<br>
ffff0000,00000000
<br>
You should have something like this instead:
<br>
00000000,0000ffff
<br>
00000000,ffff0000
<br>
0000ffff,00000000
<br>
ffff0000,00000000
<br>
<p>This bug is not harmless since memory buffers have a good chance of
<br>
being physically allocated far away from your cores.
<br>
<p>This is more likely a BIOS bug. Try upgrading.
<br>
<p>Regards
<br>
Brice
