<br>
<p>&#226;&#128;&#156;
<br>
hwloc 0.9.1 has been released today.
<br>
<p>hwloc provides a portable abstraction (across OS, versions,
<br>
architectures, ...) of the hierarchical topology of modern
<br>
architectures. It primarily aims at helping high-performance computing
<br>
applications with gathering information about the hardware so as to
<br>
exploit it accordingly and efficiently.
<br>
<p>hwloc provides a hierarchical view of the machine, NUMA memory nodes,
<br>
sockets, shared caches, cores and simultaneous multithreading. It also
<br>
gathers various attributes such as cache and memory information.
<br>
<p>hwloc supports the following operating systems:
<br>
<p>&nbsp;&nbsp;* Linux (including old kernels not having sysfs topology information, with
<br>
&nbsp;&nbsp;&nbsp;&nbsp;knowledge of cpusets, offline cpus, and Kerrighed support)              
<br>
&nbsp;&nbsp;* Solaris
<br>
&nbsp;&nbsp;* AIX
<br>
&nbsp;&nbsp;* Darwin
<br>
&nbsp;&nbsp;* OSF/1 (aka. Tru64)
<br>
&nbsp;&nbsp;* HP-UX
<br>
&nbsp;&nbsp;* Windows
<br>
&nbsp;&nbsp;* For other OSes, only the number of processors is available for now.
<br>
<p>hwloc can be download from
<br>
<a href="http://www.open-mpi.org/projects/hwloc/">http://www.open-mpi.org/projects/hwloc/</a>
<br>
<p>hwloc is available under the BSD license.
