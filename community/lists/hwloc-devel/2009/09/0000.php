Hello,
<br>
<p>3 important things to note:
<br>
<p>1) libtopology is being renamed into &quot;Hardware Locality&quot; (hwloc)
<br>
<p>This solves the conflict with the other libtopology project even if it
<br>
hasn't been active for almost year.
<br>
<p>2) At the same time, we are moving the project from INRIA Gforge to the
<br>
Open MPI server.
<br>
<p>SVN is now accessible from <a href="https://svn.open-mpi.org/svn/hwloc/trunk">https://svn.open-mpi.org/svn/hwloc/trunk</a>
<br>
I removed commit rights to everybody on Gforge (except a couple admins
<br>
for now) so that you don't commit there by mistake.
<br>
<p>The mailing lists are now hwloc-{devel,svn,bugs}@openmpi.org
<br>
I will be moving all subscribed people to the new lists soon.
<br>
<p>We'll try to get nice redirections from everything on Gforge as soon as
<br>
possible.
<br>
<p>3) By the way, we are also changing the licence from CeCILL-B to BSD so
<br>
that there is no advertising clause anymore.
<br>
<p><p>Note that the project/website/documentation do not reflect all these
<br>
changes yet, but at least the SVN and lists should be ready to work now.
<br>
