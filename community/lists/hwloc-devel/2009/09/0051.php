<p>
Gah; sorry, I did it again.  When you &quot;svn up&quot;, you'll get this:
<br>
<p>svn: Failed to add file 'doc/www.open-mpi.org.cfg': an unversioned  
<br>
file of the same name already exists
<br>
<p>Just rm doc/www.open-mpi.org.cfg and &quot;svn up&quot; again, and it'll be fine.
<br>
<p>======
<br>
<p>If you care why:
<br>
<p>The issue is that I used to have www.open-mpi.org.cfg generated by  
<br>
configure.ac, so there was a file named www.open-mpi.org.cfg.in, and  
<br>
the .cfg version was svn:ignore'd.  I changed the build system (for  
<br>
various uninteresting reasons) to *not* generate the .cfg file -- so I  
<br>
svn rm'ed the .cfg.in file and svn add'ed the .cfg file.  Hence, when  
<br>
you &quot;svn up&quot;, the old, generated .cfg file is there that svn now has  
<br>
now knowledge of (because it's no longer svn:ignore'd).  Being  
<br>
conservative, svn says &quot;hmm, I need to put in a .cfg file, but there's  
<br>
already a file there -- I won't overwrite it and will let a human  
<br>
figure it out.&quot;
