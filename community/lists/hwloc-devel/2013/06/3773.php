<p>
We're having a discussion (again :-) ) about moving OMPI to a DVCS.
<br>
<p>The short conclusion of the long conversation is: the OMPI dev community would be much more comfortable moving to a DVCS if they could see some success with other OMPI projects (e.g., hwloc and/or MTT).
<br>
<p>Would hwloc be interested in moving to git?  This would mean:
<br>
<p>1. converting the existing svn to git
<br>
&nbsp;&nbsp;&nbsp;--&gt; including all historical log messages that refer to &quot;rXXXX&quot;
<br>
2. converting the existing trac to git
<br>
&nbsp;&nbsp;&nbsp;--&gt; including all trac tickets, comments, and wiki pages that refer to &quot;rXXXX&quot;
<br>
<p>The OMPI devs -- who are mostly unfamiliar with git -- would like to see some close-to-home successes with git over a period of time that don't require heavy administrative maintenance over time (one of the pushback issues was that some organizations have hired full-time people to maintain/fix git repositories when they break/become unusable).
<br>
<p>Interested?
