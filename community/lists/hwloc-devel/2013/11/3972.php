<span class="quotelev2">&gt;&gt;
</span><br>
<span class="quotelev2">&gt;&gt;         The Hardware Locality (hwloc) team is pleased to announce the
</span><br>
<span class="quotelev2">&gt;&gt;         first
</span><br>
<span class="quotelev2">&gt;&gt;         release candidate for v1.8:
</span><br>
<span class="quotelev2">&gt;&gt;
</span><br>
<span class="quotelev2">&gt;&gt;            <a href="http://www.open-mpi.org/projects/hwloc/">http://www.open-mpi.org/projects/hwloc/</a>
</span><br>
<span class="quotelev2">&gt;&gt;
</span><br>
<span class="quotelev2">&gt;&gt;         v1.8rc1 is the first milestone of a major feature release.
</span><br>
<span class="quotelev2">&gt;&gt;         It adds PCI discovery on Linux without dependencies on
</span><br>
<span class="quotelev2">&gt;&gt;         external libs,
</span><br>
<span class="quotelev2">&gt;&gt;         a new API to manipulate differences between very similar
</span><br>
<span class="quotelev2">&gt;&gt;         topologies,
</span><br>
<span class="quotelev2">&gt;&gt;         multiple improvements to command-line tools, and more.
</span><br>
<span class="quotelev2">&gt;&gt;
</span><br>
<span class="quotelev2">&gt;&gt;         * New components
</span><br>
<span class="quotelev2">&gt;&gt;           + Add the &quot;linuxpci&quot; component that always works on Linux
</span><br>
<span class="quotelev2">&gt;&gt;         even when
</span><br>
<span class="quotelev2">&gt;&gt;             libpciaccess and libpci aren't available (and even with a
</span><br>
<span class="quotelev2">&gt;&gt;         modified
</span><br>
<span class="quotelev2">&gt;&gt;             file-system root). By default the old &quot;pci&quot; component
</span><br>
<span class="quotelev2">&gt;&gt;         runs first
</span><br>
<span class="quotelev2">&gt;&gt;             because &quot;linuxpci&quot; lacks device names (obj-&gt;name is
</span><br>
<span class="quotelev2">&gt;&gt;         always NULL).
</span><br>
<span class="quotelev2">&gt;&gt;         * API
</span><br>
<span class="quotelev2">&gt;&gt;           + Add the topology difference API in hwloc/diff.h for
</span><br>
<span class="quotelev2">&gt;&gt;         manipulating
</span><br>
<span class="quotelev2">&gt;&gt;             many similar topologies.
</span><br>
<span class="quotelev2">&gt;&gt;           + Add hwloc_topology_dup() for duplicating an entire topology.
</span><br>
<span class="quotelev2">&gt;&gt;           + hwloc.h and hwloc/helper.h have been reorganized to
</span><br>
<span class="quotelev2">&gt;&gt;         clarify the
</span><br>
<span class="quotelev2">&gt;&gt;             documentation sections. The actual inline code has moved
</span><br>
<span class="quotelev2">&gt;&gt;         out of hwloc.h
</span><br>
<span class="quotelev2">&gt;&gt;             into the new hwloc/inlines.h.
</span><br>
<span class="quotelev2">&gt;&gt;           + Deprecated functions are now in hwloc/deprecated.h, and
</span><br>
<span class="quotelev2">&gt;&gt;         not in the
</span><br>
<span class="quotelev2">&gt;&gt;             official documentation anymore.
</span><br>
<span class="quotelev2">&gt;&gt;         * Tools
</span><br>
<span class="quotelev2">&gt;&gt;           + Add hwloc-diff and hwloc-patch tools together with the
</span><br>
<span class="quotelev2">&gt;&gt;         new diff API.
</span><br>
<span class="quotelev2">&gt;&gt;           + Add hwloc-compress-dir to (de)compress an entire
</span><br>
<span class="quotelev2">&gt;&gt;         directory of XML files
</span><br>
<span class="quotelev2">&gt;&gt;             using hwloc-diff and hwloc-patch.
</span><br>
<span class="quotelev2">&gt;&gt;           + Object colors in the graphical output of lstopo may be
</span><br>
<span class="quotelev2">&gt;&gt;         changed by adding
</span><br>
<span class="quotelev2">&gt;&gt;             a &quot;lstopoStyle&quot; info attribute. See CUSTOM COLORS in the
</span><br>
<span class="quotelev2">&gt;&gt;         lstopo(1) manpage
</span><br>
<span class="quotelev2">&gt;&gt;             for details. Thanks to Jirka Hladky for discussing the idea.
</span><br>
<span class="quotelev2">&gt;&gt;           + hwloc-gather-topology may now gather I/O-related files on
</span><br>
<span class="quotelev2">&gt;&gt;         Linux when
</span><br>
<span class="quotelev2">&gt;&gt;             --io is given. Only the linuxpci component supports
</span><br>
<span class="quotelev2">&gt;&gt;         discovering I/O
</span><br>
<span class="quotelev2">&gt;&gt;             objects from these extended tarballs.
</span><br>
<span class="quotelev2">&gt;&gt;           + hwloc-annotate now supports --ri to remove/replace info
</span><br>
<span class="quotelev2">&gt;&gt;         attributes with
</span><br>
<span class="quotelev2">&gt;&gt;             a given name.
</span><br>
<span class="quotelev2">&gt;&gt;           + hwloc-info supports &quot;root&quot; and &quot;all&quot; special locations
</span><br>
<span class="quotelev2">&gt;&gt;         for dumping
</span><br>
<span class="quotelev2">&gt;&gt;             information about the root object.
</span><br>
<span class="quotelev2">&gt;&gt;           + lstopo now supports --append-legend to append custom
</span><br>
<span class="quotelev2">&gt;&gt;         lines of text
</span><br>
<span class="quotelev2">&gt;&gt;             to the legend in the graphical output. Thanks to Jirka
</span><br>
<span class="quotelev2">&gt;&gt;         Hladky for
</span><br>
<span class="quotelev2">&gt;&gt;             discussing the idea.
</span><br>
<span class="quotelev2">&gt;&gt;           + hwloc-calc and friends have a more robust parsing of
</span><br>
<span class="quotelev2">&gt;&gt;         locations given
</span><br>
<span class="quotelev2">&gt;&gt;             on the command-line and they report useful error messages
</span><br>
<span class="quotelev2">&gt;&gt;         about it.
</span><br>
<span class="quotelev2">&gt;&gt;           + Add --whole-system to hwloc-bind, hwloc-calc,
</span><br>
<span class="quotelev2">&gt;&gt;         hwloc-distances and
</span><br>
<span class="quotelev2">&gt;&gt;             hwloc-distrib, and add --restrict to hwloc-bind for
</span><br>
<span class="quotelev2">&gt;&gt;         uniformity among
</span><br>
<span class="quotelev2">&gt;&gt;             tools.
</span><br>
<span class="quotelev2">&gt;&gt;         * Misc
</span><br>
<span class="quotelev2">&gt;&gt;           + Calling hwloc_topology_load() or hwloc_topology_set_*()
</span><br>
<span class="quotelev2">&gt;&gt;         on an already
</span><br>
<span class="quotelev2">&gt;&gt;             loaded topology now returns an error (deprecated since
</span><br>
<span class="quotelev2">&gt;&gt;         release 1.6.1).
</span><br>
<span class="quotelev2">&gt;&gt;           + Fix the initialisation of cpusets and nodesets in Group
</span><br>
<span class="quotelev2">&gt;&gt;         objects added
</span><br>
<span class="quotelev2">&gt;&gt;             when inserting PCI hostbridges.
</span><br>
<span class="quotelev2">&gt;&gt;           + Never merge Group objects that were added explicitly by
</span><br>
<span class="quotelev2">&gt;&gt;         the user with
</span><br>
<span class="quotelev2">&gt;&gt;             hwloc_custom_insert_group_object_by_parent().
</span><br>
<span class="quotelev2">&gt;&gt;           + Add a sanity check during dynamic plugin loading to
</span><br>
<span class="quotelev2">&gt;&gt;         prevent some
</span><br>
<span class="quotelev2">&gt;&gt;             crashes when hwloc is dynamically loaded by another
</span><br>
<span class="quotelev2">&gt;&gt;         plugin mechanisms.
</span><br>
<span class="quotelev2">&gt;&gt;           + Add --with-hwloc-plugins-path to specify the install/load
</span><br>
<span class="quotelev2">&gt;&gt;         directories
</span><br>
<span class="quotelev2">&gt;&gt;             of plugins.
</span><br>
<span class="quotelev2">&gt;&gt;           + Add the MICSerialNumber info attribute to the root object
</span><br>
<span class="quotelev2">&gt;&gt;         when running
</span><br>
<span class="quotelev2">&gt;&gt;             hwloc inside a Xeon Phi to match the same attribute in
</span><br>
<span class="quotelev2">&gt;&gt;         the MIC OS device
</span><br>
<span class="quotelev2">&gt;&gt;             when running in the host.
</span><br>
<span class="quotelev2">&gt;&gt;
</span><br>
<span class="quotelev2">&gt;&gt;         --
</span><br>
<span class="quotelev2">&gt;&gt;         Brice
