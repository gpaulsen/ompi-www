<span class="quotelev2">&gt;&gt; On Wed, Nov 6, 2013 at 3:06 PM, Brice Goglin &lt;Brice.Goglin_at_[hidden]&gt;wrote:
</span><br>
<span class="quotelev2">&gt;&gt;
</span><br>
<span class="quotelev3">&gt;&gt;&gt; The Hardware Locality (hwloc) team is pleased to announce the first
</span><br>
<span class="quotelev3">&gt;&gt;&gt; release candidate for v1.8:
</span><br>
<span class="quotelev3">&gt;&gt;&gt;
</span><br>
<span class="quotelev3">&gt;&gt;&gt;    <a href="http://www.open-mpi.org/projects/hwloc/">http://www.open-mpi.org/projects/hwloc/</a>
</span><br>
<span class="quotelev3">&gt;&gt;&gt;
</span><br>
<span class="quotelev3">&gt;&gt;&gt; v1.8rc1 is the first milestone of a major feature release.
</span><br>
<span class="quotelev3">&gt;&gt;&gt; It adds PCI discovery on Linux without dependencies on external libs,
</span><br>
<span class="quotelev3">&gt;&gt;&gt; a new API to manipulate differences between very similar topologies,
</span><br>
<span class="quotelev3">&gt;&gt;&gt; multiple improvements to command-line tools, and more.
</span><br>
<span class="quotelev3">&gt;&gt;&gt;
</span><br>
<span class="quotelev3">&gt;&gt;&gt; * New components
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + Add the &quot;linuxpci&quot; component that always works on Linux even when
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     libpciaccess and libpci aren't available (and even with a modified
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     file-system root). By default the old &quot;pci&quot; component runs first
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     because &quot;linuxpci&quot; lacks device names (obj-&gt;name is always NULL).
</span><br>
<span class="quotelev3">&gt;&gt;&gt; * API
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + Add the topology difference API in hwloc/diff.h for manipulating
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     many similar topologies.
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + Add hwloc_topology_dup() for duplicating an entire topology.
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + hwloc.h and hwloc/helper.h have been reorganized to clarify the
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     documentation sections. The actual inline code has moved out of
</span><br>
<span class="quotelev3">&gt;&gt;&gt; hwloc.h
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     into the new hwloc/inlines.h.
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + Deprecated functions are now in hwloc/deprecated.h, and not in the
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     official documentation anymore.
</span><br>
<span class="quotelev3">&gt;&gt;&gt; * Tools
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + Add hwloc-diff and hwloc-patch tools together with the new diff API.
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + Add hwloc-compress-dir to (de)compress an entire directory of XML
</span><br>
<span class="quotelev3">&gt;&gt;&gt; files
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     using hwloc-diff and hwloc-patch.
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + Object colors in the graphical output of lstopo may be changed by
</span><br>
<span class="quotelev3">&gt;&gt;&gt; adding
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     a &quot;lstopoStyle&quot; info attribute. See CUSTOM COLORS in the lstopo(1)
</span><br>
<span class="quotelev3">&gt;&gt;&gt; manpage
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     for details. Thanks to Jirka Hladky for discussing the idea.
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + hwloc-gather-topology may now gather I/O-related files on Linux when
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     --io is given. Only the linuxpci component supports discovering I/O
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     objects from these extended tarballs.
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + hwloc-annotate now supports --ri to remove/replace info attributes
</span><br>
<span class="quotelev3">&gt;&gt;&gt; with
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     a given name.
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + hwloc-info supports &quot;root&quot; and &quot;all&quot; special locations for dumping
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     information about the root object.
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + lstopo now supports --append-legend to append custom lines of text
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     to the legend in the graphical output. Thanks to Jirka Hladky for
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     discussing the idea.
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + hwloc-calc and friends have a more robust parsing of locations given
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     on the command-line and they report useful error messages about it.
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + Add --whole-system to hwloc-bind, hwloc-calc, hwloc-distances and
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     hwloc-distrib, and add --restrict to hwloc-bind for uniformity among
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     tools.
</span><br>
<span class="quotelev3">&gt;&gt;&gt; * Misc
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + Calling hwloc_topology_load() or hwloc_topology_set_*() on an already
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     loaded topology now returns an error (deprecated since release
</span><br>
<span class="quotelev3">&gt;&gt;&gt; 1.6.1).
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + Fix the initialisation of cpusets and nodesets in Group objects added
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     when inserting PCI hostbridges.
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + Never merge Group objects that were added explicitly by the user with
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     hwloc_custom_insert_group_object_by_parent().
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + Add a sanity check during dynamic plugin loading to prevent some
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     crashes when hwloc is dynamically loaded by another plugin
</span><br>
<span class="quotelev3">&gt;&gt;&gt; mechanisms.
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + Add --with-hwloc-plugins-path to specify the install/load directories
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     of plugins.
</span><br>
<span class="quotelev3">&gt;&gt;&gt;   + Add the MICSerialNumber info attribute to the root object when
</span><br>
<span class="quotelev3">&gt;&gt;&gt; running
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     hwloc inside a Xeon Phi to match the same attribute in the MIC OS
</span><br>
<span class="quotelev3">&gt;&gt;&gt; device
</span><br>
<span class="quotelev3">&gt;&gt;&gt;     when running in the host.
</span><br>
<span class="quotelev3">&gt;&gt;&gt;
</span><br>
<span class="quotelev3">&gt;&gt;&gt; --
</span><br>
<span class="quotelev3">&gt;&gt;&gt; Brice
