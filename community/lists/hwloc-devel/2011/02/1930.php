<p>On 14/02/11 05:04, LdK wrote:
<br>
<p><span class="quotelev1">&gt; Why don't you use CMake instead of autoconf/automake
</span><br>
<span class="quotelev1">&gt; old couple as build system ?
</span><br>
<p>Any extra dependency that's needed for a piece of software
<br>
to be installed decreases its attractiveness to potential
<br>
users and (very important to hwloc) integrators.
<br>
<p>The benefit to sysadmins such as myself of packages
<br>
using autotools is that they should work with whatever
<br>
the system has already and not require another package
<br>
to be installed.
<br>
<p>Whenever we come across a piece of software we need to
<br>
install here that uses CMake there's a collective sigh
<br>
of &quot;oh no, not again&quot;..
<br>
<p>Think of using autotools as a way of increasing your
<br>
karma by taking a little bit more pain in return for
<br>
decreasing a whole lot more sysadmins pain.. ;-)
<br>
<p>cheers!
