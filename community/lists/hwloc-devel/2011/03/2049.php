<p>
Do we want to enroll hwloc in the Coverity Scan tool?
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://scan.coverity.com/developers-faq.html">http://scan.coverity.com/developers-faq.html</a>
<br>
and
<br>
&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://svn.open-mpi.org/trac/ompi/wiki/Coverity">https://svn.open-mpi.org/trac/ompi/wiki/Coverity</a>
<br>
<p>Scan is a static source code analysis tool that finds things like resource leaks, dead code, insecure coding, ...and a huge number of other problems.  It's surprisingly good at what it does.
<br>
<p>Coverity provides Scan results to Open MPI on a &quot;partnership&quot; basis; we like their tool a lot (although we don't use it nearly as much as we should :-\ ).  I don't know if hwloc would fall under the same agreements we have with Coverity or whether we just enroll hwloc as a &quot;normal&quot; open source project, but I could find out, if we're interested.
