<p>The Open Grid Scheduler Project is releasing a new major release: Grid
<br>
Engine 2011.11. We are using the open source model that was used by
<br>
Sun Grid Engine (2001 - 2009), and we offer open source (not open
<br>
core) Grid Engine with *optional* technical support. We have a growing
<br>
Grid Engine Community and joined by companies and system integrators
<br>
in the Grid Engine ecosystem.
<br>
<p><p>New features
<br>
============
<br>
<p>* Berkeley DB Spooling Directory can now be placed on NFS (NFSv3 or
<br>
older, basically any modern network filesystems)
<br>
<p>* Portable Hardware Locality Library (was under alpha/beta since April 2011)
<br>
<p>* CUDA GPU load sensor - uses NVIDIA Management Library (NVML)
<br>
<p>* User notification mails can be sent from a configurable user ID
<br>
<p>* Job exit status available to epilog - via $SGE_JOBEXIT_STAT
<br>
<p>* ARM Linux support - ARMv7.
<br>
<p>* qmake upgraded to version 3.82
<br>
<p>* Support for Linux 3.0
<br>
<p>* Perfstat library used on AIX, retiring ibm-loadsensor
<br>
<p>* Support for newer AIX versions
<br>
<p>* Tango qmon icons
<br>
<p>* Code quality improvements - static code verifier used for testing
<br>
<p>PQS API code cleaned up and we are releasing the PQS API Scheduler
<br>
Plugin Interface as technology preview.
<br>
<p><p>SC11 Demo
<br>
=========
<br>
Gridcore/Gompute is kind enough to offer part of their 20x20 booth for
<br>
the Grid Engine 2011.11 demo. I am going to remotely give a
<br>
presentation on the new features of Grid Engine 2011.11, and also the
<br>
future of open source Grid Engine.
<br>
<p>The proposed time slots are: 12:00 and 15:00 on Tuesday and Wedneday.
<br>
<p>Please sign up for the presentation at the Gridcore/Gompute booth (#6002).
<br>
