<p>
FYI, I may revert this before the final v1.5.
<br>
<p>Doxygen &gt;= 1.8 does not put code fragments inside &lt;pre&gt; tags anymore.
<br>
They use the CSS to make sure indentation is preserved (we should update
<br>
out own CSS), but that doesn't seem to work with w3m and lynx. That's
<br>
why hello.c is not indented anymore in the README. Currently under
<br>
discussion at <a href="https://bugzilla.gnome.org/show_bug.cgi?id=680073">https://bugzilla.gnome.org/show_bug.cgi?id=680073</a> but I
<br>
don't expect much.
<br>
<p>The patch below fixes indentation in the README (verbatim forces &lt;pre&gt;
<br>
tags) but it removes doxygen syntax colorizing in the HTML and PDF
<br>
outputs, which is bad IMHO.
<br>
<p>One workaround would be to modify the doxygen HTML code before passing
<br>
it to w3m/lynx and try to fix this mess. We'll see.
<br>
We may need such a step anyway because w3m does not export everything
<br>
well. For instance &amp;ndash; (generated by doxygen in place of -- since
<br>
1.8) and &#169; are exported as &quot;?&quot;.
<br>
<p>Another way would be to manually insert hello.c in the README in place
<br>
of the doxygen-generated code.
<br>
