<p>One of De Morgan's Laws:
<br>
&nbsp;&nbsp;&nbsp;&nbsp;NOT (A AND B) = (NOT A) OR (NOT B)
<br>
<p>Applied to give:
<br>
&nbsp;&nbsp;&nbsp;&nbsp;NOT (TEST1_FAIL AND TEST2_FAIL)
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;= (NOT TEST1_FAIL) OR (NOT TEST2_FAIL)
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;= TEST1_PASS OR TEST2_PASS
<br>
