<p>It sounds like what you have is a conflict between the policies of 
<br>
Debian (and hence Ubuntu) and the expectations of RHEL/CentOS users.
<br>
<p>Debian Policy is fairly clear on this matter:
<br>
<p># 11.8.1 Providing X support and package priorities
<br>
#
<br>
# Programs that can be configured with support for the X Window System
<br>
# must be configured to do so and must declare any package
<br>
# dependencies necessary to satisfy their runtime requirements when
<br>
# using the X Window System.  [...]
<br>
<p>It says you can split it into a separate package to provide GUI 
<br>
functionality *only* if the &quot;package is of higher priority than the X 
<br>
packages on which it depends&quot; (which I suspect is not the usual case).
