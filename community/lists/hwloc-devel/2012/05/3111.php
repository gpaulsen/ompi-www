Heads up: Indiana University will be migrating Open MPI's Subversion, Trac, and OpenGrok services (to include the hwloc SVN and Trac) to new servers on Friday morning, US Eastern time, June 1, 2012 (i.e., in 2 days).  
<br>
<p>The purpose of this migration is to retire some (really) old server hardware.  Delivered performance from the new server hardware probably won't be significantly impacted (i.e., it probably won't be significantly faster or slower -- this is more of an infrastructure change than an upgrade).
