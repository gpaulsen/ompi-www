<br>
<p>install-data: install-data-am
<br>
<p>install-data-am: install-dist_APPLICATIONSDATA install-man
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;@$(NORMAL_INSTALL)
