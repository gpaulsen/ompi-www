&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;$(MAKE) $(AM_MAKEFLAGS) install-data-hook
<br>
<p>Looks like it's officially documented that install-data depends on
