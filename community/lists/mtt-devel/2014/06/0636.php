<p>
+1 for using branches : branch usage is less error prone plus git makes
<br>
branching unexpensive.
<br>
<p>as far as i am concerned, i'd rather have the default master branch is
<br>
the for the &quot;stable&quot; version
<br>
and have one branch called devel (or dev, or whatever) :
<br>
- git clone =&gt; get the stable (aka master) branch by default (safe by
<br>
default)
<br>
- if you use the devel branch, one can only assume you know what you are
<br>
doing ...
<br>
<p>That being said, tags on the master branch is a good practice
