<p>
I met with Joseph Cottam (Grad student in my lab at IU) yesterday  
<br>
about MTT visualization. He is working on some new visualization  
<br>
techniques and wants to apply them to the MTT dataset.
<br>
<p>Since we are ramping up to a v1.3 release we want to visualization to  
<br>
support this effort. So we want to make sure that the visualization  
<br>
will meet the development community's needs. We should probably ask  
<br>
the devel-core list, but I thought I would start some of the  
<br>
discussion here to make sure I am asking the right questions of the  
<br>
group.
<br>
<p>To start I have some basic questions:
<br>
&nbsp;&nbsp;- How does Open MPI determine that it is stable enough to release?
<br>
&nbsp;&nbsp;- What dimensions of testing are most/least important (i.e.,  
<br>
platforms, compilers, feature sets, scale, ...)?
<br>
&nbsp;&nbsp;- What other questions would be useful to answer with regard to  
<br>
testing (thinking completely outside of the box)?
<br>
&nbsp;&nbsp;&nbsp;&nbsp;* Example: Are we testing a specific platform/configuration set  
<br>
too much/too little?
<br>
&nbsp;&nbsp;- Other questions you think we should pose to the group?
<br>
<p>We are currently feeling out the domain of possibilities, but hope to  
<br>
start doing some sketching some ideas in another week or so. This work  
<br>
should proceed fairly quickly since we are targeting a paper about  
<br>
this for the ACM Symposium on Software Visualization (<a href="http://www.softvis.org/">http://www.softvis.org/</a> 
<br>
) which is due in early April. How is that for expecting success :)
<br>
<p>Cheers,
<br>
Josh
