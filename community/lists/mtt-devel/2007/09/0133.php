<p>
The group I worked with in undergrad just setup Cacti for monitoring  
<br>
three cluster setups they have running. It is pretty snazzy. One of  
<br>
the cool things is the dynamic zoom feature they have going on.
<br>
<p>Go to: (username/password -&gt; guest/guest) [This is publicly known  
<br>
username/password so no worries about posting to a listserv, just  
<br>
don't pass around too much as I don't want to overwhelm their servers]
<br>
&nbsp;&nbsp;&nbsp;<a href="http://hp.cluster.earlham.edu/cacti/">http://hp.cluster.earlham.edu/cacti/</a>
<br>
<p>Click on 'Servers' -&gt; 'Host: Hopper'
<br>
<p>Click on the magnifying glass beside the lo0 graph (or any of the  
<br>
graphs).
<br>
<p>Use your mouse to make a bounding box in the graph by clicking and  
<br>
dragging until the section you want to look at is in the red box.
<br>
<p>When you release your mouse button it will zoon in the graph.
<br>
<p><p>I thought this was totally cool for a variety of reasons, but the UI  
<br>
of the graph zoom may influence how we want to integrate such  
<br>
functionality in the MTT reporter.
