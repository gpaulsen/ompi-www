<p>
Short version
<br>
=============
<br>
<p>The server for this mailing list will be migrating sometime soon (the exact timing is not fully predictable).  Three things you need to know:
<br>
<p>1. We'll send a &quot;This list is now closed for migration&quot; last message when the migration starts
<br>
2. We'll send a &quot;This list is now open again&quot; first message when the migration completes
<br>
3. The list email address will move from @open-mpi.org to @lists.open-mpi.org
<br>
<p>More detail
<br>
===========
<br>
<p>The Open MPI hosting infrastructure is slowly moving away from its home of 10+ years: our gracious hosts at Indiana University (thank you for all the help and support, IU!).  The next pieces to migrate are the Open MPI project mailing lists (including this one).
<br>
<p>The exact timing of the migration depends on our new hosting provider vendor; it's quite difficult to give an exact timeline.  The procedure will generally be something like this:
<br>
<p>1. Send the final &quot;This list is now closed!&quot; email across this list
<br>
2. Shut off all incoming mail to the list
<br>
3. Shut down the web pages that allow users to make changes to the list
<br>
4. Bundle up all the list data and send it to our new hosting provider
<br>
5. Work with the provider to get the new lists online
<br>
6. Send a &quot;This list is now open again!&quot; email across the list
<br>
<p>As noted above, we're changing the hostnames on the mailing lists to @lists.open-mpi.org so that we can de-couple the mailing lists from the rest of the web hosting infrastructure.  Please update your addressbook and mail filters appropriately.
<br>
<p>Webified archives of the mailing lists will continue to be available:
<br>
<p>1. Once the migration completes, the existing web archives (under, for example, <a href="https://www.open-mpi.org/community/lists/users/">https://www.open-mpi.org/community/lists/users/</a>) will continue to be available, but they'll be frozen -- no new messages will be added there.  Specifically: links to old posts will continue to work.
<br>
2. New web archives for all the lists -- to include all the old posts -- will become available elsewhere.  Specifics will be included in the &quot;The list is now open again!&quot; mail.
