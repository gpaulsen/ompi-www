<p>
We are seeing more activity with MTT development, and there is a desire to
<br>
push to a formal release at some point in the not-to-distant future. As
<br>
such, I think it is time to clean up/out the issues on GitHub. Quite a
<br>
number of those issues are feature ideas that we came up with that were
<br>
never investigated.
<br>
<p>So I propose that we do the following. All of which is open for discussion,
<br>
and I can take point on making the changes once we settle on what we want.
<br>
<p>*Milestones*:
<br>
&nbsp;1) Mark all existing milestones as completed.
<br>
&nbsp;2) Create a v4.0 milestone to track development to the 'first' release
<br>
(why not v1.0 - see [A] below)
<br>
&nbsp;3) Any issue filed against &quot;Future&quot; will be filed instead against
<br>
&quot;ArchivedFuture&quot;
<br>
<p>*Labels*:
<br>
&nbsp;1) Create a &quot;work in progress&quot; label - for PRs in progress
<br>
&nbsp;2) Create a label for each of the major parts of MTT
<br>
&nbsp;&nbsp;&nbsp;&nbsp;- &quot;Perl Client&quot;
<br>
&nbsp;&nbsp;&nbsp;&nbsp;- &quot;Python Client&quot;
<br>
&nbsp;&nbsp;&nbsp;&nbsp;- &quot;Reporter&quot;
<br>
&nbsp;&nbsp;&nbsp;&nbsp;- &quot;Database&quot;
<br>
&nbsp;&nbsp;&nbsp;&nbsp;- &quot;Server&quot;
<br>
&nbsp;3) Create a &quot;Wishlist&quot; label where we can label wild enhancement ideas
<br>
that we would like, but know we have no time to pursue in the near future.
<br>
That way it is easy to get a list of neat things to do for people wanting
<br>
to jump in.
<br>
&nbsp;4) Create an &quot;Archive&quot; label
<br>
<p>*Issues:*
<br>
&nbsp;1) All existing issues get labeled with &quot;Archive&quot; in addition to their
<br>
existing labels
<br>
<p><p>What do folks think? Did I miss anything?
<br>
<p>Thanks,
<br>
Josh
<br>
<p><p>[A] There was informal v1.0 / v2.0 / v3.0 waypoints in the history. I
<br>
