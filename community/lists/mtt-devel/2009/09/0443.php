<p><p>On Tue, Sep/29/2009 09:21:30PM, Mike Dubman wrote:
<br>
<span class="quotelev1">&gt;    Hello guys and gals,
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    We have completed development and testing of Google DataStore support in
</span><br>
<span class="quotelev1">&gt;    MTT and are glad to submit it for community tests.
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    New Files:
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    ========
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    The following new files were added to support GDS inside MTT:
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    1. client/bquery.pl
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    ** Perl-based GDS client, provides basic DB querying/fetching
</span><br>
<span class="quotelev1">&gt;    capabilities. It creates resultset (files in YAML format) from
</span><br>
<span class="quotelev1">&gt;    user-provided sql-like query
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    2. client/breport.pl
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    ** Perl-based report tool, creates excel reports from yaml files,
</span><br>
<span class="quotelev1">&gt;    generated by bquery.pl tool.
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    3. client/custom_launchers/
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    ** For brave only: custom launchers for non-standard HPC, mpi-based
</span><br>
<span class="quotelev1">&gt;    applications
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    4. lib/MTT/Reporter/MTTGDS.pm
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    ** GDS Reporter, saves mtt results to GDS (see samples/gds-demo.ini for
</span><br>
<span class="quotelev1">&gt;    configuration examples)
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    5. lib/MTT/Utils/ClusterInfo.pm
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    ** Helper library to gather node hw/sw configuration information which is
</span><br>
<span class="quotelev1">&gt;    saved in GDS together with tests results.
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    6. New TestResults analyzers for HPC applications:
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    ** lib/MTT/test/Analyze/Performance/Fluent.pm
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    ** lib/MTT/test/Analyze/Performance/HPCC.pm
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    ** lib/MTT/test/Analyze/Performance/HPLGDS.pm
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    ** lib/MTT/test/Analyze/Performance/OpenFoam.pm
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    ** lib/MTT/test/Analyze/Performance/PamCrash.pm
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    7. samples/gds-demo.ini
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    ** Example of howto configure GDS in MTT and run bquery/breport tools at
</span><br>
<span class="quotelev1">&gt;    the end of MTT session
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    8. server/gds/
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    ** GDS backend part, which is running at Google and providing Object to
</span><br>
<span class="quotelev1">&gt;    YAML, YAML to Object translation service as well as helper code for
</span><br>
<span class="quotelev1">&gt;    bquery.pl DB client.
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    9. docs/gds/
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    ** Various documentation
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    Known Issues and Limitations:
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    ======================
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    * lib/MTT/Utils/ClusterInfo.pm uses &quot;sudo&quot; command to gather node`s
</span><br>
<span class="quotelev1">&gt;    hardware information.
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    * When using client/custom_launchers/ to run tests, it is impossible to
</span><br>
<span class="quotelev1">&gt;    kill the test application when timeout reached.
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    How to start using MTToGDS:
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    ======================
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    * Contact Jeff to provide you with GDS login/password which is needed for
</span><br>
<span class="quotelev1">&gt;    querying/saving to DB (<a href="http://open-mpi-mtt.appspot.com">http://open-mpi-mtt.appspot.com</a>)
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    * See samples/gds-demo.ini for configuration examples as well as for DB
</span><br>
<span class="quotelev1">&gt;    querying and reports generation.
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    * Read Google GQL syntax documentation and use it with bquery.pl in order
</span><br>
<span class="quotelev1">&gt;    to query&amp;fetch objects from GDB.
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    * The following perl modules are required for all MTToGDS components:
</span><br>
<span class="quotelev1">&gt;    *libYAML
</span><br>
<span class="quotelev1">&gt;    YAML::Syck
</span><br>
<span class="quotelev1">&gt;    YAML::XS
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    for breport:
</span><br>
<span class="quotelev1">&gt;    GD::Graph
</span><br>
<span class="quotelev1">&gt;    Spreadsheet::WriteExcel
</span><br>
<span class="quotelev1">&gt; 
</span><br>
<span class="quotelev1">&gt;    You can install it on linux systems with yum as following:
</span><br>
<span class="quotelev1">&gt;    yum install perl-libyaml perl-YAML-Syck perl-YAML-XS perl-GD-Graph
</span><br>
<span class="quotelev1">&gt;    perl-Spreadsheet-WriteExcel
</span><br>
<span class="quotelev1">&gt; 
</span><br>
