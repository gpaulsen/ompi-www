<p>
The Open MPI Team, representing a consortium of research, academic, and industry partners, is pleased to announce the release of Open MPI version 1.8.1.
<br>
<p>*** This release includes a single critical bug fix for users who run MPI jobs
<br>
&nbsp;&nbsp;&nbsp;&nbsp;as the &quot;root&quot; user.  It is otherwise identical to the Open MPI v1.8 release.
<br>
&nbsp;&nbsp;&nbsp;&nbsp;See <a href="https://svn.open-mpi.org/trac/ompi/ticket/4536">https://svn.open-mpi.org/trac/ompi/ticket/4536</a> for more details.
<br>
<p>Version 1.8.1 can be downloaded from:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.open-mpi.org/software/ompi/v1.8/">http://www.open-mpi.org/software/ompi/v1.8/</a>
<br>
<p>Note that v1.8.2 is expected to be released within the next few weeks, and will contain the usual bug fixes and end user-suggested improvements.  The Open MPI team felt that this critical bug warranted an immediate release, but did not want to interrupt the normal post-v1.8 bug fix/improvement schedule already in progress.  Hence, we are releasing v1.8.1 with this one critical bug fix, and keeping on track with all other fixes for the next release in a few weeks.
<br>
<p>If you do not run Open MPI jobs as root, you do not need the v1.8.1 release; you can wait for v1.8.2.
<br>
<p>As usual, MPI application ABI will be preserved through the v1.8.x series.
