<p>
When Open MPI version 1.1.3 was released on January 26th 2007, an  
<br>
intermediate beta source release was unfortunately posted to the Open  
<br>
MPI web site instead of the intended final 1.1.3 release.  What was  
<br>
published was equivalent to Open MPI 1.1 series development as of mid- 
<br>
December 2006 -- it did not include all the most recent 1.1 series  
<br>
bug fixes and improvements from January 2007.
<br>
<p>Hence, the Open MPI Team is announcing the release of Open MPI  
<br>
version 1.1.4.  Version 1.1.4 can be downloaded from the main Open  
<br>
MPI web site or any of its mirrors (mirrors will be updating shortly).
<br>
<p>In short: today's 1.1.4 release is what the 1.1.3 release should have  
<br>
been.  We apologize for the mixup.
<br>
<p>The list of fixes and improvements sent out in the prior announcement  
<br>
e-mail (<a href="http://www.open-mpi.org/community/lists/announce/">http://www.open-mpi.org/community/lists/announce/</a> 
<br>
2007/01/0009.php) is representative of today's 1.1.4 release (as  
<br>
compared to the prior 1.1.2 release).  The NEWS file has been updated  
<br>
to reflect what actually shipped in 1.1.3 and what is &quot;new&quot; in 1.1.4.
<br>
<p>We strongly recommend that all users upgrade to version 1.1.4 if  
<br>
possible.
