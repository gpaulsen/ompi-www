<p>
I'm sure everyone reading this email will be in Denver at SC'13 next week (<a href="http://sc13.supercomputing.org/">http://sc13.supercomputing.org/</a>).  Right?  Of course!
<br>
<p>Many of us from the Open MPI community will be there, and we'd love to chat with real, honest-to-goodness users, admins, and developers who are using Open MPI.  Come say hi!  You can generally find me in one of two places:
<br>
<p>- Dave Goodell and I will be hanging around the Cisco booth (#2535).  
<br>
- George Bosilca and I will be hosting hosting the Open MPI &quot;State of the Union&quot; BOF on Tuesday 12:15-1:15pm in rooms 301-302-303.
<br>
<p>For the BOF, this year we're doing something a little different.  We'd like to ask for your suggestions for topics and your specific questions *before the BOF*:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.open-mpi.org/sc13/">http://www.open-mpi.org/sc13/</a>
<br>
<p>Every year, the SC program committee pesters us to have an interactive BOF.  But we find that that doesn't work well in a large room with many attendees.  So we thought we'd try a &quot;get questions ahead of time&quot; approach and see how that works.  We'll use your feedback to guide what we present at the BOF.
<br>
<p>You can assume that we'll present the normal &quot;current status&quot; and &quot;future roadmap&quot; kind of information.  Use the web form to suggest something specific you'd like to see in either of those general topics, or if you have a specific question you'd like us to answer.
<br>
<p>See you in Denver!
