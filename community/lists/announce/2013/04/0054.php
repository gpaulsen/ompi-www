<p>
The Open MPI Team, representing a consortium of research, academic, and industry partners, is pleased to announce the release of Open MPI version 1.7.1.  This is a bug-fix release driven by two issues in the v1.7.0 release.
<br>
<p>1.7.1
<br>
-----
<br>
<p>- Fixed compile error when --without-memory-manager was specified  on Linux
<br>
- Fixed XRC compile issue in Open Fabrics support.
<br>
<p>Several other minor bugs were fixed.
<br>
<p>Version 1.7.1 can be downloaded from the main Open MPI web site or any of its mirrors.
