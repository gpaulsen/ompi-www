<p>
The Open MPI Project has some exciting news to announce:
<br>
<p>1. Anyone watching the &quot;project members&quot; page on the Open MPI  web site
<br>
may have noticed that the Open MPI Project has now picked up some
<br>
commerical members: Cisco was the first (see more details here:
<br>
<a href="http://www.hpcwire.com/hpc/592267.html">http://www.hpcwire.com/hpc/592267.html</a>) and was followed quickly by
<br>
Voltaire (we didn't do a snazzy press release for Voltaire, but we're
<br>
tremendously excited about both).  Additionally, Sun has announced their
<br>
intent to join the Project (see here:
<br>
<a href="http://blogs.sun.com/roller/page/simons?entry=sun_joins_open_mpi">http://blogs.sun.com/roller/page/simons?entry=sun_joins_open_mpi</a>).  
<br>
<p>Vendor participation has been something that we've actively been working
<br>
towards for the past year.  We believe that the Open MPI Project will
<br>
greatly benefit from leverging the strengths of both the research
<br>
community and industry.  The goal, as it has been from the beginning of
<br>
the Open MPI project, is to produce a rock-solid, high-performance MPI
<br>
implementation that provides production quality to customers and a
<br>
stable platform for experimentation and development to researchers.
<br>
<p>You may also notice that my return address and signature have changed: I
<br>
am now leading up Cisco's Open MPI efforts.
<br>
<p>2. To encourage 3rd party developer and researcher participation in Open
<br>
MPI, Cisco is hosting an Open MPI Developer's Workshop from April 17-20,
<br>
2006 in San Jose, CA, USA.  This will be an extremely detailed,
<br>
technical tutorial about the innards of Open MPI itself.  The intended
<br>
audience is developers who want to work in the Open MPI code base (this
<br>
is *not* a general MPI tutorial; advanced knowledge of MPI, preferably
<br>
in the form of implemenetation experience, is required).  See
<br>
<a href="http://www.open-mpi.org/news/03-14-2006-ompi-workshop.php">http://www.open-mpi.org/news/03-14-2006-ompi-workshop.php</a> for details on
<br>
how to register.
<br>
