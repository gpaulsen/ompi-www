<p>
The Open MPI Team is pleased to announce the availability of 1.0 
<br>
release candidate tarballs on the main web site:
<br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="http://www.open-mpi.org/software/v1.0/">http://www.open-mpi.org/software/v1.0/</a>
<br>
<p>1.0rc1 is currently available.  We will likely have a few more 
<br>
candidates before publishing 1.0 &quot;final.&quot;  The software is considered 
<br>
feature complete for 1.0 (although many more features are planned for 
<br>
post-1.0), but we would love to have 3rd parties test the software in 
<br>
their own environments and send feedback on their results.  Be sure to 
<br>
read the README file in the tarball for a list of release notes.
<br>
<p>Documentation, including instructions on configuring, building, and 
<br>
running Open MPI, is available through the FAQ on the web site.  The 
<br>
content there is continually growing, and is strongly influenced by 
<br>
user feedback.
<br>
<p>Feel free to sign up on the user's mailing list for questions, 
<br>
comments, suggestions, etc.  Please be sure to see the &quot;Getting Help&quot; 
<br>
tab on the main web site for important tips on sending effective 
<br>
problem reports (<a href="http://www.open-mpi.org/community/help/">http://www.open-mpi.org/community/help/</a>).
<br>
<p>Make today an Open MPI day!
