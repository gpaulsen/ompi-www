<p>
The Open MPI Team, representing a consortium of research, academic, and industry partners, is pleased to announce the release of Open MPI version 1.5.3.  This release adds a feature that was accidentally left out of v1.5.2.
<br>
<p>*** Note that the &quot;v1.5.x&quot; series is the &quot;feature development&quot; series for Open MPI.  We consider it fairly stable, but it has not been as time-tested as the mature v1.4.x series.  Production sites may wish to stay with v1.4.x until the v1.5 series transitions to its &quot;mature&quot; counterpart (i.e., the v1.6 series). 
<br>
<p>Version 1.5.3 can be downloaded from the main Open MPI web site or any of its mirrors (mirrors will be updating shortly). 
<br>
<p>Here is a list of changes in v1.5.3 as compared to v1.5.2: 
<br>
<p>1.5.3
<br>
-----
<br>
<p>- Add missing &quot;affinity&quot; MPI extension (i.e., the OMPI_Affinity_str()
<br>
&nbsp;&nbsp;API) that was accidentally left out of the 1.5.2 release.
