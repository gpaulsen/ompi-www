<p>
The Open MPI community is pleased to announce the establishment of a new subproject built upon the Open MPI code base. Using work initially contributed by Cisco Systems, the Open Resilient Cluster Manager is an open-source project released under the Open MPI license focused on development of an &quot;always on&quot; resource manager for systems spanning the range from embedded to very large clusters.
<br>
<p>More information is available at <a href="http://www.open-mpi.org/projects/orcm/">http://www.open-mpi.org/projects/orcm/</a>
<br>
<p>While the initial code base is functional, a production release has not yet been made. Hence, we are not generating nightly tarballs at this time. Interested parties are welcome to checkout the developer's trunk per the directions on the project web site. Announcements regarding eventual production releases (expected to commence in early 2010) will be made on this mailing list.
<br>
<p>Anyone interested in contributing to the effort is most welcome!
