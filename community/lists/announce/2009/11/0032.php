<p>
Several of us from the Open MPI crew will be at SC09; if you're  
<br>
coming, be sure to stop by and say hello!  ...and use the SC09 Fist  
<br>
Bump(tm), of course (<a href="http://www.linux-mag.com/id/7608">http://www.linux-mag.com/id/7608</a>).
<br>
<p>- I'll be hanging in and around the Cisco booth (#1847), but also  
<br>
giving various other booth talks around the floor.  If you miss me,  
<br>
leave your card with the people at the front of the Cisco booth.
<br>
<p>- Dr. Edgar Gabriel from U. Houston, Josh Hursey from Indiana U., and  
<br>
Jens Doleschal from TU Dresden will be presenting Open MPI-related 15- 
<br>
minute talks in the Cisco booth.  Drop by the Cisco booth to see the  
<br>
exact schedule.
<br>
<p>- George Bosilca and myself will be hosting the Open MPI BOF  
<br>
Wednesday, Nov 18th, at 12:15pm, in E145/E146.
<br>
<p>- The Indiana University booth (#2573) will be holding a 2-hour  
<br>
collection of mini-seminars on Open MPI Thursday, Nov 19th, from 10am- 
<br>
noon; stop by for any 15-minute interval to learn fun and interesting  
<br>
things about Open MPI (see <a href="http://sc09.supercomputing.iu.edu/mini-conferences/lumsdaine">http://sc09.supercomputing.iu.edu/mini-conferences/lumsdaine</a>) 
<br>
.
<br>
<p>- Don Kerr from the Sun Open MPI team will be in the Sun booth (#435).
<br>
<p>- The MPI Forum will be holding a BOF Wednesday, Nov 18th, at 5:30pm,  
<br>
in D135/D136 to review MPI-3 efforts and request comments from the  
<br>
user community.  Many Forum members will be there for questions and  
<br>
comments as well.
<br>
<p>- ...and likely other Open MPI members / developers whom I have  
<br>
forgotten to mention (forgive me!)
