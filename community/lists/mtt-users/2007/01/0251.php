All --
<br>
<p>When you're experimenting with the new MTT 2.0 client, be aware that  
<br>
there's a &quot;--trial&quot; option to the MTT client.  This will run through  
<br>
all the tests and do a full run, but any results that are submitted  
<br>
back up to the database are specifically flagged as &quot;trial&quot; results  
<br>
that do not show up in default queries (you can go into the  
<br>
preferences and indicate that you want to see trial runs).
<br>
<p>This allows you to test your MTT setup until you get it right and not  
<br>
&quot;taint&quot; the database with potentially inaccurate results.
