<p>
Short version:
<br>
--------------
<br>
<p>MTT 2.0 tutorial teleconference covering how to use the MTT web  
<br>
interface to query for correctness and performance results and how to  
<br>
use the MTT 2.0 client to run tests.
<br>
<p>Proposed time: Noon, US eastern time, Tuesday, 9 Jan 2007 (right  
<br>
after the OMPI weekly teleconf).  Dial in details pending.  If you  
<br>
want to attend and have a conflict with the proposed time, please let  
<br>
me know ASAP.
<br>
<p>Longer version:
<br>
---------------
<br>
<p>Ethan and I are proposing to host a teleconference next Tuesday  
<br>
(1/9/2007) immediately following the OMPI weekly teleconference  
<br>
(noon, US eastern time) to give a tutorial in MTT 2.0.  The point of  
<br>
the teleconference is to show everyone how to use the reporter  
<br>
because MTT generates a *LOT* of data, and it *REALLY HELPS A LOT* if  
<br>
you understand the basics of how the data is organized before you go  
<br>
searching for information.
<br>
<p>The tutorial will be broken down into two parts:
<br>
<p>1. How to use the MTT reporter (i.e., use the web form to query for  
<br>
correctness and performance regression results in the database).
<br>
<p>2. How to run the MTT client, with particular attention paid to the  
<br>
new features that were introduced since MTT 1.0 (i.e., helping users  
<br>
migrate from 1.x to 2.0).
<br>
<p>We are assuming that there will be a larger smaller number of people  
<br>
interested in #1 than #2.  So it's expected that people may drop off  
<br>
the teleconference after #1.
<br>
