<p>
At the UTK meeting, a few organizations indicated to me that being  
<br>
able to run MTT over a developer's tree is getting to be a higher  
<br>
priority.  So I'd like to ask the developer community exactly what  
<br>
you want.
<br>
<p>MTT is a highly flexible system with lots of available options.  I'm  
<br>
*guessing* that developers don't really want all that flexibility --  
<br>
you just want to run a simple thingy against your development tree  
<br>
and get a simple text report at the end indicating &quot;passed&quot; or &quot;failed&quot;.
<br>
<p>However, at least *some* flexibility is going to be required -- every  
<br>
developer will need to run the tests a different way (different MCA  
<br>
parameters, different number of processes, different set of  
<br>
tests, ...etc.).
<br>
<p>It would be most helpful to Ethan and me if the developers could send  
<br>
us  their thoughts on what you want out of MTT to be able to run  
<br>
against your local development trees.  Here are some suggestions:
<br>
<p>1. A list of desired use-case scenarios (the more detailed, the better)
<br>
<p>2. A list of desired outputs (the more detailed -- to include sample  
<br>
outputs you'd like to see -- the better)
<br>
<p>3. How the flexibility should be effected (command line?  text config  
<br>
file?  ...?)
<br>
<p>4. Suggested [default] list of tests to run (trivial?  and/or intel?   
<br>
and/or ibm?  and/or ...?)
<br>
<p>5. Anything else you can think of.
<br>
<p>Thanks!
