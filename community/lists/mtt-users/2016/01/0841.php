<p>
Greg,
<br>
<p>MTT is a tool that automatically build, install OpenMPI and build and 
<br>
run some test suites,
<br>
and upload the results to a web server.
<br>
<p>note the test suites are *not* part of MTT itself.
<br>
<p>for legal reasons, the test suites are in a private repository, that you 
<br>
could be granted access if you
<br>
fill in the right paperwork (OpenMPI contributor agreement)
<br>
note these test suites are (generally) testing features (e.g. no bugs) 
<br>
and not performance
<br>
<p>I am not sure MTT is a right fit for you. you might want to download and 
<br>
run some MPI benchmark suites.
<br>
popular benchmarks are the OSU benchmark (Ohio State University), and 
<br>
the IMB benchmark (Intel)
<br>
<p>Cheers,
<br>
<p>Gilles
