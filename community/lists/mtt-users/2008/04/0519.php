Folks,
<br>
<p>I added another graphing preference that might make the
<br>
performance graphs easier to grok. If you click the
<br>
[Preferences] link now, there's a &quot;Split graphs&quot; option that
<br>
allows you to split graphs by Halves, Thirds, or Quarters.
<br>
Here's an example of some performance graphs split into
<br>
&quot;Quarters&quot;:
<br>
<p>&nbsp;&nbsp;<a href="http://www.open-mpi.org/mtt/index.php?do_redir=593">http://www.open-mpi.org/mtt/index.php?do_redir=593</a>
<br>
<p>The same data without the &quot;graph splitting&quot; turned on is
<br>
very hard to read towards the left-hand side of the graph:
<br>
<p>&nbsp;&nbsp;<a href="http://www.open-mpi.org/mtt/index.php?do_redir=594">http://www.open-mpi.org/mtt/index.php?do_redir=594</a>
